    <h2>1. Purpose & Scope</h2>
    <h3>1.1 Purpose</h3>
    <p>The purpose of this Complaint Handling Procedures Manual is to establish a clear, fair, and effective system for receiving, reviewing, resolving, and reporting client complaints in relation to Penthian Ltd's services as a Virtual Asset Marketplace operator licensed under the Virtual Asset and Initial Token Offering Services Act 2021 (VAITOS Act) in Mauritius.</p>
    <p>This procedure is designed to:</p>
    <ul>
        <li>Ensure transparency and accountability in handling client concerns.</li>
        <li>Enhance user trust and investor confidence in the Penthian platform.</li>
        <li>Meet the expectations and regulatory requirements of the Financial Services Commission (FSC) of Mauritius.</li>
        <li>Enable timely and constructive responses to client issues, fostering continuous improvement.</li>
    </ul>

    <h3>1.2 Scope</h3>
    <p>This policy applies to:</p>
    <ul>
        <li>All clients, users, and stakeholders of the Penthian platform (retail and institutional).</li>
        <li>Any employee, agent, or third-party contractor acting on behalf of Penthian.</li>
        <li>Complaints related to onboarding, wallet access, KYC/KYB procedures, trading functionality, token listings, smart contract execution, rental distributions, or any other service provided by Penthian.</li>
    </ul>
    <p>It covers:</p>
    <ul>
        <li>Formal and informal complaints</li>
        <li>Verbal or written submissions</li>
        <li>Anonymous complaints (where sufficient information is provided to investigate)</li>
    </ul>
    <p>This procedure does not apply to:</p>
    <ul>
        <li>General inquiries, feedback, or feature suggestions</li>
        <li>Legal disputes already under court or arbitration review</li>
        <li>Complaints involving fraudulent claims or abuse of process, which may be referred to law enforcement or regulators</li>
    </ul>

    <h2>2. Definition of a Complaint</h2>
    <p>For the purpose of this policy, a complaint is defined as:</p>
    <p>Any expression of dissatisfaction, whether oral or written, made by or on behalf of a client, regarding a product, service, process, policy, staff conduct, or platform-related matter, where a response or resolution is expected.</p>
    <p>This includes, but is not limited to:</p>
    <ul>
        <li>Issues with KYC/KYB approval delays</li>
        <li>Disputes over token allocation or purchase failures</li>
        <li>Smart contract errors affecting ownership or liquidity</li>
        <li>Concerns about delayed rental income distributions</li>
        <li>Problems with platform-managed wallets or access tokens</li>
        <li>Dissatisfaction with customer support responsiveness</li>
        <li>Perceived unfair treatment, lack of transparency, or misleading information</li>
        <li>Any potential breach of legal, regulatory, or ethical standards</li>
    </ul>

    <h3>2.1 Categories of Complaints</h3>
    <p>To ensure structured resolution, complaints are categorized into:</p>
    <table>
        <tr>
            <th>Type</th>
            <th>Examples</th>
        </tr>
        <tr>
            <td>Operational</td>
            <td>System downtime, trading errors, wallet issues</td>
        </tr>
        <tr>
            <td>Compliance</td>
            <td>KYC delays, blocked accounts, geo-restrictions</td>
        </tr>
        <tr>
            <td>Financial</td>
            <td>Distribution errors, fee disputes, payment failures</td>
        </tr>
        <tr>
            <td>Conduct</td>
            <td>Staff behavior, communication issues, bias or rudeness</td>
        </tr>
        <tr>
            <td>Legal/Regulatory</td>
            <td>Breach of Terms of Use, smart contract malfunction, unauthorized token listing</td>
        </tr>
        <tr>
            <td>Technical</td>
            <td>Bugs, identity token issues, token transfer failures</td>
        </tr>
    </table>

    <h3>2.2 Excluded Communications</h3>
    <p>The following are not considered formal complaints:</p>
    <ul>
        <li>General service inquiries</li>
        <li>Feature requests or roadmap suggestions</li>
        <li>Social media comments without formal submission</li>
        <li>Expressions of frustration without a request for resolution</li>
        <li>Anonymous feedback without actionable detail</li>
    </ul>
    <p>That said, all client communications are reviewed and may be escalated to complaint status if they meet the criteria above.</p>

    <h2>3. Complaint Submission Channels</h2>
    <p>Penthian Ltd provides clients with multiple, accessible, and transparent ways to submit complaints. These channels are regularly monitored and integrated into our internal support and compliance workflows.</p>

    <h3>3.1 Accepted Submission Channels</h3>
    <p>Clients may lodge a complaint through any of the following official channels:</p>
    <h4>a. Email</h4>
    <ul>
        <li>Complaints can be submitted to: complaints@penthian.com</li>
        <li>Auto-reply confirmation is sent with a complaint reference number</li>
    </ul>
    <h4>b. In-App Support Ticket</h4>
    <ul>
        <li>Available through the Penthian web platform or mobile app</li>
        <li>‚ÄúReport a Problem‚Äù option opens a secure complaint form</li>
        <li>Users receive updates through their support dashboard</li>
    </ul>
    <h4>c. Website Complaint Form</h4>
    <ul>
        <li>Publicly available at www.penthian.com/complaints</li>
        <li>Includes structured form with fields for name, email, complaint type, and supporting documents</li>
    </ul>
    <h4>d. Postal Mail (if required)</h4>
    <ul>
        <li>Written complaints can be mailed to:</li>
        <li>Penthian Ltd ‚Äì Compliance Department</li>
        <li>1st Floor, Ebene Tower, Cybercity, Ebene 72201, Mauritius</li>
    </ul>
    <h4>e. Direct Escalation</h4>
    <ul>
        <li>Complaints raised during onboarding or support calls may be escalated directly by Penthian staff to the Compliance Officer using internal escalation channels</li>
    </ul>

    <h3>3.2 Language & Format Requirements</h3>
    <ul>
        <li>Complaints must be submitted in English or French</li>
        <li>Clear explanation of the issue, with dates, involved parties, and requested resolution</li>
        <li>Supporting documents (screenshots, receipts, transaction hashes) are encouraged</li>
    </ul>
    <p>Anonymous complaints may be reviewed at management‚Äôs discretion if they contain sufficient detail for investigation.</p>

    <h3>3.3 Acknowledgment of Receipt</h3>
    <p>Upon receipt of a formal complaint:</p>
    <ul>
        <li>The client is issued a Complaint Reference Number (CRN)</li>
        <li>A confirmation email is sent within 48 hours (2 business days)</li>
        <li>The acknowledgment includes expected resolution timelines and next steps</li>
    </ul>

    <h2>4. Complaint Handling Process</h2>
    <p>Penthian Ltd follows a structured, transparent process to ensure that all complaints are fairly assessed, investigated, and resolved within reasonable timeframes. This process is aligned with regulatory expectations and rooted in accountability and client protection.</p>

    <h3>4.1 Stages of the Complaint Process</h3>
    <ol>
        <li><strong>Receipt & Acknowledgment</strong>
            <ul>
                <li>Complaint is received via an approved channel.</li>
                <li>Acknowledgment with Complaint Reference Number (CRN) is sent to the complainant within 2 business days.</li>
            </ul>
        </li>
        <li><strong>Preliminary Review</strong>
            <ul>
                <li>A trained staff member reviews the complaint for completeness, category, and urgency.</li>
                <li>If incomplete, the client is contacted for more information.</li>
            </ul>
        </li>
        <li><strong>Assignment to Responsible Officer</strong>
            <ul>
                <li>Complaint is assigned to the relevant department (Compliance, Support, Technical, Finance, etc.).</li>
                <li>Complex or high-risk complaints are escalated to the Compliance Officer or Complaints Committee.</li>
            </ul>
        </li>
        <li><strong>Investigation</strong>
            <ul>
                <li>All relevant documentation, transaction history, smart contract records, and internal logs are reviewed.</li>
                <li>The client may be contacted for clarification or additional evidence.</li>
            </ul>
        </li>
        <li><strong>Internal Decision</strong>
            <ul>
                <li>A formal resolution is proposed based on findings and applicable policies.</li>
                <li>For regulated matters, compliance/legal review may be required.</li>
            </ul>
        </li>
        <li><strong>Response to Client</strong>
            <ul>
                <li>A final response is provided within 10 business days, or a holding update is sent if additional time is required.</li>
                <li>The decision includes findings, outcome, and any remedial actions or compensation.</li>
            </ul>
        </li>
        <li><strong>Closure</strong>
            <ul>
                <li>The case is marked closed once the client confirms satisfaction or no response is received after 10 additional business days.</li>
                <li>Summary is recorded in the Complaints Register and internal tracking system.</li>
            </ul>
        </li>
    </ol>

    <h3>4.2 Timelines Overview</h3>
    <table>
        <tr>
            <th>Step</th>
            <th>Timeframe</th>
        </tr>
        <tr>
            <td>Acknowledge Complaint</td>
            <td>Within 2 business days</td>
        </tr>
        <tr>
            <td>Investigate & Respond</td>
            <td>Within 10 business days</td>
        </tr>
        <tr>
            <td>Final Closure</td>
            <td>Within 20 business days total (unless extension justified)</td>
        </tr>
    </table>

    <h3>4.3 Complex Cases</h3>
    <p>For cases involving smart contract malfunctions, multiple jurisdictions, or legal disputes:</p>
    <ul>
        <li>Investigation may extend up to 30 business days, subject to regulatory reporting.</li>
        <li>Clients will receive regular updates every 7‚Äì10 business days.</li>
    </ul>

    <h3>4.4 Client Rights</h3>
    <p>Clients may:</p>
    <ul>
        <li>Request status updates at any stage</li>
        <li>Request reconsideration if dissatisfied with outcome</li>
        <li>Escalate externally if not resolved (see Section 6)</li>
    </ul>

    <h2>5. Internal Responsibilities & Oversight</h2>
    <p>Clear allocation of responsibility is essential to ensure timely and fair resolution of complaints. Penthian Ltd has defined roles and escalation paths for all employees involved in the complaint handling process.</p>

    <h3>5.1 Complaint Handling Team</h3>
    <p>The complaint management structure includes the following key personnel:</p>
    <table>
        <tr>
            <th>Role</th>
            <th>Responsibilities</th>
        </tr>
        <tr>
            <td>Customer Support Team</td>
            <td>Initial receipt, acknowledgment, and categorization of complaints</td>
        </tr>
        <tr>
            <td>Complaints Officer</td>
            <td>Coordinates investigations, communicates outcomes to clients, and ensures compliance</td>
        </tr>
        <tr>
            <td>Compliance Officer</td>
            <td>Oversees regulatory adherence, escalates high-risk complaints, and reports to FSC</td>
        </tr>
        <tr>
            <td>Department Heads (e.g. Tech, Finance, Risk)</td>
            <td>Investigate specific aspects of complaints related to their domain</td>
        </tr>
        <tr>
            <td>Complaints Committee (if applicable)</td>
            <td>Reviews high-severity complaints involving large financial impact, legal threats, or regulatory risk</td>
        </tr>
    </table>

    <h3>5.2 Escalation Protocol</h3>
    <p>Complaints may be escalated internally if:</p>
    <ul>
        <li>The issue is unresolved after initial investigation</li>
        <li>The client is dissatisfied with the resolution</li>
        <li>The complaint involves a regulatory breach or high-value transaction</li>
        <li>There is a potential risk of reputational damage or systemic failure</li>
    </ul>
    <p>Escalations go from the Support Team ‚Üí Complaints Officer ‚Üí Compliance Officer ‚Üí Complaints Committee.</p>

    <h3>5.3 Independence & Impartiality</h3>
    <p>To ensure objectivity:</p>
    <ul>
        <li>Staff who are the subject of a complaint may not participate in its investigation</li>
        <li>The Complaints Officer must act independently of sales, marketing, or revenue-related functions</li>
        <li>Clients are treated fairly, without discrimination or bias, regardless of the nature of their complaint</li>
    </ul>

    <h3>5.4 Complaint Register</h3>
    <p>A centralized Complaint Register is maintained by the Compliance Officer, tracking:</p>
    <ul>
        <li>Complaint ID and timestamps</li>
        <li>Complainant identity (where known)</li>
        <li>Issue summary and category</li>
        <li>Actions taken and personnel involved</li>
        <li>Outcome and client feedback</li>
        <li>Date of closure</li>
    </ul>
    <p>This register is reviewed quarterly by senior management and is available for FSC audit.</p>

    <h3>5.5 Training & Oversight</h3>
    <ul>
        <li>All staff receive annual training on complaint handling procedures, including escalation and documentation.</li>
        <li>The effectiveness of the complaint process is evaluated during internal audits.</li>
        <li>Regular feedback loops help refine procedures based on trends in complaints.</li>
    </ul>

    <h2>6. External Dispute Resolution & FSC Reporting</h2>
    <p>In cases where internal resolution is not satisfactory or where a complaint involves regulatory implications, clients have the right to escalate their concerns to an external authority. Penthian Ltd is committed to facilitating access to independent resolution mechanisms and cooperating fully with the Financial Services Commission (FSC) of Mauritius.</p>

    <h3>6.1 Right to Escalate</h3>
    <p>If a client is dissatisfied with the final outcome or the handling of a complaint, they may escalate the matter to the FSC or an independent dispute resolution body.</p>
    <p>Details of the client‚Äôs right to escalate will be clearly stated in the final complaint outcome letter.</p>

    <h3>6.2 FSC Contact Information</h3>
    <p>Clients may file a formal complaint with the Financial Services Commission using the following contact details:</p>
    <p>Financial Services Commission (FSC) ‚Äì Mauritius<br>
    FSC House, 54 Cybercity, Ebene 72201, Mauritius<br>
    üìû Tel: +230 403 7000<br>
    üìß Email: fscmauritius@intnet.mu<br>
    üåê Website: www.fscmauritius.org</p>

    <h3>6.3 Conditions for External Filing</h3>
    <p>Clients must generally attempt resolution with Penthian first, unless the issue:</p>
    <ul>
        <li>Involves serious fraud or immediate legal breach</li>
        <li>Poses systemic or market-wide risk</li>
        <li>Has already been independently investigated and closed internally</li>
    </ul>

    <h3>6.4 FSC Reporting by Penthian</h3>
    <p>As part of its regulatory obligations, Penthian Ltd will:</p>
    <ul>
        <li>Report all unresolved complaints involving potential breaches of law, AML/CFT concerns, or investor harm to the FSC within 5 business days</li>
        <li>Submit quarterly summaries of total complaints, their nature, resolution time, and trends</li>
        <li>Cooperate fully with FSC investigations or audits relating to specific complaints</li>
    </ul>

    <h3>6.5 Retaliation-Free Policy</h3>
    <p>Clients are protected from any form of retaliation or disadvantage for submitting a complaint or escalating to the FSC.</p>

    <h2>7. Recordkeeping & Confidentiality</h2>
    <p>Maintaining detailed and secure records of all complaints is essential to demonstrate transparency, support future audits, and comply with FSC and AML/CFT regulations. At the same time, client confidentiality must be preserved in all stages of the complaint lifecycle.</p>

    <h3>7.1 Complaint Records Maintained</h3>
    <p>For every formal complaint received, Penthian maintains a complete file including:</p>
    <ul>
        <li>Complaint Reference Number (CRN)</li>
        <li>Complainant‚Äôs name and contact details (if known)</li>
        <li>Date and channel of submission</li>
        <li>Nature and classification of the complaint</li>
        <li>All supporting evidence provided by the client</li>
        <li>Internal investigation notes and findings</li>
        <li>Communications between Penthian and the complainant</li>
        <li>Final resolution, response, and client satisfaction status</li>
        <li>Escalation steps (internal or external) if applicable</li>
    </ul>

    <h3>7.2 Storage & Access Control</h3>
    <ul>
        <li>Complaint records are stored digitally in a secure, encrypted system with controlled access.</li>
        <li>Only authorized personnel (Support Manager, Complaints Officer, Compliance Officer) may access these records.</li>
        <li>Regular access logs are maintained and reviewed to prevent unauthorized data access or tampering.</li>
    </ul>

    <h3>7.3 Retention Period</h3>
    <table>
        <tr>
            <th>Record Type</th>
            <th>Retention Period</th>
        </tr>
        <tr>
            <td>Complaint files</td>
            <td>Minimum 7 years from date of closure</td>
        </tr>
        <tr>
            <td>Complaints Register</td>
            <td>Minimum 7 years</td>
        </tr>
        <tr>
            <td>FSC-related submissions</td>
            <td>Minimum 7 years</td>
        </tr>
    </table>
    <p>Records will be retained longer if related to unresolved legal, regulatory, or financial investigations.</p>

    <h3>7.4 Confidentiality Measures</h3>
    <p>Penthian Ltd upholds strict client confidentiality standards:</p>
    <ul>
        <li>Complaint-related information is only shared internally on a need-to-know basis.</li>
        <li>If shared with the FSC or legal authorities, it is done securely and lawfully.</li>
        <li>Complaint details will never be disclosed publicly without explicit written consent.</li>
    </ul>
    <p>Staff are trained on their obligation to uphold confidentiality and are subject to disciplinary action for any breaches.</p>

    <h3>7.5 Audit & Compliance Review</h3>
    <p>The Complaint Register and handling process are subject to:</p>
    <ul>
        <li>Quarterly compliance reviews</li>
        <li>Annual internal audit</li>
        <li>Spot checks by the Compliance Officer</li>
        <li>FSC audits upon request</li>
    </ul>
    <p>Findings are used to continuously improve complaint resolution procedures.</p>

    <h2>8. Continuous Improvement & Policy Review</h2>
    <p>Penthian Ltd is committed to building trust, transparency, and accountability through continual improvement of its complaint handling practices. This involves learning from client feedback, tracking recurring issues, and regularly updating this policy in line with legal, operational, and technological developments.</p>

    <h3>8.1 Trend Analysis</h3>
    <p>All complaints are logged in a centralized database and reviewed regularly to identify patterns. Key metrics tracked include:</p>
    <ul>
        <li>Volume of complaints by category and type</li>
        <li>Time to resolution</li>
        <li>Recurrence of similar issues</li>
        <li>Root causes (e.g. platform bugs, process gaps, miscommunication)</li>
        <li>Outcomes (resolved, escalated, client dissatisfied, etc.)</li>
    </ul>
    <p>Findings are used to:</p>
    <ul>
        <li>Update operational processes</li>
        <li>Improve training materials</li>
        <li>Guide system upgrades</li>
        <li>Adjust smart contract governance protocols if needed</li>
    </ul>

    <h3>8.2 Client Feedback Integration</h3>
    <p>Penthian encourages feedback after every complaint is resolved. This helps measure satisfaction and identify gaps in:</p>
    <ul>
        <li>Speed of response</li>
        <li>Quality of communication</li>
        <li>Clarity of outcomes</li>
        <li>Fairness of resolutions</li>
    </ul>
    <p>Feedback is reviewed monthly by the Compliance Officer and Complaints Officer to shape platform and policy improvements.</p>

    <h3>8.3 Staff Learning Loops</h3>
    <p>Where complaints reveal employee or process weaknesses:</p>
    <ul>
        <li>Refresher training sessions are scheduled</li>
        <li>Policy memos are issued</li>
        <li>Performance KPIs are adjusted to prioritize better complaint resolution</li>
    </ul>
    <p>Complaint insights are included in quarterly internal briefings.</p>

    <h3>8.4 Policy Review Schedule</h3>
    <p>This Complaint Handling Procedures Manual is formally reviewed:</p>
    <ul>
        <li>Annually, or</li>
        <li>Immediately following a major complaint, regulatory development, or system change</li>
    </ul>
    <p>Updates are approved by the Compliance Officer and communicated to all relevant staff.</p>

    <h3>8.5 Regulator & Legal Compliance</h3>
    <p>All updates ensure ongoing compliance with:</p>
    <ul>
        <li>FSC guidelines</li>
        <li>The Virtual Asset and Initial Token Offering Services Act 2021</li>
        <li>FIAMLA and the Data Protection Act 2017</li>
        <li>Global dispute resolution standards</li>
    </ul>
