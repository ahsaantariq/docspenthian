    <h2>For Class S Virtual Asset Marketplace License (Mauritius FSC)</h2>

    <div class="section">
        <h2>1. Introduction & Purpose</h2>
        <p>The purpose of this Market Conduct Policy is to ensure that Penthian Ltd, its employees, agents, and ecosystem participants operate with integrity, fairness, and transparency across all market-facing activities. This policy aligns with the Financial Services Commission (FSC) of Mauritius’ expectations for Virtual Asset Service Providers (VASPs) and is a critical component of our regulatory compliance and ethical governance.</p>
        <p>Penthian Ltd operates a primary and secondary marketplace for tokenized real-world assets including real estate and small businesses. This policy governs the behavior and responsibilities of:</p>
        <ul>
            <li>Internal staff and executives</li>
            <li>External agents, partners, and brokers</li>
            <li>Sellers of tokenized assets</li>
            <li>Buyers and investors on the platform</li>
        </ul>
        <p>This Market Conduct Policy protects against:</p>
        <ul>
            <li>Insider trading</li>
            <li>Price manipulation</li>
            <li>Unfair marketing or misrepresentation</li>
            <li>Conflicts of interest</li>
            <li>Abusive trading behavior</li>
            <li>Investor exploitation</li>
        </ul>
    </div>

    <div class="section">
        <h2>2. Scope & Applicability</h2>
        <p>This policy applies to all individuals and entities engaged in any market-facing or transaction-related activity on the Penthian platform, including but not limited to:</p>
        <h3>2.1 Internal Stakeholders</h3>
        <ul>
            <li>Board Members</li>
            <li>Executive Team</li>
            <li>Employees</li>
            <li>Contractors or consultants with access to confidential platform data</li>
        </ul>
        <h3>2.2 External Stakeholders</h3>
        <ul>
            <li>Asset owners and developers listing real-world assets on the primary market</li>
            <li>Investors transacting in the secondary market</li>
            <li>Token holders participating in asset governance or auctions</li>
            <li>Referral agents, whitelisted sellers, and licensed brokers</li>
            <li>Third-party service providers (e.g., custodians, auditors, oracles)</li>
        </ul>
        <h3>2.3 Covered Activities</h3>
        <ul>
            <li>Listing of tokenized assets</li>
            <li>Marketing and sale of tokenized offerings</li>
            <li>Bidding, auctioning, or trading of virtual assets</li>
            <li>Voting or governance participation (where applicable)</li>
            <li>Distribution of rental yield or dividends</li>
            <li>Use of insider information</li>
            <li>Public communication regarding listed assets</li>
        </ul>
        <p>This policy applies to all jurisdictions in which Penthian operates, subject to relevant legal and regulatory adjustments.</p>
    </div>

    <div class="section">
        <h2>3. Principles of Fair Market Conduct</h2>
        <p>Penthian Ltd is committed to operating a trusted, transparent, and ethically sound marketplace. The following principles govern all market-facing activities conducted on the platform:</p>
        <h3>3.1 Integrity</h3>
        <ul>
            <li>All participants must act honestly and ethically in all dealings.</li>
            <li>No party shall provide false or misleading information regarding asset listings, performance, or valuation.</li>
            <li>Market communications must reflect factual, verifiable claims.</li>
        </ul>
        <h3>3.2 Transparency</h3>
        <ul>
            <li>Key information about tokenized assets—including legal ownership, valuation method, risk factors, and return expectations—must be disclosed to prospective investors before purchase.</li>
            <li>Pricing models and transaction fees are disclosed clearly in user interfaces and offer documentation.</li>
        </ul>
        <h3>3.3 Investor Protection</h3>
        <ul>
            <li>Users are entitled to timely, accurate, and understandable disclosures to support informed decision-making.</li>
            <li>Platform tools (e.g., risk warnings, financial calculators, and investment dashboards) must be designed to prevent misinterpretation and overexposure.</li>
            <li>Penthian does not allow high-pressure sales tactics or guaranteed return promises.</li>
        </ul>
        <h3>3.4 Market Efficiency</h3>
        <ul>
            <li>The marketplace shall operate in a manner that promotes price discovery, liquidity, and transaction efficiency.</li>
            <li>Technical downtime or trading suspension must be reported transparently with incident response communications.</li>
        </ul>
        <h3>3.5 Non-Discrimination</h3>
        <ul>
            <li>All users shall have equal access to public offers and participation opportunities, except where jurisdictional or KYC restrictions apply.</li>
            <li>Insider access or preferential treatment to token offerings is strictly prohibited.</li>
        </ul>
    </div>

    <div class="section">
        <h2>4. Prohibited Market Behaviors</h2>
        <p>To ensure a fair, trustworthy, and compliant marketplace, Penthian strictly prohibits the following market behaviors. Violations will result in account suspension, legal action, and reporting to regulatory authorities.</p>
        <h3>4.1 Insider Trading</h3>
        <ul>
            <li>Trading based on material, non-public information is strictly forbidden.</li>
            <li>This includes unreleased financials, upcoming asset tokenization, pricing changes, or pending legal issues.</li>
            <li>All staff with access to sensitive information must maintain confidentiality.</li>
        </ul>
        <h3>4.2 Market Manipulation</h3>
        <p>Any behavior that artificially influences token prices, market perception, or trading volume is prohibited. This includes:</p>
        <ul>
            <li>Wash trading – buying and selling the same asset to inflate volume</li>
            <li>Spoofing – placing fake buy/sell orders to mislead others</li>
            <li>Pump and dump – artificially inflating price for profit</li>
            <li>Layering – placing multiple orders to distort the market</li>
        </ul>
        <h3>4.3 Misrepresentation</h3>
        <ul>
            <li>It is prohibited to provide false, misleading, or incomplete information in asset descriptions, return projections, team credentials, or risk disclosures.</li>
            <li>All forward-looking statements must be clearly labeled and based on reasonable assumptions.</li>
        </ul>
        <h3>4.4 Front Running</h3>
        <ul>
            <li>Using confidential information about pending platform trades to gain a market advantage is strictly forbidden.</li>
            <li>Employees, agents, and privileged partners may not act on unreleased data.</li>
        </ul>
        <h3>4.5 Conflict of Interest Abuse</h3>
        <ul>
            <li>Agents or staff with conflicts of interest must disclose and recuse themselves from any related decision-making or promotional activity.</li>
            <li>Referrals or kickbacks must be disclosed transparently.</li>
        </ul>
        <h3>4.6 Bribery or Coercion</h3>
        <ul>
            <li>No user or staff member may offer, solicit, or accept bribes in relation to asset listing approvals, user onboarding, or trade facilitation.</li>
            <li>All commissions must be reported and paid through approved internal processes.</li>
        </ul>
    </div>

    <div class="section">
        <h2>5. Listing & Trading Conduct Requirements</h2>
        <p>All participants involved in the listing or trading of assets on Penthian’s platform must adhere to the following rules to ensure market integrity and protect investors.</p>
        <h3>5.1 Asset Listing Conduct</h3>
        <p>For Asset Issuers / Sellers:</p>
        <ul>
            <li><strong>Due Diligence:</strong> Must complete and pass Penthian’s KYC/AML verification (via Veriff) and asset vetting.</li>
            <li><strong>Accurate Disclosures:</strong> Must provide comprehensive, accurate, and verifiable information including:
                <ul>
                    <li>Title deed / ownership documents</li>
                    <li>Valuation reports</li>
                    <li>Rental history or projected income</li>
                    <li>Legal encumbrances or disputes</li>
                </ul>
            </li>
            <li><strong>No Misleading Offers:</strong> Sellers must not inflate valuation or make unverifiable ROI claims.</li>
            <li><strong>Ongoing Responsibility:</strong> Sellers are responsible for updating information if asset conditions change post-listing.</li>
        </ul>
        <h3>5.2 Trading Conduct</h3>
        <p>For All Investors and Users:</p>
        <ul>
            <li><strong>Fair Participation:</strong> Must not exploit bugs, delays, or interface glitches to gain unfair trading advantages.</li>
            <li><strong>Platform Use:</strong> All trades must be executed through official, authorized interfaces (e.g., web app, mobile app, or API).</li>
            <li><strong>Wallet Compliance:</strong> Trading wallets must be KYC-approved and compliant with ERC-3643 identity standards.</li>
            <li><strong>Auction Conduct:</strong> During auctions, users must not engage in bid manipulation, false bidding, or coordinated bid suppression.</li>
        </ul>
        <h3>5.3 Pricing & Order Transparency</h3>
        <ul>
            <li>Penthian will display:
                <ul>
                    <li>Historical pricing data (where applicable)</li>
                    <li>Latest market value or reserve price</li>
                    <li>Associated fees, charges, and gas costs before confirmation</li>
                </ul>
            </li>
            <li>Token price and trading history must be based on executed transactions only, not unfilled orders.</li>
        </ul>
        <h3>5.4 Breach Consequences</h3>
        <p>Violations of these requirements may result in:</p>
        <ul>
            <li>Delisting of the asset</li>
            <li>Suspension of user accounts</li>
            <li>Termination of agent/referral agreements</li>
            <li>Notification to law enforcement or the FSC</li>
        </ul>
    </div>

    <div class="section">
        <h2>6. Communication & Advertising Standards</h2>
        <p>To ensure ethical and compliant promotion of tokenized assets and platform services, Penthian sets clear standards for all communications and marketing activities.</p>
        <h3>6.1 Honest Representation</h3>
        <ul>
            <li>All marketing content—whether issued by Penthian, asset owners, or affiliates—must be truthful, not misleading, and balanced.</li>
            <li>No guarantee of profits or “risk-free” claims is permitted.</li>
            <li>Forward-looking statements must:
                <ul>
                    <li>Be based on reasonable assumptions</li>
                    <li>Clearly state that projections are not guaranteed</li>
                </ul>
            </li>
        </ul>
        <h3>6.2 Mandatory Disclaimers</h3>
        <ul>
            <li>Every public communication (ads, brochures, emails, social media, landing pages) must include:
                <ul>
                    <li>Risk disclosure (e.g. “Investing in tokenized assets involves risk. You may lose part or all of your investment.”)</li>
                    <li>Jurisdictional notice (e.g. “This offer may not be available in all jurisdictions.”)</li>
                    <li>Licensing statement, where applicable (e.g. “Offered by Penthian Ltd, a licensed VASP under the FSC Mauritius.”)</li>
                </ul>
            </li>
        </ul>
        <h3>6.3 Branding & Use of Platform Name</h3>
        <ul>
            <li>Agents and partners must not use the Penthian brand, logo, or name without prior written approval.</li>
            <li>All platform representations must align with Penthian’s official positioning, tone, and visual identity.</li>
            <li>Unauthorized white-labeling, website cloning, or impersonation is strictly prohibited.</li>
        </ul>
        <h3>6.4 Communication Channels Covered</h3>
        <p>This policy applies to communications across:</p>
        <ul>
            <li>Website landing pages</li>
            <li>Paid advertising (search, display, social)</li>
            <li>Email marketing and newsletters</li>
            <li>Social media posts and community channels</li>
            <li>Events, pitch decks, and agent webinars</li>
            <li>Third-party listings or news publications</li>
        </ul>
        <h3>6.5 Content Review & Enforcement</h3>
        <ul>
            <li>All promotional content must be reviewed by Penthian’s compliance or marketing team prior to release.</li>
            <li>Breaches of communication standards may result in:
                <ul>
                    <li>Content takedown orders</li>
                    <li>Removal from agent program</li>
                    <li>Account suspension</li>
                    <li>Legal or regulatory escalation if misleading the public</li>
                </ul>
            </li>
        </ul>
    </div>

    <div class="section">
        <h2>7. Whistleblower Protections & Market Misconduct Reporting</h2>
        <p>To uphold market integrity and investor trust, Penthian encourages the reporting of any unethical, suspicious, or non-compliant conduct. This section outlines how individuals can report misconduct and the protections afforded to whistleblowers.</p>
        <h3>7.1 Right to Report Misconduct</h3>
        <p>All employees, agents, platform users, and third-party partners are empowered to report:</p>
        <ul>
            <li>Insider trading</li>
            <li>Market manipulation</li>
            <li>Bribery or coercion</li>
            <li>Unethical marketing or misrepresentation</li>
            <li>Conflicts of interest</li>
            <li>Platform abuse or regulatory breaches</li>
        </ul>
        <p>Reports can be submitted anonymously or with identity disclosed via:</p>
        <ul>
            <li>The secure Whistleblower Form on the Penthian website</li>
            <li>Email to compliance@penthian.com</li>
            <li>Direct submission to the Compliance Officer</li>
        </ul>
        <h3>7.2 Whistleblower Protections</h3>
        <p>Penthian guarantees:</p>
        <ul>
            <li>Confidentiality of the reporter’s identity (unless legally required to disclose)</li>
            <li>No retaliation against any person making a good faith report</li>
            <li>Internal review within 7 business days of receipt</li>
            <li>Timely updates to the whistleblower where appropriate</li>
        </ul>
        <p>Any attempt to harass, demote, dismiss, or threaten a whistleblower will result in disciplinary action and, where necessary, legal penalties.</p>
        <h3>7.3 Internal Investigations</h3>
        <p>Upon receiving a report:</p>
        <ul>
            <li>The Compliance Officer will initiate an independent investigation</li>
            <li>Findings are presented to the Board (or Audit Committee where established)</li>
            <li>If the issue involves criminal activity, regulators or law enforcement will be notified</li>
            <li>All cases are documented in the Market Misconduct Register</li>
        </ul>
        <h3>7.4 Reporting Obligations to FSC</h3>
        <ul>
            <li>Where material misconduct is confirmed, Penthian will notify the Financial Services Commission (FSC) within 5 business days, as per regulatory requirements.</li>
        </ul>
    </div>

    <div class="section">
        <h2>8. Enforcement & Disciplinary Actions</h2>
        <p>To maintain the credibility and trustworthiness of Penthian’s marketplace, all breaches of this Market Conduct Policy will be dealt with swiftly and fairly. Enforcement mechanisms are designed to deter misconduct, protect investors, and comply with legal and regulatory standards.</p>
        <h3>8.1 Disciplinary Process</h3>
        <p>Upon identifying a potential breach:</p>
        <ol>
            <li><strong>Preliminary Assessment</strong> – Conducted by the Compliance Officer within 48 hours of detection or report.</li>
            <li><strong>Formal Investigation</strong> – If warranted, an internal inquiry is launched. The party under investigation is given an opportunity to respond.</li>
            <li><strong>Board Review</strong> – Serious breaches or repeated violations are escalated to the Board or designated compliance committee for final decision.</li>
            <li><strong>Corrective Action</strong> – Based on the findings, one or more of the following disciplinary measures may apply.</li>
        </ol>
        <h3>8.2 Disciplinary Measures</h3>
        <p>Depending on the nature and severity of the offense, Penthian may impose:</p>
        <ul>
            <li>Written warning or reprimand</li>
            <li>Suspension or termination of platform access</li>
            <li>Delisting of associated assets</li>
            <li>Freezing of wallets or trading privileges</li>
            <li>Removal from agent or referral programs</li>
            <li>Filing of regulatory reports or complaint to the FSC</li>
            <li>Legal action, including recovery of damages or injunctive relief</li>
        </ul>
        <h3>8.3 Appeal Process</h3>
        <ul>
            <li>Individuals or entities sanctioned under this policy may submit a written appeal to the Compliance Officer within 10 business days of notification.</li>
            <li>Appeals are reviewed by an independent panel (where available) or escalated to the Board for reconsideration.</li>
            <li>The final decision is binding.</li>
        </ul>
        <h3>8.4 Public Transparency</h3>
        <ul>
            <li>Penthian reserves the right to publicly disclose confirmed violations of market conduct rules to protect other investors and uphold platform integrity.</li>
            <li>Personal data will only be disclosed where legally required or permitted under data protection laws.</li>
        </ul>
    </div>

    <div class="section">
        <h2>9. Review & Amendments</h2>
        <p>To ensure that this policy remains effective, current, and aligned with regulatory expectations and market dynamics, Penthian Ltd will conduct regular reviews and implement amendments as necessary.</p>
        <h3>9.1 Periodic Review</h3>
        <ul>
            <li>The Compliance Officer, in collaboration with the Board of Directors, will review this Market Conduct Policy at least once per year.</li>
            <li>Reviews will consider:
                <ul>
                    <li>Evolving FSC requirements</li>
                    <li>Changes in global VASP and tokenization regulations</li>
                    <li>Operational or platform changes impacting market conduct</li>
                    <li>Feedback from whistleblowers, regulators, or community stakeholders</li>
                </ul>
            </li>
        </ul>
        <h3>9.2 Amendment Process</h3>
        <ul>
            <li>Any material change to this policy will be:
                <ul>
                    <li>Reviewed by legal counsel (where necessary)</li>
                    <li>Approved by the Board of Directors</li>
                    <li>Communicated to all stakeholders, including agents and users</li>
                </ul>
            </li>
            <li>Updated versions will be made available via the platform’s policy portal and emailed to relevant users.</li>
        </ul>
        <h3>9.3 Version Control</h3>
        <ul>
            <li>Each version of the Market Conduct Policy will be recorded with:
                <ul>
                    <li>Effective date</li>
                    <li>Summary of changes</li>
                    <li>Responsible reviewer(s)</li>
                </ul>
            </li>
            <li>Historic versions will be archived and made available for audit or regulatory inspection for no less than seven years.</li>
        </ul>
