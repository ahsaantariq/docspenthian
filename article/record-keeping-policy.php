   <h2>1. Purpose & Scope</h2>
    <p>The purpose of this Record-Keeping Policy is to establish a comprehensive framework for the retention, access, protection, and disposal of all business records, client files, and digital transactions processed by Penthian Ltd.</p>
    <p>This policy applies to:</p>
    <ul>
        <li>All employees, contractors, agents, and service providers of Penthian Ltd</li>
        <li>All data formats, including physical documents, electronic files, blockchain records, and communication logs</li>
        <li>All records related to client onboarding, asset listings, KYC/AML compliance, financial transactions, smart contracts, and internal governance</li>
    </ul>
    <p>The policy ensures compliance with:</p>
    <ul>
        <li>Mauritius Financial Services Commission (FSC) guidelines</li>
        <li>AML/CFT obligations under the Financial Intelligence and Anti-Money Laundering Act</li>
        <li>Data Protection Act 2017 (Mauritius) and GDPR where applicable</li>
    </ul>

    <h2>2. Categories of Records Maintained</h2>
    <p>Penthian Ltd classifies its records into specific categories based on function and regulatory importance. These records are retained across both on-chain and off-chain systems and are managed with strict access and security protocols.</p>

    <h3>2.1 Client Records</h3>
    <ul>
        <li>KYC/KYB documentation (e.g., passports, business registration, utility bills)</li>
        <li>Customer due diligence (CDD) and enhanced due diligence (EDD) reports</li>
        <li>Risk scoring, investor classification, and onboarding approvals (via Veriff)</li>
        <li>Client communications (email, messages, recorded calls if applicable)</li>
    </ul>

    <h3>2.2 Transaction Records</h3>
    <ul>
        <li>Purchase, sale, or transfer of tokenized assets</li>
        <li>Smart contract execution logs (stored on-chain and indexed off-chain)</li>
        <li>Payment confirmations, fiat/crypto deposit records</li>
        <li>Wallet addresses used by clients and associated AML risk flags</li>
    </ul>

    <h3>2.3 AML/CFT Monitoring Records</h3>
    <ul>
        <li>Suspicious transaction reports (STRs)</li>
        <li>Transaction monitoring alerts and investigations</li>
        <li>Records of correspondence with the FIU and FSC</li>
        <li>Sanctions screening outcomes</li>
    </ul>

    <h3>2.4 Listing & Asset Records</h3>
    <ul>
        <li>Property/SME documentation and legal title verifications</li>
        <li>Token issuance data, ownership logs, and valuation reports</li>
        <li>Voting records and investor governance activity</li>
        <li>Asset performance summaries and payout records</li>
    </ul>

    <h3>2.5 Corporate & Governance Records</h3>
    <ul>
        <li>Board resolutions and minutes</li>
        <li>Internal audit reports</li>
        <li>Employee contracts, compliance attestations</li>
        <li>External legal, regulatory, and tax correspondence</li>
    </ul>

    <h3>2.6 Technical System Logs</h3>
    <ul>
        <li>Platform login records and API access trails</li>
        <li>Smart contract deployment versioning and rollback logs</li>
        <li>Infrastructure monitoring alerts (AWS/DigitalOcean logs)</li>
        <li>System downtime incidents and recovery logs</li>
    </ul>

    <h2>3. Retention Periods</h2>
    <p>Penthian Ltd applies clearly defined retention periods for each category of records, in accordance with FSC regulations, AML/CFT requirements, and international best practices.</p>

    <h3>3.1 Statutory Minimum Retention</h3>
    <p>The following retention timelines apply unless superseded by legal proceedings or regulatory directives:</p>
    <table>
        <tr>
            <th>Record Type</th>
            <th>Minimum Retention Period</th>
        </tr>
        <tr>
            <td>KYC/KYB documents</td>
            <td>7 years from relationship end</td>
        </tr>
        <tr>
            <td>AML transaction monitoring logs</td>
            <td>7 years</td>
        </tr>
        <tr>
            <td>Client transaction history</td>
            <td>7 years from transaction</td>
        </tr>
        <tr>
            <td>Suspicious transaction reports</td>
            <td>7 years</td>
        </tr>
        <tr>
            <td>Token issuance & smart contract logs</td>
            <td>Indefinitely (archived)</td>
        </tr>
        <tr>
            <td>Audit & compliance reports</td>
            <td>7 years</td>
        </tr>
        <tr>
            <td>Governance & board minutes</td>
            <td>7 years</td>
        </tr>
        <tr>
            <td>Financial records</td>
            <td>7 years</td>
        </tr>
        <tr>
            <td>Employment and contractor files</td>
            <td>7 years after termination</td>
        </tr>
    </table>

    <h3>3.2 Extended Retention (Indefinite)</h3>
    <p>The following are retained indefinitely for transparency and forensic validation:</p>
    <ul>
        <li>On-chain smart contract deployment and event logs</li>
        <li>Final investor governance votes and audit trails</li>
        <li>Any regulatory decisions or enforcement actions</li>
        <li>Ownership logs of tokenized assets and equity stakes</li>
    </ul>

    <h3>3.3 Disposal Procedures</h3>
    <ul>
        <li>Digital files are permanently deleted or anonymized</li>
        <li>Physical files are shredded securely</li>
        <li>An audit log of each deletion is maintained</li>
        <li>No data is deleted without the authorization of the Compliance Officer</li>
    </ul>

    <h2>4. Storage & Security</h2>
    <p>To protect the integrity, confidentiality, and availability of all records, Penthian Ltd enforces strong security measures across its digital infrastructure and any physical storage locations.</p>

    <h3>4.1 Digital Storage Protocols</h3>
    <ul>
        <li>All records are stored in secure, access-controlled environments using:</li>
        <ul>
            <li>Cloud infrastructure (AWS, DigitalOcean) with encrypted volumes</li>
            <li>Cold storage backups for sensitive or infrequently accessed records</li>
            <li>Immutable audit logs for transaction data and smart contract history</li>
        </ul>
        <li>Blockchain data is:</li>
        <ul>
            <li>Stored on-chain via Ethereum-compatible networks</li>
            <li>Indexed through internal databases for performance optimization</li>
            <li>Supplemented by off-chain metadata to meet legal documentation requirements</li>
        </ul>
    </ul>

    <h3>4.2 Access Controls</h3>
    <ul>
        <li>Access to sensitive records is governed by:</li>
        <ul>
            <li>Role-based permissions</li>
            <li>Two-factor authentication (2FA)</li>
            <li>Session timeouts and device restrictions</li>
        </ul>
        <li>Only authorized personnel (e.g. MLRO, CTO, auditors) may access KYC, AML, or governance data</li>
    </ul>

    <h3>4.3 Physical Records</h3>
    <ul>
        <li>Where physical records exist (e.g., original contracts):</li>
        <ul>
            <li>They are stored in locked filing cabinets within secure office premises</li>
            <li>Access is granted only to authorized compliance or legal staff</li>
            <li>Regular audits verify physical file integrity and custody</li>
        </ul>
    </ul>

    <h3>4.4 Backups & Redundancy</h3>
    <ul>
        <li>Daily encrypted backups are maintained in geographically redundant cloud storage</li>
        <li>Quarterly disaster recovery drills are conducted to test data restoration and system resilience</li>
        <li>Critical records are backed up to at least two separate regions or environments</li>
    </ul>

    <h3>4.5 Breach Notification</h3>
    <ul>
        <li>In the event of unauthorized access or data breach:</li>
        <ul>
            <li>The Compliance Officer will notify the FSC and Data Protection Commissioner within 72 hours</li>
            <li>Affected parties will be notified as required by law</li>
            <li>An internal incident report will be logged and retained for 7 years</li>
        </ul>
    </ul>

    <h2>5. Record Access & Auditability</h2>
    <p>Penthian Ltd ensures that all retained records are accessible only to authorized individuals and auditable by relevant authorities in accordance with applicable regulations and internal governance procedures.</p>

    <h3>5.1 Internal Access Controls</h3>
    <ul>
        <li>Access to records is restricted based on:</li>
        <ul>
            <li>Job function and seniority (e.g. Compliance, Tech, Legal, Audit)</li>
            <li>Data classification level</li>
            <li>Ongoing compliance with internal data handling training</li>
        </ul>
        <li>Staff access is reviewed quarterly by the Compliance Officer and CTO</li>
    </ul>

    <h3>5.2 Regulatory Access</h3>
    <ul>
        <li>Upon request, records will be made available to:</li>
        <ul>
            <li>The Financial Services Commission (FSC) of Mauritius</li>
            <li>The Financial Intelligence Unit (FIU)</li>
            <li>Any judicial or tax authority under relevant legal provisions</li>
        </ul>
        <li>Requests are fulfilled within five working days, unless otherwise directed</li>
    </ul>

    <h3>5.3 Investor & Issuer Access</h3>
    <ul>
        <li>Clients may request access to their own:</li>
        <ul>
            <li>Personal data</li>
            <li>Investment history</li>
            <li>Voting participation and ownership records</li>
        </ul>
        <li>All requests are handled via the platform’s dedicated Data Request Portal, with response within 30 days</li>
    </ul>

    <h3>5.4 Audit Trail Integrity</h3>
    <ul>
        <li>All user and admin interactions with core systems are logged, including:</li>
        <ul>
            <li>Record creation, modification, deletion</li>
            <li>Contract deployment and transaction execution</li>
            <li>KYC verification and onboarding approvals</li>
        </ul>
        <li>Logs are stored immutably and retained in accordance with the retention policy</li>
    </ul>

    <h3>5.5 Third-Party Oversight</h3>
    <ul>
        <li>Any vendors or third-party services handling record-keeping or backups must:</li>
        <ul>
            <li>Provide audit access to Penthian and FSC upon request</li>
            <li>Maintain system logs for a minimum of 7 years</li>
            <li>Abide by GDPR, DPA (Mauritius), and confidentiality clauses in contracts</li>
        </ul>
    </ul>

    <h2>6. Roles & Responsibilities</h2>
    <p>Penthian Ltd assigns clear responsibilities for the management, oversight, and enforcement of its record-keeping framework to ensure accountability and compliance across the organization.</p>

    <h3>6.1 Board of Directors</h3>
    <ul>
        <li>Approves the Record-Keeping Policy and reviews updates annually</li>
        <li>Oversees the overall effectiveness of the record management system</li>
        <li>Receives internal audit reports on compliance with retention and access procedures</li>
    </ul>

    <h3>6.2 Compliance Officer</h3>
    <ul>
        <li>Acts as the primary custodian of the Record-Keeping Policy</li>
        <li>Ensures that all legal and regulatory requirements (FSC, AML/CFT, Data Protection) are met</li>
        <li>Coordinates internal reviews and audits of data retention, storage, and deletion</li>
        <li>Oversees the response to any regulatory inquiries or legal holds</li>
    </ul>

    <h3>6.3 MLRO (Money Laundering Reporting Officer)</h3>
    <ul>
        <li>Ensures that all AML-related records (e.g., STRs, transaction logs, KYC files) are properly retained and secured</li>
        <li>Coordinates with Compliance on document availability for the FIU or law enforcement</li>
        <li>Ensures traceability of flagged transactions and alerts</li>
    </ul>

    <h3>6.4 Chief Technology Officer (CTO)</h3>
    <ul>
        <li>Implements secure storage infrastructure, access controls, and backups</li>
        <li>Coordinates encryption, redundancy, and system audit logging</li>
        <li>Works with Compliance to ensure technology vendors meet retention and auditability standards</li>
    </ul>

    <h3>6.5 Department Managers</h3>
    <ul>
        <li>Ensure that their teams understand and comply with record-keeping procedures</li>
        <li>Support Compliance in categorizing new records, filing digital documents, and applying retention rules</li>
        <li>Report any suspected data loss, access breach, or mishandling</li>
    </ul>

    <h3>6.6 Employees & Contractors</h3>
    <ul>
        <li>Must adhere to all data handling, classification, and access rules</li>
        <li>Required to complete mandatory data security and record-keeping training</li>
        <li>Must report any accidental deletion, unauthorized access, or misfiled documents</li>
    </ul>

    <h2>7. Policy Review & Updates</h2>
    <p>To ensure that the Record-Keeping Policy remains relevant, compliant, and effective, Penthian Ltd follows a structured process for periodic review and updates.</p>

    <h3>7.1 Annual Review</h3>
    <ul>
        <li>The Record-Keeping Policy is reviewed annually by the Compliance Officer in coordination with the CTO and MLRO</li>
        <li>The review evaluates:</li>
        <ul>
            <li>Changes in regulatory requirements (FSC, AML/CFT, Data Protection)</li>
            <li>Feedback from audits or inspections</li>
            <li>Technological upgrades or infrastructure changes</li>
            <li>Lessons learned from incidents or breaches</li>
        </ul>
    </ul>

    <h3>7.2 Exceptional Revisions</h3>
    <ul>
        <li>Revisions may be made outside the annual cycle due to:</li>
        <ul>
            <li>New legislation or regulatory guidance</li>
            <li>Significant business changes (e.g., new markets, services, or partners)</li>
            <li>Identified deficiencies or gaps following an audit or incident</li>
            <li>Requests by the Board, FSC, or FIU</li>
        </ul>
    </ul>

    <h3>7.3 Approval & Communication</h3>
    <ul>
        <li>All changes must be:</li>
        <ul>
            <li>Approved by the Board of Directors</li>
            <li>Documented with version control</li>
            <li>Communicated to all relevant staff and vendors</li>
        </ul>
        <li>Mandatory training sessions are held if the revisions affect day-to-day operations or data handling practices</li>
    </ul>

    <h3>7.4 Audit Log & Versioning</h3>
    <ul>
        <li>Each version of the policy is:</li>
        <ul>
            <li>Timestamped, versioned, and archived</li>
            <li>Logged with a summary of key changes and the approval date</li>
            <li>Made available to FSC upon request</li>
        </ul>
    </ul>
