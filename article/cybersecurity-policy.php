    <h2>1. Purpose & Objectives</h2>
    <p>The purpose of this Cybersecurity Policy is to establish a robust and adaptive framework to protect Penthian Ltd’s information systems, digital infrastructure, user data, and tokenized asset marketplace from internal and external cyber threats.</p>
    <p>This policy ensures alignment with:</p>
    <ul>
        <li>The Virtual Asset and Initial Token Offering Services (VAITOS) Act 2021</li>
        <li>Regulatory guidance from the Financial Services Commission (FSC) of Mauritius</li>
        <li>International standards such as ISO/IEC 27001, NIST Cybersecurity Framework, and CIS Controls</li>
        <li>Internal values including integrity, transparency, and investor protection</li>
    </ul>

    <h3>Objectives</h3>
    <p>Penthian’s cybersecurity strategy is designed to achieve the following objectives:</p>
    <h4>1.1 Protection of Information Assets</h4>
    <p>Ensure the confidentiality, integrity, and availability of all data—whether customer, issuer, transactional, or administrative.</p>
    <h4>1.2 Threat Detection & Response</h4>
    <p>Implement proactive monitoring, detection, and rapid response capabilities to mitigate the impact of cyberattacks or unauthorized access.</p>
    <h4>1.3 Secure Platform Infrastructure</h4>
    <p>Maintain security across all layers of Penthian’s infrastructure, including the cloud environment, wallet infrastructure, marketplace, API integrations, and smart contracts.</p>
    <h4>1.4 Regulatory Compliance</h4>
    <p>Satisfy the cybersecurity-related requirements under applicable virtual asset service provider (VASP) laws and frameworks, including auditability and reporting obligations.</p>
    <h4>1.5 User Trust & Resilience</h4>
    <p>Uphold Penthian’s reputation as a secure, transparent, and user-first tokenization platform through clear cybersecurity governance and zero-tolerance for negligence.</p>

    <h2>2. Scope & Applicability</h2>
    <p>This Cybersecurity Policy applies to all digital systems, data repositories, personnel, partners, and third-party service providers involved in the operation of the Penthian platform, including but not limited to:</p>

    <h3>2.1 Covered Systems and Assets</h3>
    <p>The policy applies to the protection and governance of:</p>
    <ul>
        <li>Web platform and mobile application (frontend and backend)</li>
        <li>Multi-chain wallet infrastructure (including self-custody and private key security)</li>
        <li>Smart contracts and token modules (e.g. ERC-3643, ERC-1155)</li>
        <li>Cloud-based hosting and storage systems (AWS, Azure, or equivalent)</li>
        <li>Internal IT systems (email, productivity, file sharing, internal communication tools)</li>
        <li>APIs and third-party integrations (e.g. Veriff, blockchain explorers, compliance tools)</li>
        <li>KYC/AML data storage and transmission pipelines</li>
        <li>Databases housing investor, issuer, and transactional data</li>
        <li>Administrative control panels and developer consoles</li>
        <li>Employee devices accessing Penthian systems</li>
    </ul>

    <h3>2.2 Internal Applicability</h3>
    <p>This policy applies to:</p>
    <ul>
        <li>All employees and contractors of Penthian Ltd</li>
        <li>Executive leadership and board members with access to sensitive systems</li>
        <li>Marketplace operators and compliance personnel</li>
        <li>Platform developers and DevOps engineers</li>
        <li>Marketing, sales, and support teams who access internal platforms or user data</li>
    </ul>

    <h3>2.3 External Applicability</h3>
    <p>The policy also extends to third-party service providers that interact with or manage parts of the Penthian infrastructure. These parties include:</p>
    <ul>
        <li>KYC/AML service providers (e.g. Veriff)</li>
        <li>Cloud service vendors</li>
        <li>Smart contract auditors and security consultants</li>
        <li>Legal and accounting firms with system access</li>
        <li>Joint venture partners and integrators</li>
    </ul>
    <p>Each third-party provider is required to adhere to this policy or provide documentation demonstrating equivalent or superior cybersecurity standards.</p>

    <h2>3. Roles & Responsibilities</h2>
    <p>Effective cybersecurity governance at Penthian Ltd depends on clearly defined responsibilities, accountability at all levels, and coordination between internal teams and external partners. This section outlines the key roles and their cybersecurity obligations.</p>

    <h3>3.1 Board of Directors</h3>
    <p>The Board is ultimately responsible for:</p>
    <ul>
        <li>Approving the Cybersecurity Policy and reviewing it annually</li>
        <li>Ensuring sufficient budget and resources for security initiatives</li>
        <li>Appointing a qualified Chief Information Security Officer (CISO) or equivalent</li>
        <li>Monitoring the overall cybersecurity risk posture</li>
    </ul>

    <h3>3.2 Chief Executive Officer (CEO)</h3>
    <p>The CEO ensures executive oversight and integration of cybersecurity into overall business operations by:</p>
    <ul>
        <li>Supporting compliance with the FSC and global standards</li>
        <li>Promoting a security-first culture across the company</li>
        <li>Overseeing incident response decisions at the executive level</li>
    </ul>

    <h3>3.3 Chief Information Security Officer (CISO)</h3>
    <p>The CISO (or assigned cybersecurity lead) is directly responsible for:</p>
    <ul>
        <li>Implementing and maintaining the cybersecurity program</li>
        <li>Managing threat detection and incident response</li>
        <li>Conducting risk assessments and audits</li>
        <li>Reporting material cybersecurity risks or breaches to the Board and FSC</li>
        <li>Coordinating third-party penetration testing and system hardening</li>
    </ul>

    <h3>3.4 Compliance Officer</h3>
    <p>The Compliance Officer works with the CISO to ensure:</p>
    <ul>
        <li>All cybersecurity policies align with FSC regulatory expectations</li>
        <li>Data protection obligations (e.g. under the Data Protection Act or GDPR) are met</li>
        <li>Third-party providers meet cybersecurity SLAs and audit requirements</li>
    </ul>

    <h3>3.5 IT & Engineering Team</h3>
    <p>This team is responsible for:</p>
    <ul>
        <li>Ensuring secure development practices (DevSecOps)</li>
        <li>Applying updates, security patches, and configurations</li>
        <li>Managing API security and access permissions</li>
        <li>Monitoring infrastructure for intrusion attempts or anomalies</li>
    </ul>

    <h3>3.6 All Employees</h3>
    <p>Every Penthian team member is required to:</p>
    <ul>
        <li>Comply with this Cybersecurity Policy and related IT policies</li>
        <li>Complete annual cybersecurity training</li>
        <li>Use strong passwords and multi-factor authentication</li>
        <li>Report suspicious activities or phishing attempts immediately</li>
    </ul>

    <h3>3.7 Third-Party Vendors</h3>
    <p>Third-party providers must:</p>
    <ul>
        <li>Sign a cybersecurity and confidentiality agreement</li>
        <li>Demonstrate compliance with cybersecurity standards (ISO 27001, SOC 2, or equivalent)</li>
        <li>Cooperate in vulnerability assessments and risk reviews</li>
        <li>Notify Penthian of any known or suspected breaches related to their services</li>
    </ul>

    <h2>4. Risk Management & Assessment</h2>
    <p>Cybersecurity risks are continuously evolving and require ongoing identification, evaluation, and mitigation. Penthian Ltd adopts a structured risk management approach to reduce vulnerabilities and maintain operational integrity across all systems and processes.</p>

    <h3>4.1 Risk Management Framework</h3>
    <p>Penthian’s cybersecurity risk management follows the NIST Cybersecurity Framework and includes:</p>
    <ol>
        <li><strong>Identify</strong> – Assets, systems, data, and threats</li>
        <li><strong>Protect</strong> – Implement controls to safeguard systems</li>
        <li><strong>Detect</strong> – Monitor and identify threats or breaches</li>
        <li><strong>Respond</strong> – Act on security incidents and limit impact</li>
        <li><strong>Recover</strong> – Restore affected services and learn from events</li>
    </ol>

    <h3>4.2 Cyber Risk Categories</h3>
    <p>Penthian identifies the following key categories of cyber risk:</p>
    <table>
        <tr>
            <th>Risk Category</th>
            <th>Examples</th>
        </tr>
        <tr>
            <td>External Threats</td>
            <td>DDoS attacks, ransomware, phishing, credential stuffing</td>
        </tr>
        <tr>
            <td>Internal Threats</td>
            <td>Insider leaks, poor access management, negligent staff</td>
        </tr>
        <tr>
            <td>Third-Party Risk</td>
            <td>API exploits, supply chain attacks, vendor data breaches</td>
        </tr>
        <tr>
            <td>Technology Risk</td>
            <td>Smart contract bugs, code vulnerabilities, outdated libraries</td>
        </tr>
        <tr>
            <td>Compliance Risk</td>
            <td>Violation of FSC rules, data protection laws, or industry standards</td>
        </tr>
    </table>

    <h3>4.3 Risk Assessments</h3>
    <ul>
        <li><strong>Initial Risk Assessment:</strong> Performed during onboarding of any new technology, smart contract, or third-party vendor</li>
        <li><strong>Periodic Reviews:</strong> Conducted quarterly to reassess the threat landscape</li>
        <li><strong>Ad-hoc Assessments:</strong> Triggered by incidents, system changes, or regulatory updates</li>
    </ul>
    <p>Assessments include penetration testing, vulnerability scanning, configuration reviews, and security audits.</p>

    <h3>4.4 Risk Register & Mitigation Plan</h3>
    <p>A centralized Cyber Risk Register is maintained and updated by the CISO. It includes:</p>
    <ul>
        <li>Description of each identified risk</li>
        <li>Probability and impact rating</li>
        <li>Assigned owner or response team</li>
        <li>Mitigation strategies or controls</li>
        <li>Residual risk and monitoring frequency</li>
    </ul>

    <h3>4.5 Risk Reporting</h3>
    <p>Material risks or confirmed breaches are reported to:</p>
    <ul>
        <li>Penthian’s Board of Directors</li>
        <li>The Financial Services Commission (FSC), where applicable</li>
        <li>Impacted users or stakeholders, in compliance with disclosure regulations</li>
    </ul>

    <h2>5. Access Controls & Identity Management</h2>
    <p>Access control is a foundational pillar of Penthian’s cybersecurity framework. This section outlines the principles and systems used to ensure that only authorized users can access sensitive systems, data, or administrative tools, in line with the principles of least privilege and zero trust.</p>

    <h3>5.1 Identity Verification</h3>
    <h4>a. Internal Users</h4>
    <ul>
        <li>All employees must log in using company-issued credentials authenticated via Multi-Factor Authentication (MFA)</li>
        <li>Access to internal systems is tied to job function and role-based permissions</li>
    </ul>
    <h4>b. External Users (Investors, Issuers, Agents)</h4>
    <ul>
        <li>All users must complete KYC/KYB verification via Veriff before gaining platform access</li>
        <li>Wallet addresses are tied to verified identities using ERC-3643 business logic and whitelisting mechanisms</li>
        <li>Session data is encrypted and tracked for behavioral anomalies</li>
    </ul>

    <h3>5.2 Role-Based Access Control (RBAC)</h3>
    <p>Access to systems is governed by predefined roles, including:</p>
    <table>
        <tr>
            <th>Role</th>
            <th>Access Scope</th>
        </tr>
        <tr>
            <td>Admin</td>
            <td>Full backend/platform access including user controls</td>
        </tr>
        <tr>
            <td>Compliance</td>
            <td>Access to KYC/KYB, risk dashboard, and reporting</td>
        </tr>
        <tr>
            <td>Developer</td>
            <td>Codebase, staging, and smart contract deployment only</td>
        </tr>
        <tr>
            <td>Issuer</td>
            <td>Access to listing tools, asset dashboards, and investor messages</td>
        </tr>
        <tr>
            <td>Investor</td>
            <td>Access to view and transact on verified offerings only</td>
        </tr>
        <tr>
            <td>Agent</td>
            <td>Limited access to referral tools and managed property dashboards</td>
        </tr>
    </table>
    <p>Access levels are reviewed quarterly and upon any change in role or employment status.</p>

    <h3>5.3 Password Policy</h3>
    <ul>
        <li>Passwords must meet complexity standards (minimum 12 characters, symbols, upper/lower case)</li>
        <li>MFA is enforced for all staff and critical system access</li>
        <li>Password managers are required for internal staff accounts</li>
        <li>Automatic lockout occurs after 5 failed login attempts</li>
        <li>Passwords are never stored in plaintext</li>
    </ul>

    <h3>5.4 Session Management</h3>
    <ul>
        <li>User sessions automatically expire after 15 minutes of inactivity</li>
        <li>Admin sessions are logged and stored for audit</li>
        <li>Session hijacking attempts are flagged by anomaly detection</li>
    </ul>

    <h3>5.5 Device & Network Controls</h3>
    <ul>
        <li>Access to Penthian’s internal platforms is limited to company-approved devices</li>
        <li>All employee devices must have up-to-date antivirus and endpoint protection</li>
        <li>Remote access requires VPN and device fingerprinting</li>
        <li>IP whitelisting is used for sensitive admin panels and backend services</li>
    </ul>

    <h3>5.6 Access Review & De-provisioning</h3>
    <ul>
        <li>Access rights are reviewed quarterly</li>
        <li>Immediate deactivation is enforced upon employee exit or contract termination</li>
        <li>Automated tools are used to detect and revoke stale or inactive accounts</li>
    </ul>

    <h2>6. Data Security & Encryption</h2>
    <p>Protecting sensitive user, investor, issuer, and operational data is critical to Penthian’s trust model. This section outlines how data is secured during storage, transmission, and use—using encryption, classification, and restricted access mechanisms.</p>

    <h3>6.1 Data Classification</h3>
    <p>Penthian classifies data into the following categories:</p>
    <table>
        <tr>
            <th>Classification</th>
            <th>Examples</th>
            <th>Access Level</th>
        </tr>
        <tr>
            <td>Confidential</td>
            <td>KYC/KYB documents, investor transactions, wallet addresses</td>
            <td>Restricted to Compliance & Admin</td>
        </tr>
        <tr>
            <td>Internal</td>
            <td>Employee records, internal communications, financial reports</td>
            <td>Staff access based on role</td>
        </tr>
        <tr>
            <td>Public</td>
            <td>Platform FAQs, marketing materials, investor education</td>
            <td>Open to all users</td>
        </tr>
    </table>
    <p>All classified data is labeled and stored in systems with appropriate access controls.</p>

    <h3>6.2 Data Storage Security</h3>
    <ul>
        <li><strong>Data at Rest:</strong> All databases are encrypted using AES-256 encryption standards</li>
        <li><strong>Cloud Storage:</strong> Hosted on secure, ISO 27001-compliant providers with encryption keys managed via HSMs (Hardware Security Modules)</li>
        <li><strong>Smart Contracts:</strong> Data stored on-chain (e.g. ownership records) is hashed or obfuscated where feasible</li>
        <li><strong>Backups:</strong> Encrypted backups are performed daily and stored in geographically separate data centers with 30-day retention</li>
    </ul>

    <h3>6.3 Data in Transit</h3>
    <ul>
        <li>TLS 1.3 encryption is enforced on all web and API communications</li>
        <li>All data transferred between internal services (microservices, APIs) uses mutual TLS (mTLS)</li>
        <li>SFTP is used for any file-based data exchange between systems</li>
    </ul>
    
     <h3>6.4 KYC/KYB Data Handling</h3>
    <ul>
        <li>Veriff integration ensures that identity documents are handled in compliance with GDPR and FSC requirements</li>
        <li>Personally identifiable information (PII) is encrypted both at rest and in transit</li>
        <li>Access to verification records is limited to the Compliance Team, with all access logged</li>
    </ul>

    <h3>6.5 Token & Wallet Data</h3>
    <ul>
        <li>Wallet private keys are never stored by Penthian for self-custody wallets</li>
        <li>Any custodial features are protected by multi-signature authorization and air-gapped cold storage</li>
        <li>Smart contract interactions are audited and signed by verified roles only</li>
    </ul>

    <h3>6.6 Third-Party Data Handling</h3>
    <ul>
        <li>All vendors handling data (e.g. Veriff, analytics providers, email delivery systems) are required to sign Data Protection Agreements</li>
        <li>Vendor systems must meet or exceed Penthian’s encryption and storage standards</li>
        <li>Data access by third parties is monitored and logged continuously</li>
    </ul>

    <h2>7. Endpoint Security & Device Controls</h2>
    <p>To safeguard sensitive systems and data, Penthian enforces rigorous controls over all devices (endpoints) used by employees, contractors, and administrators. This section outlines how devices are managed, monitored, and secured.</p>

    <h3>7.1 Device Registration & Authorization</h3>
    <ul>
        <li>All laptops, desktops, and mobile devices accessing Penthian systems must be registered and approved by the IT department</li>
        <li>Device IDs and MAC addresses are logged and associated with user profiles</li>
        <li>Bring Your Own Device (BYOD) is prohibited for staff accessing sensitive environments</li>
    </ul>

    <h3>7.2 Endpoint Protection Tools</h3>
    <p>All authorized devices must have:</p>
    <ul>
        <li>Antivirus & Anti-malware software installed and kept up to date</li>
        <li>Endpoint Detection & Response (EDR) tools for advanced threat monitoring</li>
        <li>Firewall enabled and configured to block unauthorized inbound/outbound traffic</li>
        <li>Auto-lock and screen timeout enabled for mobile and desktop devices</li>
    </ul>

    <h3>7.3 Operating System & Patch Management</h3>
    <ul>
        <li>Operating systems must be updated regularly with security patches</li>
        <li>Unsupported operating systems (e.g. Windows 7) are strictly prohibited</li>
        <li>Penthian uses automated tools to enforce patch compliance and scan for vulnerabilities</li>
    </ul>

    <h3>7.4 Disk & File Encryption</h3>
    <ul>
        <li>All laptops and desktops must use full-disk encryption (e.g. BitLocker or FileVault)</li>
        <li>Sensitive files stored locally must be encrypted individually using approved tools</li>
        <li>USB drives and external storage must be encrypted and approved before use</li>
    </ul>

    <h3>7.5 Remote Access Controls</h3>
    <ul>
        <li>Remote access is only permitted through company-approved VPNs</li>
        <li>Devices must be authenticated via multi-factor authentication (MFA) and IP whitelisting</li>
        <li>Remote desktop protocols (RDP) are monitored for unusual activity</li>
    </ul>

    <h3>7.6 Device Monitoring & Audits</h3>
    <ul>
        <li>All endpoint activity is logged, including access times, IP addresses, and device IDs</li>
        <li>Logs are reviewed periodically to identify anomalous or suspicious behavior</li>
        <li>Devices not in compliance are quarantined and reported to IT security for remediation</li>
    </ul>

    <h3>7.7 Lost or Stolen Devices</h3>
    <p>If a device is lost or stolen:</p>
    <ol>
        <li>User must report incident within 1 hour</li>
        <li>IT team triggers remote lock or remote wipe</li>
        <li>Security team reviews access logs to assess breach exposure</li>
        <li>Incident report is filed and escalated if user data is suspected to be compromised</li>
    </ol>

    <h2>8. Incident Response Plan (IRP)</h2>
    <p>Penthian maintains a structured and rapid response framework to detect, contain, mitigate, and recover from cybersecurity incidents. This ensures platform resilience, regulatory compliance, and continued investor trust.</p>

    <h3>8.1 Incident Definition</h3>
    <p>A security incident includes any event that:</p>
    <ul>
        <li>Compromises the confidentiality, integrity, or availability of Penthian systems or data</li>
        <li>Violates cybersecurity policies or applicable laws</li>
        <li>Involves unauthorized access, disclosure, alteration, or destruction of data</li>
        <li>Affects wallet infrastructure, smart contracts, or token transaction logic</li>
        <li>Triggers alarms from security monitoring or third-party vendors</li>
    </ul>

    <h3>8.2 Incident Response Team (IRT)</h3>
    <p>The Incident Response Team includes:</p>
    <ul>
        <li>Chief Information Security Officer (CISO) – IRP coordinator and lead responder</li>
        <li>Compliance Officer – Regulatory reporting and user disclosure</li>
        <li>Engineering Lead – Technical triage, patching, and rollback</li>
        <li>Legal Representative – Liabilities, breach disclosure obligations</li>
        <li>Communications Lead – Internal and external updates</li>
    </ul>

    <h3>8.3 Incident Response Stages</h3>
    <table>
        <tr>
            <th>Stage</th>
            <th>Action</th>
        </tr>
        <tr>
            <td>1. Identification</td>
            <td>Detect anomaly via automated alerts or manual reports</td>
        </tr>
        <tr>
            <td>2. Containment</td>
            <td>Isolate affected systems or services to prevent further damage</td>
        </tr>
        <tr>
            <td>3. Investigation</td>
            <td>Gather logs, data, and witness input to determine scope and origin</td>
        </tr>
        <tr>
            <td>4. Notification</td>
            <td>Inform internal teams, regulators, and users as required</td>
        </tr>
        <tr>
            <td>5. Eradication</td>
            <td>Remove malware, close vulnerabilities, revoke access if needed</td>
        </tr>
        <tr>
            <td>6. Recovery</td>
            <td>Restore systems from backup, verify integrity, resume operations</td>
        </tr>
        <tr>
            <td>7. Postmortem</td>
            <td>Analyze root cause, update playbooks, report to board & FSC</td>
        </tr>
    </table>

    <h3>8.4 Incident Classification</h3>
    <p>Incidents are categorized by severity:</p>
    <table>
        <tr>
            <th>Level</th>
            <th>Impact</th>
            <th>Response Time</th>
        </tr>
        <tr>
            <td>Critical</td>
            <td>Major system outage, data breach, smart contract exploit</td>
            <td>Immediate</td>
        </tr>
        <tr>
            <td>High</td>
            <td>Unauthorized access, phishing with credentials at risk</td>
            <td>≤ 2 hours</td>
        </tr>
        <tr>
            <td>Medium</td>
            <td>Failed intrusion attempt, suspicious login patterns</td>
            <td>≤ 24 hours</td>
        </tr>
        <tr>
            <td>Low</td>
            <td>Policy violations, misconfigured access</td>
            <td>≤ 72 hours</td>
        </tr>
    </table>

    <h3>8.5 Reporting & Disclosure</h3>
    <ul>
        <li>All incidents are logged in the Incident Register</li>
        <li>Material incidents must be reported to the FSC within 72 hours</li>
        <li>Affected users will be notified with details of scope, impact, and remedial actions</li>
    </ul>

    <h3>8.6 Post-Incident Review</h3>
    <p>After resolution, the IR Team will:</p>
    <ul>
        <li>Conduct a lessons learned session</li>
        <li>Review timeline, detection gaps, and response speed</li>
        <li>Recommend policy updates, technical upgrades, or training needs</li>
        <li>File an official Incident Report Summary for Board and regulator records</li>
    </ul>

    <h2>9. Security Monitoring & Threat Detection</h2>
    <p>Penthian employs layered monitoring tools and processes to detect potential cyber threats in real time. Early detection is key to protecting investor assets, platform integrity, and regulatory compliance.</p>

    <h3>9.1 Continuous Monitoring</h3>
    <p>Penthian uses Security Information and Event Management (SIEM) systems and automated tools to monitor:</p>
    <ul>
        <li>Web & Mobile Application Logs</li>
        <li>Smart Contract Activity</li>
        <li>Admin and User Login Attempts</li>
        <li>API Usage and Rate Patterns</li>
        <li>Cloud Infrastructure Logs</li>
        <li>Third-party Integrations and Data Flows</li>
        <li>Wallet Transaction Flows (on-chain)</li>
    </ul>
    <p>All logs are centralized, timestamped, and stored in secure, immutable storage for a minimum of 12 months.</p>

    <h3>9.2 Anomaly Detection</h3>
    <p>Monitoring tools use behavior-based algorithms to identify unusual patterns such as:</p>
    <ul>
        <li>Repeated failed login attempts (possible brute-force attacks)</li>
        <li>Abnormal wallet transaction sizes or timing</li>
        <li>Access attempts from blacklisted IP addresses or geolocations</li>
        <li>Code or contract interaction that deviates from typical user flows</li>
        <li>Excessive API calls that suggest scraping or abuse</li>
    </ul>
    <p>Alerts are triaged by the CISO team with appropriate severity ratings.</p>

    <h3>9.3 Smart Contract Monitoring</h3>
    <p>Penthian uses automated services to monitor smart contracts for:</p>
    <ul>
        <li>Reentrancy attacks</li>
        <li>Overflows or underflows</li>
        <li>Unauthorized minting or transfer activity</li>
        <li>Function call abuse (e.g., price manipulation)</li>
    </ul>
    <p>Each critical contract includes built-in pause mechanisms to halt trading if a threat is detected.</p>

    <h3>9.4 Blockchain & Wallet Watchlists</h3>
    <p>Wallets associated with past breaches, hacks, or dark net activity are flagged using global threat intelligence databases (e.g., Chainalysis, CipherTrace).</p>
    <p>Suspicious wallets interacting with the platform are immediately reported to Compliance and placed on a quarantine list pending investigation.</p>

    <h3>9.5 Physical Access Monitoring</h3>
    <p>If Penthian establishes any physical infrastructure (e.g., office with servers or admin terminals), access logs and CCTV may be used to track and review physical security violations.</p>

    <h3>9.6 External Intelligence Feeds</h3>
    <p>Penthian subscribes to multiple external sources for up-to-date threat intelligence, including:</p>
    <ul>
        <li>Mauritius National CERT alerts</li>
        <li>Cybersecurity ISACs (Information Sharing & Analysis Centers)</li>
        <li>Global blockchain exploit trackers</li>
        <li>FSC and FATF guidance updates</li>
    </ul>

    <h2>10. Vendor & Third-Party Risk Management</h2>
    <p>As a platform that integrates with multiple external vendors, APIs, and smart contract providers, Penthian maintains a strict risk management protocol for all third-party relationships. This ensures continuity of service, data protection, and security alignment across our digital infrastructure.</p>

    <h3>10.1 Vendor Selection & Due Diligence</h3>
    <p>Before onboarding any vendor, Penthian performs a due diligence process that includes:</p>
    <ul>
        <li>Evaluation of cybersecurity certifications (ISO 27001, SOC 2, etc.)</li>
        <li>Review of security policies, incident response procedures, and historical breaches</li>
        <li>Legal review of Data Protection Agreements (DPAs) and Service Level Agreements (SLAs)</li>
        <li>Assessment of technical integrations and API vulnerabilities</li>
    </ul>
    <p>Vendors must meet or exceed the security standards outlined in this policy.</p>

    <h3>10.2 Approved Vendor List</h3>
    <p>Only vendors included in the Approved Vendor Register may be used for:</p>
    <ul>
        <li>KYC/KYB and identity verification (e.g. Veriff)</li>
        <li>Cloud hosting and infrastructure (e.g. AWS, Azure)</li>
        <li>Email communication (e.g. SendGrid, Mailchimp)</li>
        <li>Smart contract auditing (e.g. OpenZeppelin, Certik)</li>
        <li>Data analytics and monitoring tools</li>
    </ul>
    <p>This register is reviewed quarterly by Compliance and the CISO.</p>

    <h3>10.3 Contractual Obligations</h3>
    <p>All vendor agreements must include:</p>
    <ul>
        <li>Data processing terms compliant with GDPR and local law</li>
        <li>Breach notification clauses (within 48 hours)</li>
        <li>Right to audit by Penthian or regulators</li>
        <li>Termination clauses for failure to meet cybersecurity requirements</li>
    </ul>

    <h3>10.4 Ongoing Monitoring</h3>
    <p>Once onboarded, vendors are subject to:</p>
    <ul>
        <li>Annual or bi-annual security review</li>
        <li>Penetration testing of exposed APIs or SDKs</li>
        <li>Uptime and service delivery audits</li>
        <li>Incident reporting drills and tabletop exercises</li>
    </ul>

    <h3>10.5 Third-Party Wallet & Token Risks</h3>
    <p>For smart contract and token integrations:</p>
    <ul>
        <li>All contracts must be audited before deployment</li>
        <li>Permissions must be minimized and follow the least privilege principle</li>
        <li>Whitelists/blacklists are updated in real time to prevent compromised contracts from interacting with the marketplace</li>
        <li>All ERC-20 and ERC-1155 tokens are scanned for malicious behavior patterns</li>
    </ul>

    <h3>10.6 Termination & Offboarding</h3>
    <p>When offboarding a vendor:</p>
    <ul>
        <li>Access to systems is immediately revoked</li>
        <li>Data held by the vendor is destroyed or returned per DPA terms</li>
        <li>Any replacement vendor must undergo the full due diligence process before handover</li>
    </ul>

    <h2>11. Training, Awareness & Culture</h2>
    <p>Cybersecurity is not just technical—it’s cultural. Penthian embeds security awareness into its team DNA by educating employees, partners, and contractors at all levels about their responsibilities and best practices.</p>

    <h3>11.1 Onboarding Security Training</h3>
    <ul>
        <li>Every new hire, regardless of department, must complete a mandatory cybersecurity training program within their first 7 days</li>
        <li>Training includes phishing awareness, password hygiene, data protection, secure device use, and reporting procedures</li>
        <li>Developers receive additional training on secure coding practices and smart contract risks</li>
    </ul>

    <h3>11.2 Annual Refresher Training</h3>
    <ul>
        <li>All employees must pass an annual cybersecurity exam and simulation</li>
        <li>Content is updated regularly to reflect new threats, scams, and technologies (e.g., deepfakes, wallet scams, AI risks)</li>
        <li>A passing score is required for continued access to sensitive systems</li>
    </ul>

    <h3>11.3 Phishing Simulations</h3>
    <ul>
        <li>Penthian conducts periodic simulated phishing tests to assess user vigilance</li>
        <li>Employees who fall for simulations are required to retake targeted training</li>
        <li>Results are anonymized but used to improve organizational awareness</li>
    </ul>

    <h3>11.4 Developer & Technical Training</h3>
    <p>Developers, engineers, and DevOps must complete training on:</p>
    <ul>
        <li>Smart contract security (e.g. reentrancy, overflows, gas limit attacks)</li>
        <li>Safe deployment pipelines and staging procedures</li>
        <li>Key management and signature verification</li>
        <li>Common Web3 vulnerabilities (e.g., front-running, flash loans)</li>
    </ul>

    <h3>11.5 Vendor Awareness</h3>
    <p>Vendors with access to Penthian systems are provided with a Security Handbook and are contractually required to ensure their teams understand and adhere to:</p>
    <ul>
        <li>Platform-specific security guidelines</li>
        <li>Secure API and SDK usage</li>
        <li>Data access policies and obligations</li>
    </ul>

    <h3>11.6 Culture of Reporting</h3>
    <ul>
        <li>All employees are encouraged to report suspicious activity immediately to the CISO or IT team without fear of reprisal</li>
        <li>Anonymous whistleblower mechanisms are available for internal misuse or compliance failures</li>
        <li>Security wins (e.g. threats caught, breaches avoided) are celebrated and communicated internally to reinforce vigilance</li>
    </ul>

    <h3>11.7 Leadership & Tone from the Top</h3>
    <ul>
        <li>The CEO and senior executives champion cybersecurity by participating in training, reviewing reports, and allocating resources</li>
        <li>The Fruits of the Spirit—Love, Joy, Peace, Patience, Kindness, Goodness, Faithfulness, Gentleness, and Self-Control—are embedded into Penthian’s leadership style and decision-making, fostering trust, responsibility, and ethical technology use</li>
    </ul>

    <h2>12. Policy Governance & Review</h2>
    <p>This section outlines the governance structure and review procedures to ensure the Cybersecurity Policy remains aligned with evolving threats, regulatory expectations, and Penthian’s operational growth.</p>

    <h3>12.1 Ownership & Accountability</h3>
    <ul>
        <li>The Chief Information Security Officer (CISO) is the primary owner of this policy</li>
        <li>The Board of Directors provides oversight and ensures cybersecurity is prioritized as a business imperative</li>
        <li>Department heads are accountable for enforcing relevant controls within their functions (e.g. DevOps, Compliance, Marketing)</li>
    </ul>

    <h3>12.2 Governance Committee</h3>
    <p>A Cybersecurity & Risk Committee meets quarterly and includes:</p>
    <ul>
        <li>CISO (Chair)</li>
        <li>Compliance Officer</li>
        <li>Legal Counsel</li>
        <li>CTO or Engineering Lead</li>
        <li>Independent advisor or external auditor (if applicable)</li>
    </ul>
    <p>The committee reviews all major incidents, new threat reports, audit outcomes, and required updates to the policy.</p>

    <h3>12.3 Policy Review Cycle</h3>
    <ul>
        <li>The Cybersecurity Policy is reviewed and updated at least once per year</li>
        <li>Reviews are triggered earlier if:</li>
        <li>A significant breach or incident occurs</li>
        <li>New FSC or FATF guidance is issued</li>
        <li>Material changes to Penthian’s business model or architecture take place</li>
        <li>A vendor or system change creates new risks</li>
    </ul>

    <h3>12.4 Change Management</h3>
    <ul>
        <li>Any policy update must be documented, versioned, and approved by the Cybersecurity & Risk Committee</li>
        <li>Material updates are communicated to all staff via email and posted on the internal policy portal</li>
        <li>Users with access to critical systems must confirm policy acknowledgment</li>
    </ul>

    <h3>12.5 External Audits & Certification</h3>
    <ul>
        <li>Penthian will conduct annual penetration tests and third-party audits of its cybersecurity systems</li>
        <li>As the business scales, Penthian may pursue industry certifications such as:</li>
        <li>ISO/IEC 27001</li>
        <li>SOC 2 Type II</li>
        <li>FSC-accepted assurance reports</li>
        <li>Audit reports and remediation efforts will be shared with the Board and regulators as needed.</li>
    </ul>

    <h3>12.6 Breach Reporting to Regulators</h3>
    <ul>
        <li>Any confirmed cyber breach that affects customer funds, tokens, wallets, or PII will be reported to the FSC Mauritius within 72 hours, along with mitigation and recovery plans</li>
        <li>Affected users will be notified transparently, in line with local laws and platform commitment to investor protection</li>
    </ul>
