     <h2>For Class S Virtual Asset Marketplace License (Mauritius FSC)</h2>

    <div class="section">
        <h2>1. Introduction & Purpose</h2>
        <p>The purpose of this policy is to establish a framework for the identification, assessment, approval, and monitoring of all outsourced functions and service providers used by Penthian Ltd in its capacity as a licensed Virtual Asset Marketplace (Class S) under the Financial Services Commission (FSC) of Mauritius.</p>
        <p>Penthian acknowledges that outsourcing, while offering operational efficiency and specialized expertise, also introduces specific risks to compliance, data security, and business continuity. This policy ensures that any outsourced service maintains the same standards of integrity, confidentiality, and performance as those performed in-house.</p>
    </div>

    <div class="section">
        <h2>2. Scope & Applicability</h2>
        <p>This policy applies to all current and future outsourcing arrangements entered into by Penthian Ltd, across all departments and subsidiaries involved in the operation of its virtual asset marketplace platform.</p>
        <p>It covers:</p>
        <h3>2.1 Types of Outsourced Activities</h3>
        <p>The following are considered material or critical outsourced services and are subject to this policy:</p>
        <ul>
            <li>Technology Infrastructure: Hosting, cloud storage, cybersecurity tools, and developer resources</li>
            <li>Compliance Services: KYC/AML screening, sanctions checks, identity verification (e.g., Veriff)</li>
            <li>Smart Contract Audits: Third-party code review and audit reporting services</li>
            <li>Legal & Regulatory Advisory: External law firms or regulatory consultants</li>
            <li>Custodial Wallet Infrastructure: Third-party infrastructure used for wallet key management or recovery</li>
            <li>Tokenization & Asset Verification: Partners involved in real estate or SME token listing preparation</li>
            <li>Payment Gateways & Settlement: Fiat on/off-ramp partners or transaction processors</li>
        </ul>
        <h3>2.2 Functions Excluded</h3>
        <p>The following are not considered outsourcing under this policy:</p>
        <ul>
            <li>Procurement of off-the-shelf software</li>
            <li>Use of standard cloud-based office productivity tools (e.g., Google Workspace, Slack)</li>
            <li>External recruitment and staffing agencies for temporary staff</li>
            <li>Legal counsel not directly handling token issuance or investor compliance</li>
        </ul>
        <h3>2.3 Applicability</h3>
        <p>This policy is binding for:</p>
        <ul>
            <li>Board of Directors</li>
            <li>Compliance Officer</li>
            <li>Chief Technology Officer</li>
            <li>Any department engaging with or managing third-party service providers</li>
            <li>Any external vendor contract that may materially impact client data, regulatory obligations, or business continuity</li>
        </ul>
    </div>

    <div class="section">
        <h2>3. Regulatory Framework & Guiding Principles</h2>
        <p>Penthian Ltd adheres to all relevant regulatory requirements issued by the Financial Services Commission (FSC) of Mauritius, including those under the Virtual Asset and Initial Token Offering Services Act 2021 (VAITOS Act) and related FSC guidelines for outsourcing by financial institutions.</p>
        <h3>3.1 Regulatory Compliance</h3>
        <p>Outsourced arrangements must comply with:</p>
        <ul>
            <li>VAITOS Act and subsequent FSC rules regarding licensed Virtual Asset Service Providers</li>
            <li>Data Protection Act 2017 (Mauritius) and General Data Protection Regulation (GDPR) where applicable</li>
            <li>FATF Recommendations on outsourcing in the context of virtual asset and AML/CFT obligations</li>
        </ul>
        <p>Penthian ensures that outsourcing does not diminish the effectiveness of its governance or hinder regulatory oversight.</p>
        <h3>3.2 Core Principles</h3>
        <p>All outsourcing relationships must reflect the following guiding principles:</p>
        <ol>
            <li><strong>Accountability</strong>
                <ul>
                    <li>Penthian remains fully responsible for outsourced functions, including compliance with laws and regulations, and cannot delegate this responsibility to vendors.</li>
                </ul>
            </li>
            <li><strong>Due Diligence</strong>
                <ul>
                    <li>Comprehensive due diligence must be conducted before entering into any outsourcing arrangement, including assessments of financial strength, technical capability, security posture, and regulatory standing.</li>
                </ul>
            </li>
            <li><strong>Confidentiality & Data Protection</strong>
                <ul>
                    <li>Outsourced providers must comply with confidentiality obligations and data protection standards equal to or exceeding Penthian’s own policies.</li>
                </ul>
            </li>
            <li><strong>Continuity & Exit Strategy</strong>
                <ul>
                    <li>All contracts must include provisions for business continuity and an exit plan to allow Penthian to terminate and replace providers with minimal disruption.</li>
                </ul>
            </li>
            <li><strong>Auditability</strong>
                <ul>
                    <li>Vendors must agree to provide audit rights and regular reporting to Penthian and, where requested, to regulators.</li>
                </ul>
            </li>
            <li><strong>Materiality Assessment</strong>
                <ul>
                    <li>Outsourcing arrangements are reviewed to determine if they are material or critical to the delivery of regulated services.</li>
                </ul>
            </li>
        </ol>
    </div>

    <div class="section">
        <h2>4. Risk Assessment & Due Diligence</h2>
        <p>Before entering into any outsourcing arrangement, Penthian conducts a thorough assessment of associated risks and performs structured due diligence to ensure the suitability and reliability of the service provider.</p>
        <h3>4.1 Risk Assessment Criteria</h3>
        <p>Each potential outsourcing arrangement is evaluated based on:</p>
        <ul>
            <li>Nature and criticality of the outsourced function</li>
            <li>Impact on Penthian’s regulatory obligations</li>
            <li>Data sensitivity and volume involved</li>
            <li>Dependency level on the outsourced service</li>
            <li>Reputation and financial stability of the provider</li>
            <li>Location and legal environment of the service provider (onshore vs offshore)</li>
        </ul>
        <p>Risks are scored according to probability and severity, and only providers with an acceptable risk profile are approved.</p>
        <h3>4.2 Due Diligence Checklist</h3>
        <p>The Compliance and Procurement teams are responsible for completing a due diligence report which includes the following:</p>
        <table>
            <tr>
                <th>Due Diligence Area</th>
                <th>Examples</th>
            </tr>
            <tr>
                <td>Legal Standing</td>
                <td>Company registration, licenses, litigation history</td>
            </tr>
            <tr>
                <td>Financial Stability</td>
                <td>Financial statements, funding history, client references</td>
            </tr>
            <tr>
                <td>Technical Capability</td>
                <td>Infrastructure scalability, security certifications (e.g., ISO 27001)</td>
            </tr>
            <tr>
                <td>Compliance & Controls</td>
                <td>Internal controls, data protection policies, incident response process</td>
            </tr>
            <tr>
                <td>Business Continuity</td>
                <td>Disaster recovery procedures, SLA commitments, past performance reviews</td>
            </tr>
            <tr>
                <td>Sub-outsourcing Disclosure</td>
                <td>Disclosure of subcontractors, approval and audit rights</td>
            </tr>
        </table>
        <h3>4.3 Approval Process</h3>
        <p>All material or critical outsourcing engagements must be approved by:</p>
        <ul>
            <li>The Chief Operating Officer or equivalent</li>
            <li>The Compliance Officer</li>
            <li>The Board of Directors (for strategic or high-risk functions)</li>
        </ul>
        <p>A written Outsourcing Agreement must be signed, including:</p>
        <ul>
            <li>Scope of services</li>
            <li>Data protection obligations</li>
            <li>Audit rights</li>
            <li>Exit and termination clauses</li>
            <li>Penalties for breach of contract</li>
        </ul>
    </div>

    <div class="section">
        <h2>5. Contractual & Legal Safeguards</h2>
        <p>All outsourcing arrangements must be governed by a formal, written agreement that clearly defines the responsibilities, obligations, and liabilities of the service provider. These agreements ensure regulatory compliance, business continuity, and legal protection for Penthian Ltd.</p>
        <h3>5.1 Mandatory Clauses</h3>
        <p>Every outsourcing agreement must include:</p>
        <ol>
            <li><strong>Scope of Services</strong>
                <ul>
                    <li>Detailed description of the functions to be performed</li>
                    <li>Service levels and performance metrics (SLAs)</li>
                </ul>
            </li>
            <li><strong>Confidentiality</strong>
                <ul>
                    <li>Obligation to protect Penthian’s data and intellectual property</li>
                    <li>Non-disclosure of client information to unauthorized parties</li>
                </ul>
            </li>
            <li><strong>Data Protection</strong>
                <ul>
                    <li>Compliance with the Mauritius Data Protection Act 2017, GDPR, and other applicable laws</li>
                    <li>Requirement for encrypted data transmission and secure storage</li>
                    <li>Limitation on cross-border data transfers without prior written approval</li>
                </ul>
            </li>
            <li><strong>Business Continuity</strong>
                <ul>
                    <li>Requirement to maintain disaster recovery plans and redundancies</li>
                    <li>Commitment to restore services within predefined RTO and RPO targets</li>
                </ul>
            </li>
            <li><strong>Audit & Inspection Rights</strong>
                <ul>
                    <li>Penthian and the FSC must have the right to audit the service provider</li>
                    <li>Providers must supply reports and respond to audit findings in a timely manner</li>
                </ul>
            </li>
            <li><strong>Subcontracting Restrictions</strong>
                <ul>
                    <li>Subcontracting requires Penthian’s prior written consent</li>
                    <li>Subcontractors must be subject to the same controls and obligations</li>
                </ul>
            </li>
            <li><strong>Termination & Exit</strong>
                <ul>
                    <li>Conditions under which the agreement can be terminated</li>
                    <li>Provision for smooth transition of services, data return, and deletion</li>
                    <li>Penalties for early termination or breach</li>
                </ul>
            </li>
        </ol>
        <h3>5.2 Jurisdiction & Dispute Resolution</h3>
        <p>Contracts must clearly define governing law and dispute resolution mechanisms:</p>
        <ul>
            <li>Preference is given to the jurisdiction of Mauritius or a mutually agreed neutral venue</li>
            <li>Dispute resolution may include:
                <ul>
                    <li>Escalation process</li>
                    <li>Mediation or arbitration</li>
                    <li>Court proceedings (if unresolved)</li>
                </ul>
            </li>
        </ul>
        <h3>5.3 Periodic Review</h3>
        <p>Contracts are reviewed annually to ensure terms remain relevant, enforceable, and aligned with regulatory updates:</p>
        <ul>
            <li>Updated SLAs</li>
            <li>Changes in scope or technology</li>
            <li>Revised compliance requirements</li>
        </ul>
    </div>

    <div class="section">
        <h2>6. Ongoing Oversight & Performance Monitoring</h2>
        <p>To ensure continued alignment with regulatory standards and internal risk management objectives, Penthian maintains structured oversight of all outsourced service providers. This includes regular performance reviews, compliance audits, and risk reassessments.</p>
        <h3>6.1 Oversight Responsibilities</h3>
        <p>The following roles are responsible for overseeing outsourcing arrangements:</p>
        <table>
            <tr>
                <th>Role</th>
                <th>Responsibility</th>
            </tr>
            <tr>
                <td>Outsourcing Manager</td>
                <td>Day-to-day vendor relationship management</td>
            </tr>
            <tr>
                <td>Compliance Officer</td>
                <td>Monitoring regulatory alignment and data protection compliance</td>
            </tr>
            <tr>
                <td>IT & Security Team</td>
                <td>Assessing security posture and technical performance</td>
            </tr>
            <tr>
                <td>Executive Management</td>
                <td>Oversight of critical services, escalation of breaches</td>
            </tr>
        </table>
        <h3>6.2 Performance Monitoring Tools</h3>
        <p>Penthian applies the following tools to monitor vendor performance:</p>
        <ul>
            <li>Service Level Agreements (SLAs): Tracked against uptime, response times, and resolution rates</li>
            <li>Quarterly Performance Reviews: Formal evaluations of vendor output, responsiveness, and reliability</li>
            <li>Risk Scorecards: Updated to reflect vendor-related incidents, compliance breaches, or infrastructure issues</li>
            <li>Vendor Score Reports: Internal scoring of financial, technical, and legal health</li>
        </ul>
        <h3>6.3 Issue Escalation Protocol</h3>
        <p>Issue escalation protocol includes:</p>
        <ul>
            <li>Minor issues are logged and addressed with the vendor via standard support channels</li>
            <li>Repeated or unresolved issues are escalated to the Outsourcing Oversight Committee</li>
            <li>High-risk or regulatory breaches must be reported to the Board and, if necessary, the FSC</li>
        </ul>
        <h3>6.4 Compliance Reassessments</h3>
        <p>A full compliance and data protection review is conducted at least annually:</p>
        <ul>
            <li>High-risk or material vendors may be subject to:
                <ul>
                    <li>Site visits or virtual audits</li>
                    <li>Penetration testing or code review</li>
                    <li>External third-party audits at vendor’s expense</li>
                </ul>
            </li>
        </ul>
    </div>

    <div class="section">
        <h2>7. Business Continuity & Exit Strategy</h2>
        <p>Penthian Ltd ensures that all outsourcing arrangements support operational resilience and provide a clear path to recovery or termination in the event of service interruption, regulatory failure, or contractual breach.</p>
        <h3>7.1 Business Continuity Planning (BCP)</h3>
        <p>All outsourced service providers classified as material or critical must:</p>
        <ul>
            <li>Maintain their own documented Business Continuity Plans (BCP)</li>
            <li>Commit to Recovery Time Objectives (RTO) and Recovery Point Objectives (RPO) that align with Penthian’s platform</li>
            <li>Conduct at least annual BCP testing and share test results when requested</li>
            <li>Provide emergency escalation contacts and 24/7 availability for high-impact incidents</li>
            <li>Ensure continuity of service during global disruptions, including:
                <ul>
                    <li>Cyberattacks</li>
                    <li>Cloud outages</li>
                    <li>Political or economic instability</li>
                    <li>Pandemics or public emergencies</li>
                </ul>
            </li>
        </ul>
        <h3>7.2 Exit Planning & Vendor Substitution</h3>
        <p>Penthian maintains a proactive exit strategy for every critical vendor to protect platform integrity and investor trust. Exit Plans Include:</p>
        <ul>
            <li>Identification of alternate service providers or internalization plans</li>
            <li>Data migration procedures with verifiable handover of:
                <ul>
                    <li>Source code (if applicable)</li>
                    <li>Logs and audit trails</li>
                    <li>Client records</li>
                </ul>
            </li>
            <li>Re-certification of infrastructure security upon migration</li>
            <li>Legal notice periods and wind-down arrangements</li>
        </ul>
        <p>Triggers for Exit:</p>
        <ul>
            <li>Repeated SLA violations</li>
            <li>Regulatory non-compliance</li>
            <li>Breach of contract or data leak</li>
            <li>Change of control or insolvency</li>
        </ul>
        <h3>7.3 Client Impact Mitigation</h3>
        <p>To protect users and investors during vendor exits:</p>
        <ul>
            <li>Clients are notified in advance (where material impact is anticipated)</li>
            <li>No disruption occurs to asset custody, access to funds, or transaction history</li>
            <li>Penthian maintains technical and legal ownership of smart contracts and all tokenized assets</li>
            <li>Backup providers or internal fallback tools are activated immediately if needed</li>
        </ul>
    </div>

    <div class="section">
        <h2>8. Documentation, Reporting & Review</h2>
        <p>Penthian maintains comprehensive records of all outsourcing arrangements and ensures that oversight processes are reviewed regularly to align with evolving business needs and regulatory standards.</p>
        <h3>8.1 Documentation Requirements</h3>
        <p>All outsourcing activities must be fully documented and centrally stored for ease of access and audit readiness. This includes:</p>
        <ul>
            <li>Copies of signed outsourcing agreements</li>
            <li>Due diligence reports</li>
            <li>Risk assessments</li>
            <li>Ongoing performance evaluations</li>
            <li>Audit and incident reports</li>
            <li>Business continuity plans of service providers</li>
        </ul>
        <p>These records are retained for a minimum of 7 years or as otherwise required by the FSC or applicable data protection legislation.</p>
        <h3>8.2 Internal Reporting</h3>
        <p>The Compliance Officer provides quarterly reports to the Board summarizing:</p>
        <ul>
            <li>New outsourcing arrangements</li>
            <li>Risk ratings and critical dependencies</li>
            <li>SLA performance trends and audit findings</li>
            <li>Breaches, escalations, and remediation plans</li>
        </ul>
        <p>The Outsourcing Oversight Committee, composed of key executives, meets semi-annually to:</p>
        <ul>
            <li>Review material vendors</li>
            <li>Reassess outsourcing strategy</li>
            <li>Update exit and continuity plans</li>
        </ul>
        <h3>8.3 Annual Policy Review</h3>
        <p>This Outsourcing Policy is subject to formal review at least once every 12 months and also in the event of:</p>
        <ul>
            <li>Material changes to regulatory obligations (e.g., updated FSC or VAITOS guidance)</li>
            <li>New categories of critical outsourcing</li>
            <li>Major incidents involving current service providers</li>
        </ul>
        <p>Changes to the policy must be approved by the Board of Directors.</p>
    </div>

    <div class="section">
        <h2>9. Roles & Responsibilities</h2>
        <p>The effective management of outsourcing at Penthian Ltd depends on clearly defined roles and responsibilities to ensure operational integrity, regulatory compliance, and risk control.</p>
        <h3>9.1 Board of Directors</h3>
        <ul>
            <li>Approves this Outsourcing Policy and all material outsourcing arrangements</li>
            <li>Ensures that outsourcing does not impair Penthian’s compliance obligations</li>
            <li>Reviews quarterly outsourcing risk reports and audit results</li>
            <li>Validates exit strategies and recovery readiness for critical providers</li>
        </ul>
        <h3>9.2 Chief Executive Officer (CEO)</h3>
        <ul>
            <li>Provides strategic oversight of the outsourcing framework</li>
            <li>Ensures organizational alignment with outsourcing risk appetite and governance principles</li>
            <li>Approves new outsourcing categories or changes to major vendor relationships</li>
        </ul>
        <h3>9.3 Compliance Officer</h3>
        <ul>
            <li>Conducts regulatory due diligence and reviews service agreements for compliance gaps</li>
            <li>Monitors data protection and AML/CTF compliance of third-party vendors</li>
            <li>Reports outsourcing risks, failures, and breaches to the Board and FSC</li>
            <li>Maintains up-to-date outsourcing risk register</li>
        </ul>
        <h3>9.4 Chief Technology Officer (CTO)</h3>
        <ul>
            <li>Oversees technical due diligence and security audits for outsourced IT and infrastructure services</li>
            <li>Validates cybersecurity controls, encryption standards, and backup procedures</li>
            <li>Manages interface and continuity between Penthian’s tech stack and outsourced systems</li>
        </ul>
        <h3>9.5 Outsourcing Manager (or Delegate)</h3>
        <ul>
            <li>Coordinates due diligence, onboarding, and monitoring of vendors</li>
            <li>Ensures that SLAs, performance reports, and audits are conducted on schedule</li>
            <li>Maintains documentation and vendor scorecards</li>
        </ul>
        <h3>9.6 Legal Advisor or Legal Counsel</h3>
        <ul>
            <li>Reviews outsourcing contracts to ensure regulatory enforceability</li>
            <li>Provides advice on jurisdiction, intellectual property, liability, and dispute clauses</li>
            <li>Participates in escalations and termination negotiations</li>
        </ul>
    </div>

    <div class="section">
        <h2>10. Policy Breaches & Disciplinary Measures</h2>
        <h3>10.1 Identification of Breaches</h3>
        <p>A breach of this Outsourcing Policy occurs when:</p>
        <ul>
            <li>A material outsourcing contract is entered into without due diligence or Board approval</li>
            <li>Regulatory requirements (e.g. FSC rules or VAITOS Act) are not satisfied in outsourcing arrangements</li>
            <li>Critical services are outsourced without exit plans, data safeguards, or SLA enforcement</li>
            <li>Service providers materially fail to meet contractual obligations</li>
            <li>Penthian data is transferred or accessed in a manner inconsistent with policy or law</li>
        </ul>
        <h3>10.2 Reporting of Breaches</h3>
        <p>All actual or suspected breaches must be reported immediately to the Compliance Officer:</p>
        <ul>
            <li>High-risk breaches (e.g. data loss, service outage, or regulatory impact) are escalated to:
                <ul>
                    <li>CEO</li>
                    <li>Board of Directors</li>
                    <li>FSC (if material or required by regulation)</li>
                </ul>
            </li>
        </ul>
        <h3>10.3 Consequences of Breaches</h3>
        <p>Depending on severity, a breach may result in:</p>
        <ul>
            <li><strong>Vendor sanctions:</strong>
                <ul>
                    <li>Suspension or termination of contract</li>
                    <li>Financial penalties or legal proceedings</li>
                    <li>Blacklisting of the provider for future engagement</li>
                </ul>
            </li>
            <li><strong>Internal disciplinary measures:</strong>
                <ul>
                    <li>Formal warnings or retraining for staff</li>
                    <li>Reassignment or restriction of responsibilities</li>
                    <li>Termination of employment (for willful or negligent violations)</li>
                </ul>
            </li>
            <li><strong>Regulatory outcomes:</strong>
                <ul>
                    <li>FSC-imposed sanctions</li>
                    <li>Additional compliance audits or remediation orders</li>
                    <li>Reputational damage and loss of license eligibility</li>
                </ul>
            </li>
        </ul>
        <h3>10.4 Remediation & Review</h3>
        <p>Following a breach:</p>
        <ul>
            <li>A root cause analysis is performed by the Compliance Officer</li>
            <li>Corrective actions are documented and assigned a timeline</li>
            <li>The Outsourcing Policy may be updated to address systemic gaps</li>
            <li>Board oversight is engaged to ensure accountability and transparency</li>
        </ul>
