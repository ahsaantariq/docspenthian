        <h2>7. Wallet & Smart Contract Access Controls</h2>
        <p>Penthian integrates identity verification with blockchain-based access control, ensuring that only verified users can interact with tokenized assets. This section outlines how wallets are authorized, how identity tokens work, and how access is enforced via smart contracts.</p>
        <h3>7.1 Wallet Types Supported</h3>
        <p>Penthian supports two types of user wallets:</p>
        <ul>
            <li>Self-custody wallets (e.g., MetaMask, WalletConnect, Ledger)</li>
            <li>Platform-managed wallets (custodial or semi-custodial, subject to jurisdiction and user choice)</li>
        </ul>
        <p>All wallets must complete KYC/KYB and pass compliance checks before being activated.</p>
        <h3>7.2 ERC-3643 Identity Token Integration</h3>
        <p>To enforce access control on-chain, Penthian issues identity tokens based on the ERC-3643 standard:</p>
        <ul>
            <li>Each verified wallet is assigned an ERC-3643-compliant token</li>
            <li>This token acts as a compliance passport, allowing interaction with whitelisted assets</li>
            <li>Revocation of the identity token disables smart contract access</li>
        </ul>
        <p>These tokens are non-transferable and stored in the user's wallet alongside asset tokens.</p>
        <h3>7.3 Access Control Mechanism</h3>
        <p>Smart contracts deployed by Penthian include logic that:</p>
        <ul>
            <li>Checks for a valid ERC-3643 identity token before allowing purchase, transfer, or listing of any asset</li>
            <li>Blocks access from non-compliant or expired wallets</li>
            <li>Tracks interactions to detect unusual or risky behavior</li>
        </ul>
        <h3>7.4 Multi-Wallet Support & Identity Mapping</h3>
        <p>Clients can link multiple wallets to a single verified identity if approved by compliance. Each wallet:</p>
        <ul>
            <li>Is verified individually</li>
            <li>Has its own identity token</li>
            <li>Can be suspended independently if risk is detected</li>
        </ul>
        <p>Mapping is stored off-chain with encrypted references for internal tracking.</p>
        <h3>7.5 Lost Access or Wallet Recovery</h3>
        <p>In the event of a lost wallet:</p>
        <ul>
            <li>Clients must undergo re-verification</li>
            <li>A new wallet can be linked and assigned an updated identity token</li>
            <li>Tokenized assets may be reissued or migrated under legal supervision and internal policy (requires documented proof of loss and board-level approval for high-value assets)</li>
        </ul>
        <h3>7.6 Suspicious Wallet Activity</h3>
        <p>Wallets may be temporarily frozen or blacklisted if:</p>
        <ul>
            <li>Transactions breach thresholds without justification</li>
            <li>Wallet interacts with flagged or darkweb-associated addresses</li>
            <li>Smart contract usage shows signs of automation, front-running, or bot behavior</li>
            <li>Access is attempted from geo-blocked IPs or Tor networks</li>
        </ul>
        <h3>7.7 Jurisdiction-Based Restrictions</h3>
        <p>Smart contracts and frontend APIs include geo-IP and on-chain restriction layers. Users from the following may be blocked or limited:</p>
        <ul>
            <li>OFAC-sanctioned regions</li>
            <li>FATF high-risk jurisdictions</li>
            <li>Countries where tokenized asset trading is illegal or restricted</li>
        </ul>
