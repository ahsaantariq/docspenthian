        <h2>2. Onboarding Workflow Overview</h2>
        <p>Penthian’s onboarding process is a multi-step, risk-based workflow that ensures all clients are properly identified, verified, and approved before accessing the platform. This workflow is designed for both individuals (retail) and entities (institutional) and is powered by integrated systems and human review.</p>
        <h3>2.1 Core Workflow Steps</h3>
        <p>The onboarding process involves the following key steps:</p>
        <p><strong>Step 1: Account Creation</strong></p>
        <ul>
            <li>User provides email and password or connects via social login (where applicable).</li>
            <li>A verification link or code is sent to confirm contact information.</li>
        </ul>
        <p><strong>Step 2: KYC/KYB Information Collection</strong></p>
        <ul>
            <li>Powered by Veriff integration.</li>
            <li><strong>Retail Clients provide:</strong>
                <ul>
                    <li>Full legal name</li>
                    <li>Government-issued ID</li>
                    <li>Selfie and liveness check</li>
                    <li>Proof of address (e.g. utility bill, bank statement)</li>
                </ul>
            </li>
            <li><strong>Institutional Clients provide:</strong>
                <ul>
                    <li>Company incorporation documents</li>
                    <li>UBO identification and verification</li>
                    <li>Board resolution or proof of authority</li>
                    <li>Business address and tax number</li>
                </ul>
            </li>
        </ul>
        <p><strong>Step 3: Screening & Risk Scoring</strong></p>
        <ul>
            <li>Automated checks against global watchlists, sanctions databases (UN, OFAC, EU, etc.).</li>
            <li>PEP (Politically Exposed Person) screening.</li>
            <li>AML risk profiling based on jurisdiction, source of funds, and user activity expectations.</li>
        </ul>
        <p><strong>Step 4: Approval or Escalation</strong></p>
        <ul>
            <li>Low- to medium-risk clients are automatically approved upon successful checks.</li>
            <li>High-risk or flagged clients are escalated to the Compliance team for manual review.</li>
            <li>Enhanced Due Diligence (EDD) is applied where required.</li>
        </ul>
        <p><strong>Step 5: Wallet Setup & Token Access</strong></p>
        <ul>
            <li>Upon approval, the user receives access to their wallet (non-custodial or platform-based).</li>
            <li>A unique ERC-3643-compliant identity token is assigned to the wallet.</li>
            <li>User can now buy, trade, and hold tokenized assets within legal limits.</li>
        </ul>
        <h3>2.2 Smart Contract Onboarding Integration</h3>
        <p>Penthian’s onboarding process includes smart contract-based enforcement of access control:</p>
        <ul>
            <li>ERC-3643 identity tokens verify KYC status on-chain.</li>
            <li>Only wallets with valid identity tokens can interact with tokenized assets.</li>
            <li>Revocation of identity tokens disables platform access if KYC/KYB expires or is invalidated.</li>
        </ul>
        <h3>2.3 Multi-Jurisdictional Flow</h3>
        <p>Depending on the client’s jurisdiction, additional steps may include:</p>
        <ul>
            <li>Geo-IP blocks for restricted or high-risk jurisdictions</li>
            <li>Request for tax residency certification (e.g., W-8BEN, W-9)</li>
            <li>Localized consent forms or disclosures where required by law</li>
        </ul>
