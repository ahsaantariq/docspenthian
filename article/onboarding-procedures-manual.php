        <h2>3. Client Risk Profiling</h2>
        <p>Risk-based client profiling enables Penthian to apply appropriate levels of due diligence, monitoring, and controls to each client. This aligns with AML/CFT obligations and helps protect the platform from exposure to money laundering, fraud, and reputational risk.</p>
        <h3>3.1 Risk Categories</h3>
        <p>Penthian classifies clients into three main risk levels:</p>
        <table>
            <tr>
                <th>Risk Level</th>
                <th>Description</th>
            </tr>
            <tr>
                <td>Low Risk</td>
                <td>Clients from low-risk jurisdictions, with standard ID, low volume activity, no red flags</td>
            </tr>
            <tr>
                <td>Medium Risk</td>
                <td>Clients with moderate volume, high-risk industry exposure, or partial documentation mismatches</td>
            </tr>
            <tr>
                <td>High Risk</td>
                <td>PEPs, clients from high-risk jurisdictions, large volume investors, or entities with unclear ownership structure</td>
            </tr>
        </table>
        <h3>3.2 Risk Factors Considered</h3>
        <p>Risk profiles are based on a combination of static and dynamic factors:</p>
        <p><strong>Static Factors</strong></p>
        <ul>
            <li>Jurisdiction of residence or incorporation</li>
            <li>Type of client (retail vs. institutional)</li>
            <li>Nature of business (for entities)</li>
            <li>Source of funds (declared and verified)</li>
        </ul>
        <p><strong>Dynamic Factors</strong></p>
        <ul>
            <li>Trading volume and frequency</li>
            <li>Token types purchased or listed</li>
            <li>Interaction with wallets or addresses flagged by Chainalysis or equivalent</li>
            <li>Past incidents or internal flags</li>
        </ul>
        <h3>3.3 Risk Scoring Methodology</h3>
        <p>Each factor is assigned a numeric score (1–5). The aggregate score places the client into one of the three risk categories. Profiles are updated dynamically if:</p>
        <ul>
            <li>Activity significantly changes (e.g., sudden increase in volume)</li>
            <li>New intelligence or sanctions list entries emerge</li>
            <li>A client’s behavior violates platform policies</li>
        </ul>
        <h3>3.4 Enhanced Due Diligence (EDD)</h3>
        <p>High-risk clients undergo additional checks, including:</p>
        <ul>
            <li>Video verification</li>
            <li>Proof of source of wealth</li>
            <li>Ongoing monitoring for adverse media</li>
            <li>Manual review of onboarding documents</li>
            <li>Board-level signoff for institutional clients</li>
        </ul>
        <p>EDD is documented in the compliance portal and reviewed quarterly.</p>
        <h3>3.5 Periodic Review of Profiles</h3>
        <p>All client profiles are reviewed as follows:</p>
        <table>
            <tr>
                <th>Risk Level</th>
                <th>Review Frequency</th>
            </tr>
            <tr>
                <td>Low</td>
                <td>Every 12 months</td>
            </tr>
            <tr>
                <td>Medium</td>
                <td>Every 6 months</td>
            </tr>
            <tr>
                <td>High</td>
                <td>Every 3 months or as triggered by alerts</td>
            </tr>
        </table>
        <p>Triggers may include large single transactions, rapid KYC reattempts, or jurisdiction changes.</p>
        <h3>3.6 Risk Mitigation Actions</h3>
        <p>Based on profile, the following measures may apply:</p>
        <ul>
            <li>Transaction limits or withdrawal cooldowns</li>
            <li>Smart contract restrictions on access to specific tokens</li>
            <li>Holding periods for secondary sales</li>
            <li>Manual approval of off-platform withdrawals</li>
            <li>Suspension or termination of account (subject to review)</li>
        </ul>
