                <th>Onboarding Type</th>
                <th>Expected Completion Time</th>
            </tr>
            <tr>
                <td>Standard Retail Client</td>
                <td>&lt; 10 minutes</td>
            </tr>
            <tr>
                <td>Medium-Risk Retail</td>
