        <h2>4. Client Acceptance & Rejection Criteria</h2>
        <p>To maintain platform integrity and ensure compliance with regulatory obligations, Penthian applies clearly defined criteria for accepting or rejecting clients during the onboarding process. These criteria are based on legal, ethical, and risk-based considerations.</p>
        <h3>4.1 Client Acceptance Criteria</h3>
        <p>A client may be accepted if they meet all of the following conditions:</p>
        <ul>
            <li>Successful KYC/KYB verification via Veriff (or other approved provider)</li>
            <li>No match found on global sanctions lists (e.g., OFAC, UN, EU, FATF)</li>
            <li>Clear source of funds or wealth if requested</li>
            <li>Valid jurisdiction not listed as a restricted country</li>
            <li>Risk profile is within Penthian’s tolerance level (Low to Medium risk)</li>
            <li>No adverse media or reputation risk identified</li>
            <li>Acknowledgment of platform Terms of Use, AML Policy, and Risk Disclosures</li>
        </ul>
        <p>Institutional clients must additionally provide:</p>
        <ul>
            <li>Certificate of incorporation</li>
            <li>Proof of address and tax registration</li>
            <li>UBO details with ID and verification</li>
            <li>Resolution authorizing account creation</li>
            <li>Valid business use-case for engaging in tokenized assets</li>
        </ul>
        <h3>4.2 Client Rejection Criteria</h3>
        <p>A client must be rejected or blocked if any of the following are true:</p>
        <ul>
            <li>KYC/KYB verification failed, or documents are clearly forged or expired</li>
            <li>Hit found on any sanctions, terrorism, or law enforcement watchlist</li>
            <li>False information submitted during onboarding</li>
            <li>Jurisdiction is classified as high-risk or restricted by the FSC or Penthian’s internal country list</li>
            <li>Involvement in suspicious industries, including:
                <ul>
                    <li>Darknet marketplaces</li>
                    <li>Unlicensed gambling or betting</li>
                    <li>Human trafficking, weapons trade, or illicit finance</li>
                </ul>
            </li>
            <li>Multiple identity attempts or identity manipulation detected</li>
            <li>PEP status without full disclosure and EDD completion</li>
            <li>Non-cooperation with requests for additional information</li>
            <li>Failure to consent to platform legal documents and AML/CFT policy</li>
        </ul>
        <h3>4.3 Temporary Suspension or Escalation</h3>
        <p>Where the client’s status is uncertain, Penthian may temporarily suspend onboarding and request further clarification. Examples include:</p>
        <ul>
            <li>Conflicting ID documents</li>
            <li>Unverified corporate structure</li>
            <li>Large value transfers during onboarding</li>
            <li>Incomplete response to risk questionnaire</li>
        </ul>
        <p>Such cases are escalated to the Compliance Officer and logged for audit purposes.</p>
        <h3>4.4 Appeals Process</h3>
        <p>Clients may submit an appeal for review via the support team if rejected. Appeals will:</p>
        <ul>
            <li>Be reviewed by the Compliance and Legal teams</li>
            <li>Require submission of new or clarified documents</li>
            <li>Be resolved within 7 business days</li>
        </ul>
        <p>Final decisions are documented and shared with the FSC upon request.</p>
