    <div class="section">
        <h2>1. Purpose & Scope</h2>
        <p>The purpose of this Onboarding Procedures Manual is to define the standardized process by which Penthian Ltd onboards new clients — both individuals and entities — onto its regulated virtual asset platform in Mauritius. These procedures ensure full compliance with:</p>
        <ul>
            <li>The Financial Services Commission (FSC) of Mauritius regulatory requirements</li>
            <li>The Financial Intelligence and Anti-Money Laundering Act (FIAMLA)</li>
            <li>The Virtual Asset and Initial Token Offering Services Act 2021</li>
            <li>Internal AML/CFT Policy and Risk Management Framework</li>
            <li>International best practices in customer due diligence, investor protection, and fraud prevention</li>
        </ul>
        <h3>1.1 Objective</h3>
        <p>This manual aims to:</p>
        <ul>
            <li>Provide a clear and consistent client onboarding framework</li>
            <li>Establish controls to verify identity, risk profile, and source of funds</li>
            <li>Prevent access to the Penthian platform by sanctioned, fraudulent, or high-risk entities</li>
            <li>Enable seamless integration with KYC providers and smart contract token standards (e.g., ERC-3643)</li>
            <li>Protect the integrity of Penthian’s tokenization, trading, and investment activities</li>
            <li>Promote ethical standards and transparency in accordance with Penthian’s core values</li>
        </ul>
        <h3>1.2 Scope of Application</h3>
        <p>This manual applies to the onboarding of:</p>
        <ul>
            <li><strong>Retail Investors:</strong> Individuals seeking to buy, sell, or hold tokenized assets on Penthian</li>
            <li><strong>Institutional Clients:</strong> Legal entities such as funds, property developers, corporate asset owners, or licensed brokers</li>
            <li><strong>Platform Agents:</strong> Individuals or companies acting as introducers or asset listers on behalf of third parties</li>
            <li><strong>Third-Party Service Providers:</strong> Including KYC vendors, custodians, and technology integrators with privileged access</li>
            <li><strong>Penthian Staff (in admin role onboarding flows):</strong> As part of internal user access and operational control</li>
        </ul>
        <h3>1.3 Integration with Other Policies</h3>
        <p>The onboarding process works in tandem with:</p>
        <ul>
            <li>AML/CFT Policy (for KYC/KYB and screening procedures)</li>
            <li>Internal Controls Manual (for staff responsibilities and segregation of duties)</li>
            <li>Risk Management Framework (for profiling and escalation of high-risk users)</li>
            <li>Marketplace Rules (for user conduct and asset listing eligibility)</li>
            <li>IT & Cybersecurity Policy (for data protection and secure onboarding flows)</li>
        </ul>
