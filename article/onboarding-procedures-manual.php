        <h2>6. Source of Funds & Wealth Verification</h2>
        <p>To ensure the legitimacy of all capital entering the Penthian platform, the source of funds and, where required, the source of wealth must be verified for all clients whose risk profile, transaction volume, or jurisdiction triggers Enhanced Due Diligence (EDD). This practice supports our AML/CFT obligations and helps mitigate the risk of money laundering, fraud, or reputational damage.</p>
        <h3>6.1 Key Definitions</h3>
        <ul>
            <li><strong>Source of Funds (SoF):</strong> The origin of the specific money used for an individual transaction or investment on the platform.</li>
            <li><strong>Source of Wealth (SoW):</strong> The origin of a client’s overall wealth, assets, or accumulated funds used for larger or recurring investments.</li>
        </ul>
        <h3>6.2 When Verification Is Required</h3>
        <p>Penthian requires source of funds and/or wealth documentation in the following scenarios:</p>
        <table>
            <tr>
                <th>Trigger</th>
                <th>Required</th>
            </tr>
            <tr>
                <td>Transaction exceeds USD 15,000 equivalent</td>
                <td>Source of Funds</td>
            </tr>
            <tr>
                <td>Client is categorized as High Risk</td>
                <td>Both SoF and SoW</td>
            </tr>
            <tr>
                <td>Client is a PEP or from a high-risk jurisdiction</td>
                <td>Both SoF and SoW</td>
            </tr>
            <tr>
                <td>Institutional clients listing assets</td>
                <td>Source of Wealth and Business Justification</td>
            </tr>
            <tr>
                <td>Multiple high-volume transactions in short timeframe</td>
                <td>Source of Funds</td>
            </tr>
            <tr>
                <td>Investor flagged in internal or partner monitoring</td>
                <td>Both SoF and SoW</td>
            </tr>
        </table>
        <h3>6.3 Acceptable Source of Funds Documents</h3>
        <ul>
            <li>Recent payslips (last 3 months)</li>
            <li>Bank statements showing salary or savings accumulation</li>
            <li>Sale of property or assets (contract + transaction proof)</li>
            <li>Inheritance documents with legal backing</li>
            <li>Crypto transaction history with exchange withdrawal records</li>
            <li>Company dividends or director loan documentation</li>
        </ul>
        <h3>6.4 Acceptable Source of Wealth Documents</h3>
        <ul>
            <li>Certified financial statements or tax returns</li>
            <li>Sale agreement for a business or major real estate asset</li>
            <li>Historical investment portfolio statements</li>
            <li>Legal inheritance letters</li>
            <li>Audit-confirmed proof of startup proceeds</li>
            <li>Trust deeds or corporate shareholding records</li>
        </ul>
        <h3>6.5 Verification Procedure</h3>
        <ol>
            <li>Client uploads documents via secure Veriff or Penthian portal.</li>
            <li>Compliance team or Veriff analysts review for consistency and authenticity.</li>
            <li>If flagged:
                <ul>
                    <li>Additional documents requested</li>
                    <li>Escalation to senior Compliance Officer</li>
                </ul>
            </li>
            <li>Verified documentation is logged and added to client profile.</li>
        </ol>
        <h3>6.6 False or Unclear Submissions</h3>
        <p>If a client provides:</p>
        <ul>
            <li>Fabricated documents</li>
            <li>Vague descriptions or unverifiable origin</li>
            <li>Refuses to comply or delays submission</li>
        </ul>
        <p>Then onboarding is paused or terminated. Cases may be escalated to the Financial Intelligence Unit (FIU) in Mauritius if suspicion arises.</p>
        <h3>6.7 Retention & Privacy</h3>
        <p>All submitted documents:</p>
        <ul>
            <li>Are encrypted and stored for 7 years, in line with AML regulations</li>
            <li>Are never shared unless required by law or with written client consent</li>
            <li>Are only accessible to authorized Compliance Officers and FSC examiners</li>
        </ul>
