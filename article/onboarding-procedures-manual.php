        <h2>5. Identity Verification (KYC/KYB) Procedures</h2>
        <p>Identity verification is central to Penthian’s onboarding process. All clients — individuals and entities — must undergo robust Know Your Customer (KYC) or Know Your Business (KYB) checks before being approved to access the platform or transact in virtual assets. These procedures are performed in partnership with Veriff, our KYC provider, and aligned with FSC regulatory obligations and global AML/CFT standards.</p>
        <h3>5.1 KYC for Individual Clients (Retail Investors)</h3>
        <p>Penthian requires the following information and documentation from individual users:</p>
        <p><strong>Required Information</strong></p>
        <ul>
            <li>Full legal name</li>
            <li>Date of birth</li>
            <li>Residential address</li>
            <li>Nationality</li>
            <li>Tax residency</li>
            <li>Source of funds declaration</li>
            <li>Email and mobile contact</li>
        </ul>
        <p><strong>Required Documents</strong></p>
        <ul>
            <li>Government-issued photo ID (passport, driver’s license, or national ID)</li>
            <li>Recent proof of address (utility bill, bank statement, etc.)</li>
            <li>Live selfie or video liveness check</li>
        </ul>
        <p><strong>Verification Steps</strong></p>
        <ol>
            <li>Document and facial recognition scan by Veriff</li>
            <li>Cross-check against sanctions and PEP databases</li>
            <li>Verification of address against databases or document metadata</li>
            <li>Risk score generation</li>
        </ol>
        <h3>5.2 KYB for Institutional Clients</h3>
        <p>KYB procedures apply to corporate clients such as property developers, investment funds, and listing agents.</p>
        <p><strong>Required Information</strong></p>
        <ul>
            <li>Legal name of the entity</li>
            <li>Date of incorporation and registration number</li>
            <li>Country of incorporation and tax ID</li>
            <li>Registered business address</li>
            <li>Nature of business and purpose of account</li>
            <li>Details of directors and Ultimate Beneficial Owners (UBOs)</li>
        </ul>
        <p><strong>Required Documents</strong></p>
        <ul>
            <li>Certificate of incorporation</li>
            <li>Company extract (e.g., from official registry)</li>
            <li>Corporate resolution authorizing platform use</li>
            <li>Proof of address</li>
            <li>ID and KYC documents for all UBOs (holding 25%+ ownership or control)</li>
            <li>Proof of source of funds or revenue stream</li>
        </ul>
        <p><strong>Verification Steps</strong></p>
        <ol>
            <li>Entity data validated against business registries</li>
            <li>UBO documents verified through Veriff or internal review</li>
            <li>Sanctions and PEP screening applied to all related parties</li>
            <li>Additional due diligence for high-risk industries or jurisdictions</li>
        </ol>
        <h3>5.3 Re-verification & Expiry Management</h3>
        <ul>
            <li>KYC/KYB documents are monitored for expiry (e.g., passport expiry dates)</li>
            <li>Re-verification is triggered annually for Low Risk, semi-annually for Medium Risk, and quarterly for High Risk clients</li>
            <li>Users receive automatic alerts when updates are required</li>
        </ul>
        <h3>5.4 Document Storage & Access</h3>
        <p>All KYC/KYB documents are:</p>
        <ul>
            <li>Encrypted and stored securely in compliance with GDPR and Mauritius Data Protection Act</li>
            <li>Access-limited to approved Compliance Officers</li>
            <li>Backed up to redundant encrypted cloud servers</li>
            <li>Never shared with third parties except where legally required</li>
        </ul>
