      <h2>1. Introduction</h2>
    <p>The Governance Framework outlines the formal structure through which Penthian Ltd ensures accountability, compliance, ethical leadership, and effective oversight in accordance with:</p>
    <ul>
        <li>The Financial Services Act 2007 (Mauritius)</li>
        <li>The Virtual Asset and Initial Token Offering Services Act 2021 (VAITOS Act)</li>
        <li>Guidelines from the Financial Services Commission (FSC)</li>
        <li>International governance standards applicable to regulated fintech and blockchain firms</li>
    </ul>
    <p>As a regulated Virtual Asset Service Provider (VASP), Penthian maintains strong internal governance to safeguard investors, uphold market integrity, and ensure long-term sustainability.</p>

    <h2>2. Governance Objectives & Principles</h2>
    <p>The governance framework of Penthian Ltd is rooted in integrity, transparency, compliance, and innovation. As a next-generation platform for tokenized ownership, we recognize the need for strong, values-based leadership combined with agile, risk-aware decision-making.</p>

    <h3>2.1 Objectives of Governance</h3>
    <ol>
        <li><strong>Regulatory Compliance:</strong> To maintain full compliance with the FSC, VAITOS Act, and other relevant laws and standards governing VASPs.</li>
        <li><strong>Strategic Oversight:</strong> To guide the company’s mission, vision, and strategic direction in alignment with investor protection and sustainable innovation.</li>
        <li><strong>Risk Management:</strong> To identify, monitor, and mitigate financial, operational, cybersecurity, and reputational risks across the organization.</li>
        <li><strong>Stakeholder Trust:</strong> To build trust with investors, users, partners, regulators, and employees through fair, accountable practices and open communication.</li>
        <li><strong>Operational Excellence:</strong> To ensure efficient decision-making, internal control systems, and continual improvement of business operations.</li>
        <li><strong>Ethical Stewardship:</strong> To promote a culture anchored in the Fruits of the Spirit (love, joy, peace, patience, kindness, goodness, faithfulness, gentleness, self-control), guiding how we serve clients and operate as a corporate citizen.</li>
    </ol>

    <h3>2.2 Governance Principles</h3>
    <table>
        <tr>
            <th>Principle</th>
            <th>Application at Penthian Ltd</th>
        </tr>
        <tr>
            <td>Accountability</td>
            <td>Clear delineation of authority and responsibility across directors, management, and functional teams.</td>
        </tr>
        <tr>
            <td>Transparency</td>
            <td>Transparent reporting to shareholders, regulators, and stakeholders.</td>
        </tr>
        <tr>
            <td>Integrity</td>
            <td>Uncompromised adherence to ethical standards in operations, decisions, and communications.</td>
        </tr>
        <tr>
            <td>Independence</td>
            <td>Independent oversight via non-executive directors and external advisors where appropriate.</td>
        </tr>
        <tr>
            <td>Competence</td>
            <td>Appointing qualified individuals to leadership and control functions, including AML/CFT and compliance.</td>
        </tr>
        <tr>
            <td>Agility</td>
            <td>Fast and informed decision-making that allows innovation without compromising stability or trust.</td>
        </tr>
    </table>

    <h2>3. Board Composition & Structure</h2>
    <p>The Board of Directors at Penthian Ltd holds ultimate responsibility for the company’s governance, performance, and compliance. The Board is structured to ensure diverse experience, strategic guidance, and effective oversight of management.</p>

    <h3>3.1 Board Structure</h3>
    <ul>
        <li>Minimum of 3 Directors, including:</li>
        <li>At least one Executive Director (e.g., CEO or Managing Director)</li>
        <li>At least one Independent Non-Executive Director</li>
        <li>Compliance or Risk Oversight Representation (e.g., Board member with AML/CFT expertise)</li>
        <li>Chairperson must be a Non-Executive Director, ensuring separation of governance and management functions.</li>
    </ul>

    <h3>3.2 Roles & Responsibilities</h3>
    <table>
        <tr>
            <th>Role</th>
            <th>Key Responsibilities</th>
        </tr>
        <tr>
            <td>Chairperson</td>
            <td>Lead board meetings, ensure board effectiveness, support corporate governance practices.</td>
        </tr>
        <tr>
            <td>Executive Directors</td>
            <td>Oversee day-to-day operations, implement strategy, liaise with stakeholders.</td>
        </tr>
        <tr>
            <td>Non-Executive Directors</td>
            <td>Provide independent oversight, advise on strategy, risk, and regulatory alignment.</td>
        </tr>
    </table>

    <h3>3.3 Director Qualifications</h3>
    <p>Each board member must:</p>
    <ul>
        <li>Be fit and proper per FSC criteria</li>
        <li>Have relevant experience in blockchain, finance, legal, real estate, or technology</li>
        <li>Have no disqualifying criminal or regulatory history</li>
        <li>Comply with ongoing professional conduct obligations</li>
    </ul>

    <h3>3.4 Board Meetings</h3>
    <ul>
        <li>Held quarterly, with ad-hoc meetings convened when needed</li>
        <li>Decisions recorded in formal minutes and retained per internal policy</li>
        <li>Quorum: Minimum 2 directors, including at least one non-executive</li>
    </ul>

    <h3>3.5 Conflicts of Interest</h3>
    <ul>
        <li>All directors must declare actual or potential conflicts of interest in writing</li>
        <li>Conflicted directors must recuse themselves from related decisions</li>
        <li>A Conflict Register is maintained and reviewed annually</li>
    </ul>

    <h2>4. Executive Management & Key Persons</h2>
    <p>Penthian Ltd has appointed a competent executive leadership team and key persons to ensure regulatory compliance, strategic execution, and operational integrity. All appointments are subject to FSC’s fit and proper assessment standards.</p>

    <h3>4.1 Executive Management Team</h3>
    <table>
        <tr>
            <th>Position</th>
            <th>Responsibilities</th>
        </tr>
        <tr>
            <td>Chief Executive Officer (CEO)</td>
            <td>Leads overall strategy, represents the company, ensures stakeholder alignment.</td>
        </tr>
        <tr>
            <td>Chief Operating Officer (COO)</td>
            <td>Oversees business operations, team performance, and platform scalability.</td>
        </tr>
        <tr>
            <td>Chief Technology Officer (CTO)</td>
            <td>Leads platform development, cybersecurity, and blockchain integrations (ERC-3643, ERC-1155).</td>
        </tr>
        <tr>
            <td>Chief Financial Officer (CFO)</td>
            <td>Manages financial reporting, investor relations, and treasury.</td>
        </tr>
    </table>

    <h3>4.2 Key Persons & Control Functions</h3>
    <table>
        <tr>
            <th>Function</th>
            <th>Designation</th>
            <th>Key Duties</th>
        </tr>
        <tr>
            <td>Compliance Officer</td>
            <td>(Named in FSC application)</td>
            <td>Monitors adherence to legal and regulatory frameworks (FSC, AML/CFT, VAITOS).</td>
        </tr>
        <tr>
            <td>MLRO (Money Laundering Reporting Officer)</td>
            <td>(Named in FSC application)</td>
            <td>Reports suspicious activity, manages AML/CFT program, liaises with FIU.</td>
        </tr>
        <tr>
            <td>Data Protection Officer (DPO)</td>
            <td>(Appointed)</td>
            <td>Oversees data privacy, user rights, and breach notifications.</td>
        </tr>
        <tr>
            <td>Risk Officer</td>
            <td>(May be distinct or part of COO role)</td>
            <td>Manages enterprise risk framework, reporting to Board and regulators.</td>
        </tr>
    </table>

    <h3>4.3 Fit & Proper Criteria</h3>
    <p>All executives and key persons must:</p>
    <ul>
        <li>Demonstrate high standards of competence and integrity</li>
        <li>Have no disqualifying regulatory or legal background</li>
        <li>Be approved by the FSC (if applicable) before assuming role</li>
        <li>Fulfill ongoing training and role-specific compliance obligations</li>
    </ul>

    <h3>4.4 Delegation & Oversight</h3>
    <ul>
        <li>Where responsibilities are delegated (e.g. outsourced IT, audit), management remains accountable</li>
        <li>Delegation agreements must include service standards, audit rights, and compliance covenants</li>
        <li>Regular reporting to the Board is mandatory for all control functions</li>
    </ul>

    <h2>5. Committees & Substructures</h2>
    <p>To support its strategic oversight and control functions, Penthian Ltd has established internal committees with defined roles, scopes, and responsibilities. These substructures enhance accountability, compliance, and operational resilience.</p>

    <h3>5.1 Governance Committees</h3>
    <table>
        <tr>
            <th>Committee</th>
            <th>Mandate</th>
        </tr>
        <tr>
            <td>Audit & Risk Committee</td>
            <td>Oversees financial integrity, internal controls, and enterprise risk framework.</td>
        </tr>
        <tr>
            <td>Compliance & AML Committee</td>
            <td>Monitors compliance with FSC, AML/CFT regulations, VAITOS, and internal policies.</td>
        </tr>
        <tr>
            <td>Technology & Security Committee</td>
            <td>Reviews system architecture, blockchain upgrades, cybersecurity, and IT risks.</td>
        </tr>
        <tr>
            <td>Tokenization & Asset Listings Committee</td>
            <td>Evaluates real-world asset listings, token structures, and due diligence.</td>
        </tr>
        <tr>
            <td>Remuneration & Ethics Committee</td>
            <td>Advises on executive remuneration, performance incentives, and ethical standards.</td>
        </tr>
    </table>

    <h3>5.2 Composition</h3>
    <ul>
        <li>Committees include a mix of board members, executives, and relevant domain experts</li>
        <li>At least one non-executive director or external advisor sits on each key committee</li>
        <li>Members are formally appointed by the Board and subject to annual review</li>
    </ul>

    <h3>5.3 Meeting Frequency</h3>
    <ul>
        <li>Committees meet quarterly or more frequently if required</li>
        <li>Formal agendas and minutes are maintained</li>
        <li>Reports and recommendations are escalated to the Board for action or ratification</li>
    </ul>

    <h3>5.4 Oversight of Substructures</h3>
    <ul>
        <li>Committees may form working groups or task forces for special projects (e.g. new token standards, high-risk asset classes)</li>
        <li>All substructures operate under terms of reference (ToR) approved by the Board</li>
        <li>Oversight includes performance evaluation and continuous improvement reviews</li>
    </ul>

    <h2>6. Risk Management & Internal Controls</h2>
    <p>Penthian Ltd has implemented a comprehensive enterprise risk management (ERM) framework designed to identify, assess, monitor, and mitigate risks across its operations, technology infrastructure, tokenized marketplaces, and compliance environment.</p>

    <h3>6.1 Risk Categories Monitored</h3>
    <table>
        <tr>
            <th>Risk Category</th>
            <th>Description</th>
        </tr>
        <tr>
            <td>Regulatory Risk</td>
            <td>Risk of non-compliance with FSC, AML/CFT, VAITOS, MiCA, or other obligations</td>
        </tr>
        <tr>
            <td>Operational Risk</td>
            <td>Failures in processes, human errors, or disruptions to business continuity</td>
        </tr>
        <tr>
            <td>Technology Risk</td>
            <td>System downtime, blockchain vulnerabilities, smart contract failures</td>
        </tr>
        <tr>
            <td>Cybersecurity Risk</td>
            <td>Threats from hacks, phishing, data breaches, and loss of customer trust</td>
        </tr>
        <tr>
            <td>Liquidity Risk</td>
            <td>Risk of insufficient capital for token redemptions or asset payouts</td>
        </tr>
        <tr>
            <td>Reputational Risk</td>
            <td>Damage to public image due to incidents, fraud, or governance lapses</td>
        </tr>
        <tr>
            <td>Market Risk</td>
            <td>Exposure to asset volatility, token mispricing, or investor sentiment shifts</td>
        </tr>
    </table>

    <h3>6.2 Internal Control Systems</h3>
    <ul>
        <li>Segregation of Duties in finance, compliance, and IT</li>
        <li>Whistleblower Channel for reporting internal misconduct anonymously</li>
        <li>Audit Trails & Logging on platform and financial transactions</li>
        <li>Periodic Penetration Testing of smart contracts and marketplace systems</li>
        <li>Mandatory KYC/AML Checks for all users via Veriff integration</li>
        <li>Token Contract Verification prior to deployment (ERC-3643, ERC-1155 standards)</li>
    </ul>

    <h3>6.3 Risk Register & Dashboard</h3>
    <ul>
        <li>A dynamic Risk Register is maintained and reviewed monthly by the Audit & Risk Committee</li>
        <li>Includes probability ratings, impact severity, mitigation plans, and owners</li>
        <li>The CTO and COO maintain a real-time Risk Dashboard tied to platform metrics</li>
    </ul>

    <h3>6.4 Business Continuity & Disaster Recovery</h3>
    <ul>
        <li>Critical systems are cloud-hosted with multi-region failover and backups</li>
        <li>A formal Disaster Recovery Plan (DRP) is in place and tested twice per year</li>
        <li>The COO oversees contingency planning, with Board approval on DRP updates</li>
    </ul>

    <h2>7. Compliance & AML Governance</h2>
    <p>Penthian Ltd is committed to maintaining a culture of compliance and upholding international best practices in Anti-Money Laundering (AML), Counter-Terrorist Financing (CFT), and compliance governance as required under the VAITOS Act, FSC regulations, and FATF standards.</p>

    <h3>7.1 Compliance Function</h3>
    <ul>
        <li>Led by a designated Compliance Officer approved by the FSC</li>
        <li>Reports directly to the Board of Directors</li>
        <li>Oversees the implementation of all regulatory obligations, including:</li>
        <li>AML/CFT program</li>
        <li>KYC/KYB onboarding via Veriff</li>
        <li>Ongoing monitoring and transaction reviews</li>
        <li>Internal control reviews and breach reporting</li>
    </ul>

    <h3>7.2 AML/CFT Framework</h3>
    <table>
        <tr>
            <th>Element</th>
            <th>Details</th>
        </tr>
        <tr>
            <td>Risk-Based Approach (RBA)</td>
            <td>Customers and assets categorized into risk tiers with enhanced due diligence for high-risk classes</td>
        </tr>
        <tr>
            <td>Customer Due Diligence (CDD)</td>
            <td>KYC/KYB checks conducted using Veriff including biometric, document, sanctions, and PEP screening</td>
        </tr>
        <tr>
            <td>Suspicious Transaction Reporting</td>
            <td>Filed by the MLRO to the Financial Intelligence Unit (FIU) under regulatory deadlines</td>
        </tr>
        <tr>
            <td>Training & Awareness</td>
            <td>Annual AML/CFT training for all employees and agents, with tracking and certification</td>
        </tr>
        <tr>
            <td>Audit & Monitoring</td>
            <td>AML program independently reviewed at least annually; internal testing conducted quarterly</td>
        </tr>
    </table>

    <h3>7.3 Ongoing Monitoring</h3>
    <ul>
        <li>Real-time transaction monitoring for red flags and behavioral anomalies</li>
        <li>Blockchain analysis tools (e.g., Chainalysis or equivalent) may be used in the future</li>
        <li>Token ownership movements, large transfers, and withdrawal patterns are analyzed and risk scored</li>
    </ul>

    <h3>7.4 Sanctions Compliance</h3>
    <ul>
        <li>Sanctions screening is embedded into onboarding and ongoing monitoring</li>
        <li>Penthian complies with:</li>
        <li>United Nations Sanctions</li>
        <li>EU Sanctions</li>
        <li>OFAC (US) List</li>
        <li>Mauritius regulatory obligations</li>
    </ul>

    <h3>7.5 Reporting Lines</h3>
    <ul>
        <li>The MLRO has independent authority to escalate matters to:</li>
        <li>The Board</li>
        <li>The FSC</li>
        <li>The FIU</li>
        <li>Breaches or suspected non-compliance are escalated within 24 hours</li>
    </ul>

    <h2>8. Ethics, Conduct & Culture</h2>
    <p>At Penthian Ltd, our culture is rooted in ethical leadership, transparent practices, and faith-based values. We believe our strength lies not only in technology but also in integrity, moral clarity, and a service-driven mindset.</p>

    <h3>8.1 Foundational Values</h3>
    <p>Penthian’s internal culture is built on the Fruits of the Spirit as guiding principles:</p>
    <ul>
        <li><strong>Love:</strong> Genuine care for our team, clients, and partners</li>
        <li><strong>Joy:</strong> Positive energy and purpose in every engagement</li>
        <li><strong>Peace:</strong> Resolving disputes with wisdom and fairness</li>
        <li><strong>Patience:</strong> Long-term thinking in both vision and process</li>
        <li><strong>Kindness:</strong> How we treat clients, even in conflict</li>
        <li><strong>Goodness:</strong> Choosing what is right over what is easy</li>
        <li><strong>Faithfulness:</strong> Dependability in action, not just words</li>
        <li><strong>Gentleness:</strong> Strength under control in leadership and service</li>
        <li><strong>Self-control:</strong> Integrity in decision-making, even under pressure</li>
    </ul>
    <p>These are not slogans; they are principles by which all Penthian executives, staff, and agents are expected to operate.</p>

    <h3>8.2 Code of Conduct</h3>
    <p>All staff, agents, board members, and representatives must adhere to our Code of Conduct, which includes:</p>
    <ul>
        <li>Compliance with Laws & Regulations</li>
        <li>Zero Tolerance for Bribery, Corruption, or Discrimination</li>
        <li>Professional Conduct in All Dealings</li>
        <li>Confidentiality & Respect for Data Privacy</li>
        <li>No Conflicts of Interest or Insider Dealings</li>
        <li>Transparency in Fees, Product Terms, and Risks</li>
    </ul>

    <h3>8.3 Whistleblower Protection</h3>
    <ul>
        <li>Penthian maintains a confidential whistleblower channel</li>
        <li>Employees can report:</li>
        <li>Fraud</li>
        <li>Ethical misconduct</li>
        <li>Breach of compliance</li>
        <li>Reports go directly to the Chairperson of the Board or the Compliance Officer</li>
        <li>Retaliation against whistleblowers is strictly prohibited and leads to immediate disciplinary action</li>
    </ul>

    <h3>8.4 Cultural Alignment</h3>
    <ul>
        <li>All hiring and agent onboarding includes an ethics and cultural alignment interview</li>
        <li>Annual culture audits are conducted to assess alignment across departments</li>
        <li>Company-wide values-based initiatives are held to reinforce our ethical DNA</li>
    </ul>

    <h2>9. Stakeholder Engagement & Transparency</h2>
    <p>Penthian Ltd believes that sustained value creation comes from building open, respectful, and transparent relationships with all stakeholders — including investors, regulators, clients, partners, employees, and the public.</p>

    <h3>9.1 Investor Engagement</h3>
    <ul>
        <li>Investors receive periodic performance reports, including:</li>
        <li>Asset performance metrics</li>
        <li>Token distributions and yields</li>
        <li>Platform upgrades and roadmap updates</li>
        <li>Major decisions (e.g., changes in token rights, platform fees, or governance structure) may be subject to investor voting through on-chain governance.</li>
    </ul>

    <h3>9.2 Regulatory Cooperation</h3>
    <ul>
        <li>Penthian maintains an open-door policy with the Financial Services Commission (FSC)</li>
        <li>Regulatory queries are prioritized and answered in a timely, transparent manner</li>
        <li>All required returns, audits, and filings are submitted punctually and in full</li>
    </ul>

    <h3>9.3 Transparency in Products</h3>
    <ul>
        <li>Every tokenized asset listed on Penthian’s marketplace includes:</li>
        <li>A Property or SME Whitepaper</li>
        <li>Legal structure details</li>
        <li>Voting or dividend rights</li>
        <li>Risk factors</li>
        <li>Users are provided clear disclaimers and educational content at the point of investment</li>
    </ul>

    <h3>9.4 Feedback Channels</h3>
    <ul>
        <li>Clients and investors can raise questions via:</li>
        <li>Email</li>
        <li>Platform-integrated support chat</li>
        <li>Live webinars and AMA sessions</li>
        <li>Feedback is recorded, tracked, and reviewed monthly by the Customer Experience Team</li>
    </ul>

    <h3>9.5 Media & Public Disclosure</h3>
    <ul>
        <li>Penthian maintains regular communication via:</li>
        <li>Official blog</li>
        <li>Social media channels</li>
        <li>Press releases on key updates and achievements</li>
        <li>All marketing and communications are vetted by compliance for fairness, clarity, and accuracy</li>
    </ul>

    <h2>10. Governance Review & Evolution</h2>
    <p>Penthian Ltd is committed to continuous improvement in its governance systems to ensure resilience, regulatory alignment, and institutional-grade trustworthiness as the platform scales.</p>

    <h3>10.1 Annual Governance Review</h3>
    <ul>
        <li>A comprehensive review of the governance framework is conducted once every year by:</li>
        <li>The Board of Directors</li>
        <li>The Audit & Risk Committee</li>
        <li>Independent external advisors (where necessary)</li>
        <li>This review assesses:</li>
        <li>Board and committee performance</li>
        <li>Policy effectiveness</li>
        <li>Risk oversight quality</li>
        <li>Compliance and AML effectiveness</li>
        <li>Stakeholder satisfaction and feedback trends</li>
    </ul>

    <h3>10.2 Trigger-Based Review</h3>
    <p>The governance framework may be reviewed outside the annual cycle in the event of:</p>
    <ul>
        <li>Major regulatory changes (e.g. updates to VAITOS or FATF guidance)</li>
        <li>Launch of new product verticals or token types</li>
        <li>A significant compliance incident or market event</li>
        <li>Expansion into a new jurisdiction</li>
        <li>Onboarding of new institutional partners</li>
    </ul>

    <h3>10.3 Document Management</h3>
    <ul>
        <li>All governance-related documents (policies, minutes, ToRs) are:</li>
        <li>Digitally stored and encrypted</li>
        <li>Version-controlled and timestamped</li>
        <li>Accessible to directors, regulators, and auditors upon request</li>
    </ul>

    <h3>10.4 Board Evolution Planning</h3>
    <p>To ensure strategic agility and future-readiness:</p>
    <ul>
        <li>The Board maintains a Board Skills Matrix to identify gaps</li>
        <li>New directors are considered based on:</li>
        <li>Jurisdictional relevance</li>
        <li>Blockchain/legal expertise</li>
