        <h2>6. Regulatory Framework & Class S Justification</h2>
        <h3>Overview of Mauritius’ VASP Regulatory Framework</h3>
        <p>Mauritius has established itself as a progressive jurisdiction for virtual asset innovation through the Virtual Asset and Initial Token Offering Services Act 2021 (VAITOS Act). This framework brings clarity, transparency, and international alignment to the regulation of virtual asset service providers (VASPs), while promoting innovation through responsible licensing and compliance enforcement by the Financial Services Commission (FSC).</p>
        <p>Under this act, Mauritius recognizes several classes of virtual asset services. Penthian Ltd is applying for the Class S – Virtual Asset Marketplace license, which is best aligned with our business model and operations.</p>
        <h3>Why Class S (Marketplace License)?</h3>
        <p>Penthian is not a simple token issuer or trading desk. We are building a compliant, multi-layered marketplace that allows real-world assets — such as real estate and SMEs — to be fractionalized, listed, traded, governed, and financially managed through blockchain technology. The Class S license is the only one within Mauritius’ framework that fits this complete model.</p>
        <h4>Key marketplace functions under Class S include:</h4>
        <table>
            <tr>
                <th>Function</th>
                <th>Penthian Implementation</th>
            </tr>
            <tr>
                <td>Listing & Tokenization</td>
                <td>Asset owners (developers, businesses) list tokenized real-world assets through compliant ERC-3643 smart contracts</td>
            </tr>
            <tr>
                <td>Primary Market</td>
                <td>Investors purchase fractional ownership in vetted, income-generating assets</td>
            </tr>
            <tr>
                <td>Secondary Market</td>
                <td>Peer-to-peer trading of tokens through an integrated exchange module</td>
            </tr>
            <tr>
                <td>Governance</td>
                <td>Investors participate in on-chain voting and proposals related to tokenized assets</td>
            </tr>
            <tr>
                <td>Asset Distribution</td>
                <td>Investors receive rental yield or profit share based on token holdings</td>
            </tr>
            <tr>
                <td>AML/KYC Compliance</td>
                <td>All onboarding and transactions are KYC-verified through Veriff, with additional AML protocols in place</td>
            </tr>
        </table>
        <h3>Scope of Operation Under Class S License</h3>
        <p>Penthian will:</p>
        <ul>
            <li>Operate a full digital asset marketplace from Mauritius</li>
            <li>Tokenize and fractionalize physical assets (real estate, business ownership)</li>
            <li>Enable primary listings and secondary trading of virtual asset tokens</li>
            <li>Support both retail and sophisticated investors, with different onboarding tiers</li>
            <li>Secure client assets using self-custody architecture or integrated custodial partners</li>
            <li>Maintain full compliance with AML/CFT laws through risk-based transaction monitoring</li>
        </ul>
        <h3>Why Mauritius?</h3>
        <p>Mauritius offers the perfect jurisdictional foundation for Penthian because:</p>
        <ul>
            <li>It has legal clarity and enforceable licensing for VASPs (unlike many emerging markets)</li>
            <li>It is recognized by the FATF and EU as a compliant, risk-sensitive jurisdiction</li>
            <li>It is strategically located between Africa, Asia, and the Middle East</li>
            <li>It provides a cost-effective and reputable regulatory base for global operations</li>
            <li>It allows us to build from a compliant foundation while expanding globally</li>
        </ul>
        <p>We see Mauritius not only as a regulatory base but as a long-term strategic partner in helping develop a trustworthy, investor-centric tokenized ownership economy.</p>
