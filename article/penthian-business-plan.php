        <h2>4. Background & Genesis of Penthian</h2>
        <p>Penthian was born out of a personal vision, tested through real-life experience in the property and hospitality industry, and sharpened by the possibilities of blockchain technology.</p>
        <h3>The Problem We Saw</h3>
        <p>For years, our founder Godwill Soma worked in senior leadership roles across the hotel, accommodation, and property industries in Australia. While managing multimillion-dollar buildings and negotiating large leasing deals, he constantly encountered one limiting reality: ownership was only accessible to the wealthy.</p>
        <p>His colleagues, many of whom helped build, clean, or manage these assets, could never dream of owning them. Meanwhile, developers often had unsold apartments or stalled capital due to poor exit strategies and at times tough market conditions. On the investor side, people wanted access to income-generating real estate, but entry costs, legal complexities, and liquidity barriers kept them out.</p>
        <p>There had to be a better way.</p>
        <h3>The Early Ideas</h3>
        <p>Before blockchain, Godwill explored co-ownership using traditional legal structures. He consulted conveyancers, lawyers and mortgage brokers, including CPA and CA accountants and explored ways for groups to co-buy apartments or hotels. The legal setup was inefficient, the cost was high, and the model didn’t scale.</p>
        <p>In 2018, he discovered Ethereum. After seeing a friend profit from trading crypto in 2019, he dove deep into smart contracts, decentralization, and fractional asset ownership. He realized blockchain could solve every friction point: legal, financial, and operational that he had faced in traditional co-ownership.</p>
        <p>He tried to explain this to his father, a priest and former mechanic in Africa, using real estate examples: “Imagine if an apartment building could issue digital shares to investors, and everything ran like an automated business on the blockchain.” It didn’t fully make sense to his father then, but it became a defining moment. The vision became clear.</p>
        <h3>From Fractuate to Penthian</h3>
        <p>The original concept began under the name Fractuate — a platform to allow people to co-own hotels, real estate, and businesses. Over time, the vision matured. The technology advanced. The brand evolved.</p>
        <p>By 2024, Fractuate had built:</p>
        <ul>
            <li>A live tokenization and trading platform</li>
            <li>A working MVP using ERC-721, ERC-1155, and later ERC-3643</li>
            <li>Integration with Veriff for full KYC</li>
            <li>A self-custody wallet under development</li>
            <li>Functional modules for rental income distribution, secondary market auctions, and on-chain voting</li>
        </ul>
        <p>As the platform matured, a new name was needed, one that reflected structure, balance, trust, and long-term vision. Thus, Penthian was born.</p>
        <h3>Why Mauritius?</h3>
        <p>Mauritius is strategically positioned as a compliant, blockchain-friendly jurisdiction. It offers strong regulatory clarity and international credibility — making it the ideal base for Penthian to apply for its Class S VASP license and operate globally with integrity.</p>
