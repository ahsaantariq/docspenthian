    <div class="section">
        <h2>3. Outsourcing Risk Management Controls</h2>
        <p>Penthian Ltd maintains strong internal oversight to ensure that all third-party providers deliver services in line with regulatory expectations, security requirements, and business continuity standards.</p>
        <h3>3.1 Due Diligence & Selection</h3>
        <p>Before onboarding any service provider, Penthian conducts a thorough review that includes:</p>
        <ul>
            <li>Reputation check and track record of the provider</li>
            <li>Review of licenses, certifications, and legal standing</li>
            <li>Assessment of data protection measures and compliance with relevant laws (e.g. GDPR)</li>
            <li>Evaluation of technical capability, scalability, and alignment with platform needs</li>
            <li>Vetting of ownership structure and any potential conflicts of interest</li>
        </ul>
        <h3>3.2 Contractual Safeguards</h3>
        <p>All outsourcing arrangements are governed by formal contracts that include:</p>
        <ul>
            <li>Service level agreements (SLAs) defining performance, uptime, and turnaround times</li>
            <li>Data handling clauses including encryption, confidentiality, and breach notification</li>
            <li>Audit rights allowing Penthian or its regulators to inspect operations</li>
            <li>Termination and exit clauses to ensure transfer of data and minimal service disruption</li>
        </ul>
        <h3>3.3 Ongoing Monitoring</h3>
        <p>Penthian monitors outsourced providers through:</p>
        <ul>
            <li>Quarterly performance reviews against SLA targets</li>
            <li>Annual audits or self-certification of controls</li>
            <li>Incident reporting requirements for service failures or breaches</li>
            <li>Regular meetings or updates with technical and compliance teams</li>
        </ul>
        <h3>3.4 Contingency Planning</h3>
        <ul>
            <li>Critical providers (e.g. Veriff, infrastructure) have:
                <ul>
                    <li>Redundancy protocols and backup infrastructure</li>
                    <li>Disaster recovery procedures tested semi-annually</li>
                    <li>Defined transition plans if vendor replacement is required</li>
                </ul>
            </li>
        </ul>
        <h3>3.5 Compliance Officer Oversight</h3>
        <ul>
            <li>The MLRO and Compliance Officer oversee the management of third-party risks</li>
            <li>The Board of Directors is briefed annually on the performance and risk status of all critical vendors</li>
            <li>Key vendor risk issues are logged in the Risk Register and escalated through internal governance</li>
        </ul>
    </div>

    <div class="section">
        <h2>4. Data Security in Outsourced Arrangements</h2>
        <p>Given Penthian’s reliance on third-party services to support critical operations, the secure handling of data—especially client, investor, and token-related data—is a top priority. All outsourcing partners must comply with Penthian’s strict data security policies.</p>
        <h3>4.1 Data Classification & Protection</h3>
        <ul>
            <li>All data shared with vendors is categorized according to sensitivity:
                <ul>
                    <li>Public – General website content or documentation</li>
                    <li>Internal – Employee or operational data</li>
                    <li>Confidential – Client identity, smart contracts, source code</li>
                    <li>Highly Confidential – Wallet credentials, biometric data, encrypted keys</li>
                </ul>
            </li>
            <li>Confidential and highly confidential data must:
                <ul>
                    <li>Be encrypted in transit and at rest</li>
                    <li>Be stored only on approved secure servers</li>
                    <li>Never be shared without prior written approval</li>
                </ul>
            </li>
        </ul>
        <h3>4.2 Access Controls</h3>
        <ul>
            <li>Role-based access is enforced, ensuring that vendors only access data necessary to perform their function</li>
            <li>Each vendor is assigned unique API credentials or access tokens</li>
            <li>Access logs are reviewed monthly by the Compliance and Tech teams</li>
        </ul>
        <h3>4.3 Data Residency & Storage</h3>
        <ul>
            <li>Penthian favors vendors with data centers in compliant jurisdictions, prioritizing:
                <ul>
                    <li>Mauritius</li>
                    <li>EU (GDPR-compliant)</li>
                    <li>Australia, UAE, or other FSC-recognized countries</li>
                </ul>
            </li>
            <li>Cross-border data transfer agreements are in place to protect user rights and platform integrity</li>
        </ul>
        <h3>4.4 Data Breach Handling</h3>
        <ul>
            <li>Vendors must immediately report any:
                <ul>
                    <li>Unauthorized access or exfiltration of data</li>
                    <li>Malware, ransomware, or denial-of-service attack</li>
                    <li>Misuse or accidental exposure of Penthian’s confidential data</li>
                </ul>
            </li>
            <li>Penthian has a 24-hour incident response window to notify affected users and regulators</li>
        </ul>
        <h3>4.5 Audits & Penetration Testing</h3>
        <ul>
            <li>Vendors involved in hosting, security, or KYC functions must:
                <ul>
                    <li>Undergo annual third-party security audits</li>
                    <li>Participate in penetration tests initiated by Penthian or its auditors</li>
                    <li>Share the results with Penthian’s Compliance and Technology teams</li>
                </ul>
            </li>
        </ul>
    </div>

    <div class="section">
        <h2>5. Termination & Exit Strategy</h2>
        <p>Penthian Ltd maintains clear procedures for disengaging with third-party vendors in a way that protects data, maintains service continuity, and mitigates operational disruption.</p>
        <h3>5.1 Termination Triggers</h3>
        <p>Outsourcing arrangements may be terminated due to:</p>
        <ul>
            <li>Breach of contract, non-performance, or repeated SLA violations</li>
            <li>Security incidents or non-compliance with data protection laws</li>
            <li>Change of control, insolvency, or regulatory sanctions on the vendor</li>
            <li>Strategic shift (e.g., moving services in-house or changing providers)</li>
            <li>Regulatory instruction or FSC-mandated change</li>
        </ul>
        <h3>5.2 Exit Protocol</h3>
        <p>When offboarding a provider, Penthian ensures:</p>
        <ul>
            <li>Secure handover of all data, keys, and documentation</li>
            <li>Deletion or return of client data within a specified timeline</li>
            <li>Access privileges are immediately revoked</li>
            <li>All non-compete and confidentiality clauses remain enforceable post-termination</li>
        </ul>
        <h3>5.3 Service Continuity</h3>
        <p>To maintain uninterrupted operations:</p>
        <ul>
            <li>Backup providers or in-house contingencies are identified for all critical services</li>
            <li>Each contract includes a 30 to 90-day transition period</li>
            <li>The Compliance and Technology teams jointly oversee the migration process</li>
        </ul>
        <h3>5.4 Documentation & Reporting</h3>
        <ul>
            <li>Every termination event is:
                <ul>
                    <li>Logged in the Outsourcing Register</li>
                    <li>Reviewed by the Board or Risk Committee</li>
                    <li>Audited during the next internal or external compliance review</li>
                </ul>
            </li>
        </ul>
