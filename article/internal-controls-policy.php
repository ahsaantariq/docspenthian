     <h2>1. Introduction & Objective</h2>
    <p>This Internal Controls Policy outlines the key principles, structures, and procedures that govern how Penthian Ltd manages its business operations, regulatory obligations, and risk exposures in line with the Financial Services Commission (FSC) requirements under the Virtual Asset and Initial Token Offering Services Act 2021 (VAITOS) and the Financial Intelligence and Anti-Money Laundering Act (FIAMLA).</p>
