    </ul>
    <p>Failure to adhere to this policy may result in disciplinary action, up to and including termination of employment or contract, and may be reported to the relevant authorities where required by law.</p>

    <h2>3. Governance Structure</h2>
    <p>Penthian Ltdâ€™s governance framework is built on clear accountability, layered oversight, and operational independence. The structure is designed to support strong internal controls, uphold regulatory obligations under the FSC, and ensure that decision-making is informed, ethical, and transparent.</p>

    <h3>A. Board of Directors</h3>
    <p>The Board of Directors is the highest governing body of Penthian Ltd and is ultimately responsible for:</p>
    <ul>
        <li>Approving and overseeing the internal control framework</li>
        <li>Appointing the Money Laundering Reporting Officer (MLRO) and Compliance Officer</li>
        <li>Setting risk appetite and strategic direction</li>
        <li>Approving policies related to AML/CFT, cybersecurity, governance, and investor protection</li>
        <li>Reviewing internal audit reports and compliance updates</li>
        <li>Ensuring management adheres to regulatory and fiduciary duties</li>
    </ul>
    <p>Board meetings occur at least quarterly or more frequently if significant issues arise.</p>

    <h3>B. Executive Management</h3>
    <p>The Executive Team (CEO, COO, CTO, CFO) is responsible for implementing the policies and strategies approved by the Board. Their duties include:</p>
    <ul>
        <li>Daily operational oversight of the Penthian platform</li>
        <li>Delegating tasks to functional managers (Compliance, Legal, Technology, Product)</li>
        <li>Monitoring and reporting performance metrics</li>
        <li>Ensuring resourcing and budget for internal control functions</li>
        <li>Ensuring segregation of duties across business and technical teams</li>
        <li>Overseeing third-party vendor contracts and risk</li>
    </ul>

    <h3>C. Compliance Department</h3>
    <p>Led by the Compliance Officer, this department is responsible for:</p>
    <ul>
        <li>Monitoring AML/CFT compliance across onboarding, tokenization, and transactions</li>
        <li>Overseeing KYC/KYB processes via Veriff integration</li>
        <li>Maintaining the AML/CFT Manual and Internal Controls Policy</li>
        <li>Conducting staff training on compliance procedures</li>
        <li>Ensuring compliance with ERC-3643 smart contract rules</li>
        <li>Reviewing suspicious activity and escalating to the MLRO</li>
    </ul>
    <p>The Compliance Officer reports independently to both the CEO and the Board.</p>

    <h3>D. MLRO (Money Laundering Reporting Officer)</h3>
    <p>The MLRO is a designated senior officer responsible for:</p>
    <ul>
        <li>Reviewing and filing Suspicious Transaction Reports (STRs) to the FIU</li>
        <li>Liaising with regulators and law enforcement</li>
        <li>Investigating red-flagged behavior or transactions</li>
        <li>Approving high-risk client onboarding and enhanced due diligence cases</li>
        <li>Coordinating internal AML investigations and reporting to the Board</li>
    </ul>
    <p>The MLRO must be functionally independent from sales, marketing, and operations.</p>

    <h3>E. Internal Audit Function</h3>
    <p>Penthian maintains a semi-annual internal audit function, either in-house or outsourced, to:</p>
    <ul>
        <li>Assess the effectiveness of internal controls</li>
        <li>Test compliance with AML/CFT and cybersecurity policies</li>
        <li>Identify weaknesses or violations</li>
        <li>Recommend improvements and corrective actions</li>
        <li>Report findings directly to the Board</li>
    </ul>

    <h3>F. Technology & Smart Contract Oversight</h3>
    <p>The CTO and Smart Contract Auditing Team are responsible for:</p>
    <ul>
        <li>Reviewing all platform and wallet code for security vulnerabilities</li>
        <li>Ensuring code deployments respect compliance rules (e.g., KYC enforcement)</li>
        <li>Maintaining logs of all code changes, upgrades, and permissioning updates</li>
        <li>Performing regular penetration tests and smart contract audits</li>
        <li>Collaborating with the Compliance Officer on system updates</li>
    </ul>

    <h3>G. Reporting Channels</h3>
    <p>All control-related issues, breaches, or exceptions are escalated through these channels:</p>
    <ul>
        <li>Compliance Officer â†’ Executive Team & MLRO</li>
        <li>MLRO â†’ FIU, where required</li>
        <li>Internal Auditor â†’ Board of Directors</li>
        <li>CTO â†’ Executive Team (for code-related issues)</li>
        <li>Staff or Whistleblower â†’ Independent reporting channel (Section 13)</li>
    </ul>

    <h2>4. Roles & Responsibilities</h2>
    <p>To ensure operational integrity, regulatory compliance, and risk mitigation, Penthian Ltd assigns clear roles and responsibilities across the organization. This clarity avoids conflict of interest, ensures segregation of duties, and creates accountability for every core function of the business.</p>

    <h3>A. Board of Directors</h3>
    <ul>
        <li>Approves all core internal policies and compliance procedures</li>
        <li>Oversees the risk management and internal control framework</li>
        <li>Receives regular reports from the MLRO and Compliance Officer</li>
        <li>Signs off on strategic and high-risk decisions (e.g., new jurisdictions, partnerships)</li>
        <li>Holds final authority on breaches, escalations, and remediation plans</li>
    </ul>

    <h3>B. Chief Executive Officer (CEO)</h3>
    <ul>
        <li>Sets strategic direction aligned with compliance and risk standards</li>
        <li>Ensures the organizationâ€™s operations comply with FSC regulations</li>
        <li>Oversees all departments and delegates authority to Executive Team</li>
        <li>Supports internal audits and implements Board-approved changes</li>
        <li>Upholds ethical leadership and fiduciary accountability</li>
    </ul>

    <h3>C. Chief Operating Officer (COO)</h3>
    <ul>
        <li>Ensures operational procedures comply with approved policies</li>
        <li>Coordinates activities across departments to maintain business continuity</li>
        <li>Implements internal controls within customer service, asset listings, and operations</li>
        <li>Supports the Compliance Officer with procedural enforcement</li>
    </ul>

    <h3>D. Chief Technology Officer (CTO)</h3>
    <ul>
        <li>Oversees smart contract development, deployment, and audits</li>
        <li>Maintains system logs, release notes, and access records</li>
        <li>Ensures that blockchain architecture enforces AML/CFT rules (e.g., ERC-3643 restrictions)</li>
        <li>Works with Compliance to prevent vulnerabilities in the tokenization and trading engine</li>
    </ul>

    <h3>E. Compliance Officer</h3>
    <ul>
        <li>Maintains the AML/CFT Manual and Internal Controls Policy</li>
        <li>Reviews onboarding, investor verification, and issuer documentation</li>
        <li>Ensures use of Veriff and other compliance tools is effective and aligned with FSC standards</li>
        <li>Conducts ongoing monitoring and investigates flagged activities</li>
        <li>Reports breaches and policy exceptions to the MLRO and Executive Team</li>
        <li>Manages employee training programs</li>
    </ul>

    <h3>F. Money Laundering Reporting Officer (MLRO)</h3>
    <ul>
        <li>Files Suspicious Transaction Reports (STRs) with the FIU</li>
        <li>Approves onboarding of high-risk clients and issuers</li>
        <li>Reviews compliance audits and red flags raised internally</li>
        <li>Oversees remediation steps for any reported misconduct</li>
        <li>Serves as the main point of contact with the Financial Services Commission (FSC)</li>
    </ul>

    <h3>G. Internal Auditor / Audit Committee</h3>
    <ul>
        <li>Performs independent reviews of internal controls</li>
        <li>Evaluates financial procedures, data security, and compliance effectiveness</li>
        <li>Reports findings to the Board and ensures tracking of corrective actions</li>
        <li>May include outsourced experts when appropriate</li>
    </ul>

    <h3>H. Developers & Smart Contract Engineers</h3>
    <ul>
        <li>Build and maintain platform features in accordance with internal control and compliance requirements</li>
        <li>Follow change management and peer-review procedures before pushing updates</li>
        <li>Ensure transfer logic enforces permissioning and KYC-based restrictions (via ERC-3643)</li>
        <li>Maintain development logs and audit trails</li>
    </ul>

    <h3>I. Operations Team</h3>
    <ul>
        <li>Executes onboarding flows, client servicing, and token listing logistics</li>
        <li>Reports irregularities, user complaints, or potential breaches to Compliance</li>
        <li>Adheres to platform usage policies and customer data protection protocols</li>
    </ul>

    <h3>J. All Employees & Contractors</h3>
    <ul>
        <li>Must complete AML/CFT and internal controls training</li>
        <li>Are expected to escalate suspicious or unusual activity</li>
        <li>Have access to anonymous reporting channels (see Section 13)</li>
        <li>Are responsible for protecting confidential information and client data</li>
    </ul>
    <p>This section ensures that no single individual has unchecked authority or unrestricted access to sensitive systems or client funds. Every key function has oversight, peer checks, and escalation points â€” helping maintain the security and trust Penthian promises its users.</p>

    <h2>5. Segregation of Duties</h2>
    <p>Segregation of duties (SoD) is a foundational principle of Penthianâ€™s internal control framework. It ensures that no single individual or team has end-to-end control over any critical process â€” especially those involving financial transactions, compliance approvals, smart contract deployment, or client data.</p>
    <p>This division of responsibility reduces the risk of:</p>
    <ul>
        <li>Fraud and misuse of authority</li>
        <li>Conflicts of interest</li>
        <li>Unintentional errors</li>
        <li>Unauthorized transactions</li>
        <li>Regulatory non-compliance</li>
    </ul>

    <h3>A. Key Areas Where SoD Is Enforced</h3>
    <h4>1. Onboarding & KYC</h4>
    <ul>
        <li>Operations staff conduct first-level KYC and document collection</li>
        <li>Compliance Officer reviews and validates onboarding</li>
        <li>MLRO approves high-risk clients and enhanced due diligence (EDD) cases</li>
    </ul>
    <h4>2. Smart Contract Deployment</h4>
    <ul>
        <li>Developers write code</li>
        <li>CTO or designated approver reviews code and signs off</li>
        <li>Smart contract auditor (internal or third-party) conducts formal audit</li>
        <li>Deployment key is held separately and requires dual-authorization (CTO + CEO or Compliance)</li>
    </ul>
    <h4>3. Token Listing Approval</h4>
    <ul>
        <li>Issuer onboarding handled by Business Development</li>
        <li>Legal review by Compliance and MLRO for KYB and regulatory risk</li>
        <li>Technical review by the CTOâ€™s team for token contract structure</li>
        <li>Board or senior compliance officer gives final go-ahead for listing</li>
    </ul>
    <h4>4. Fund & Token Transfer Authority</h4>
    <ul>
        <li>Wallet custody and treasury operations require multi-signature authorization</li>
        <li>No single party can move funds from custodial wallets or treasury pools</li>
        <li>All transactions are logged and must be reviewed weekly by Compliance</li>
    </ul>
    <h4>5. Access to Systems</h4>
    <ul>
        <li>Tech Team maintains infrastructure but cannot access compliance data</li>
        <li>Compliance Team accesses onboarding data but has no backend code deployment authority</li>
        <li>Admin privileges are divided between CTO, COO, and Compliance, based on function</li>
    </ul>

    <h3>B. Role Rotation and Cross-Checking</h3>
    <ul>
        <li>Penthian rotates staff in key operational positions (e.g., KYC review) at regular intervals</li>
        <li>Dual-review is required for sensitive approvals (e.g., wallet whitelisting, EDD clearance)</li>
        <li>Quarterly internal audits verify that no SoD violations have occurred</li>
        <li>SoD logic is enforced both at human and smart contract levels (via ERC-3643 permissioning)</li>
    </ul>

    <h3>C. Exceptions Handling</h3>
    <p>In exceptional or urgent cases where segregation is temporarily bypassed (e.g., staff illness, system emergency), the following must occur:</p>
    <ul>
        <li>A written exception request is logged with the Compliance Officer</li>
        <li>CEO or Board must approve the temporary measure</li>
        <li>Follow-up review of the event is conducted within 72 hours</li>
        <li>The reason and decision are documented and stored for audit purposes</li>
    </ul>

    <h3>D. Training & Monitoring</h3>
    <ul>
        <li>All staff are trained on SoD principles during onboarding and annually thereafter</li>
        <li>Any breach or conflict of interest must be reported immediately to the MLRO or via the Whistleblower Channel</li>
        <li>Automated systems (e.g., user onboarding, asset transfer, token listing) are built to enforce segregation through role-based access control and activity logs</li>
    </ul>
    <p>This strict enforcement of Segregation of Duties ensures that no single person has the power to compromise user funds, bypass compliance, or manipulate core processes â€” strengthening trust, transparency, and institutional resilience across the Penthian ecosystem.</p>

    <h2>6. Access Controls & Information Security</h2>
    <p>Protecting digital infrastructure, client data, wallet systems, and backend operational tools is central to Penthianâ€™s risk management and compliance framework. This section outlines how access to systems, data, and smart contracts is tightly controlled and monitored to prevent unauthorized use, data breaches, and malicious attacks.</p>

    <h3>A. Role-Based Access Control (RBAC)</h3>
    <p>Penthian enforces Role-Based Access Control (RBAC) across all systems â€” including its tokenization engine, wallet infrastructure, admin portals, and KYC/AML platforms. Access is granted based strictly on:</p>
    <ul>
        <li>Role and department</li>
        <li>Operational necessity (least privilege principle)</li>
        <li>Segregation of Duties (Section 5)</li>
        <li>Executive or Compliance approval</li>
    </ul>
    <p>Examples:</p>
    <ul>
        <li>Only the Compliance Team can access KYC records via Veriff.</li>
        <li>Only the CTO and assigned engineers can deploy or upgrade smart contracts.</li>
        <li>Only finance staff and MLRO can access transactional records and reporting logs.</li>
    </ul>
    <p>All access changes are logged and reviewed quarterly.</p>

    <h3>B. Authentication & Credential Management</h3>
    <ul>
        <li>All internal systems require multi-factor authentication (MFA)</li>
        <li>Admin passwords must meet high-entropy standards and rotate every 90 days</li>
        <li>Use of hardware security modules (HSMs) or encrypted key vaults for sensitive credentials</li>
        <li>API keys for third-party systems are regularly rotated and access-scoped</li>
    </ul>

    <h3>C. Smart Contract and Wallet Security</h3>
    <ul>
        <li>Wallets used for treasury, user assets, and smart contract deployment are multi-signature secured</li>
        <li>Smart contract upgrades are logged, require multi-person sign-off, and are reviewed before execution</li>
        <li>All smart contracts undergo periodic code audits and formal testing before launch</li>
        <li>ERC-3643 permissioning is used to prevent unauthorized token transfers and enforce compliance controls</li>
    </ul>

    <h3>D. Data Protection</h3>
    <p>All personal and sensitive data is:</p>
    <ul>
        <li>Encrypted at rest and in transit (AES-256 or equivalent)</li>
        <li>Hosted on ISO 27001 and GDPR-compliant cloud infrastructure</li>
        <li>Monitored for unauthorized access attempts or anomalies</li>
        <li>Backed up regularly and stored in secured regions (with geographical redundancy)</li>
    </ul>

    <h3>E. Logging and Monitoring</h3>
    <ul>
        <li>All system access is logged in real time, including successful and failed login attempts</li>
        <li>Audit logs are immutable and stored for a minimum of 7 years</li>
        <li>Suspicious behavior is flagged using automated tools (e.g., unusual access times or locations)</li>
        <li>System logs are reviewed by the CTO and Internal Auditor on a scheduled basis</li>
    </ul>

    <h3>F. Incident Response</h3>
    <p>In the event of a breach or suspected compromise:</p>
    <ol>
        <li>CTO and MLRO are alerted immediately</li>
        <li>A forensic review is initiated within 24 hours</li>
        <li>Access is revoked or restricted to isolate affected systems</li>
        <li>Regulators (FSC, FIU) are notified if necessary</li>
        <li>All incidents are documented and submitted to the Board and Internal Audit team</li>
        <li>A root cause analysis is conducted and fixes deployed within 72 hours</li>
    </ol>

    <h3>G. Third-Party Access</h3>
    <ul>
        <li>External vendors (e.g., smart contract auditors, Veriff) are given access only through scoped and monitored APIs</li>
        <li>All third-party tools must be vetted for security certifications and compliance standing</li>
        <li>Access logs and integrations are reviewed quarterly by the Compliance Officer and CTO</li>
    </ul>

    <h3>H. Staff Responsibilities</h3>
    <ul>
        <li>Employees are prohibited from sharing passwords or credentials</li>
        <li>Remote work access must occur via VPN with MFA</li>
        <li>Suspicious emails, phishing attempts, or abnormal system behavior must be reported immediately</li>
        <li>Quarterly training is provided on cybersecurity best practices and phishing simulations</li>
    </ul>
    <p>Access control and information security form the backbone of trust in the Penthian ecosystem. By combining strict internal permissions, encryption, and proactive monitoring, Penthian ensures that client assets, personal data, and operational systems remain secure, auditable, and protected from internal or external threats.</p>

    <h2>7. Operational Controls & Process Oversight</h2>
