    <ul>
        <li>Operates with integrity, accountability, and discipline</li>
        <li>Implements effective segregation of duties and risk mitigation</li>
        <li>Maintains clear lines of reporting and responsibility across departments</li>
        <li>Minimizes the risk of fraud, operational failures, or regulatory breaches</li>
        <li>Upholds its fiduciary and ethical obligations to clients, investors, and regulators</li>
        <li>Safeguards client assets and personal data</li>
        <li>Detects and responds quickly to anomalies, threats, or compliance issues</li>
    </ul>
    <p>This policy is aligned with Penthianâ€™s values and founding principles â€” including our commitment to transparency, investor protection, technological trust, and the fruits of the Spirit (love, joy, peace, patience, kindness, goodness, faithfulness, gentleness, and self-control). These values are embedded in our leadership, compliance, and smart contract protocols.</p>
    <p>All employees, officers, directors, and contractors must read, understand, and comply with this Internal Controls Policy. It is reviewed regularly and approved by the Board of Directors.</p>

    <h2>2. Scope & Applicability</h2>
    <p>This Internal Controls Policy applies to all business units, departments, and personnel within Penthian Ltd, including directors, senior executives, employees, contractors, agents, and technology service providers involved in the operation of the Penthian platform.</p>
    <p>It covers the full lifecycle of our platformâ€™s operations and activities, including but not limited to:</p>
    <ul>
        <li>Client onboarding and KYC/AML processes</li>
        <li>Tokenization of real-world assets (RWA)</li>
        <li>Listing and trading on the Penthian primary and secondary marketplaces</li>
        <li>Asset and investor management, including revenue distribution</li>
        <li>Use of smart contracts, wallets, and ERC-3643 token permissioning</li>
        <li>Risk scoring, monitoring, and alert handling</li>
        <li>Governance of service providers, vendors, and outsourced technology partners</li>
        <li>Financial and treasury operations</li>
        <li>Recordkeeping and reporting to regulators (FSC, FIU)</li>
        <li>Compliance with data protection laws and cross-border restrictions</li>
        <li>Internal and external audits, reviews, and investigations</li>
    </ul>
    <p>This policy also extends to automated systems and smart contract protocols, which are considered functionally equivalent to internal teams when enforcing platform rules and compliance logic. Where technology is used to perform functions typically assigned to humans (e.g., transaction validation, KYC checks, wallet restrictions), those functions fall under the same internal control framework.</p>
    <p>The policy is mandatory for:</p>
    <ul>
        <li>All current employees and management of Penthian Ltd</li>
        <li>All third-party service providers involved in compliance-sensitive operations</li>
        <li>Any new hire, executive, or advisor during their onboarding process</li>
