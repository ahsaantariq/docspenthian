    <h2>8. Business Continuity & Disaster Recovery</h2>
    <p>Ensuring the continuity of critical services and rapid recovery from disruptions is vital to protecting Penthian’s users, platform, and regulatory obligations. This section defines how Penthian prepares for, responds to, and recovers from IT-related incidents or external disasters.</p>

    <h3>8.1 Objectives</h3>
    <p>Penthian’s Business Continuity and Disaster Recovery framework is designed to:</p>
    <ul>
        <li>Maintain continuous access to the platform’s core services</li>
        <li>Minimize financial, operational, and reputational impact from disruptions</li>
        <li>Restore mission-critical operations within agreed recovery timeframes</li>
        <li>Ensure data integrity, availability, and confidentiality at all times</li>
        <li>Comply with FSC guidelines and international standards (e.g., ISO 22301)</li>
    </ul>

    <h3>8.2 Key Definitions</h3>
    <ul>
        <li><strong>Business Continuity Plan (BCP):</strong> Procedures to maintain operations during adverse conditions</li>
        <li><strong>Disaster Recovery Plan (DRP):</strong> Procedures to restore IT infrastructure after a catastrophic event</li>
        <li><strong>Recovery Time Objective (RTO):</strong> Max acceptable time to restore service (Target: 4 hours)</li>
        <li><strong>Recovery Point Objective (RPO):</strong> Max acceptable data loss window (Target: 1 hour)</li>
    </ul>

    <h3>8.3 Covered Scenarios</h3>
    <p>The BCDR plan addresses:</p>
    <table>
        <tr>
            <th>Category</th>
            <th>Examples</th>
        </tr>
        <tr>
            <td>Natural Disasters</td>
            <td>Earthquakes, floods, fires</td>
        </tr>
        <tr>
            <td>Infrastructure Failure</td>
            <td>Power outage, server crash, data loss</td>
        </tr>
        <tr>
            <td>Cyber Events</td>
            <td>Ransomware, DDoS, data breach</td>
        </tr>
        <tr>
            <td>Human Error</td>
            <td>Accidental deletion, misconfiguration</td>
        </tr>
        <tr>
            <td>Regulatory Actions</td>
            <td>Government intervention, license issues</td>
        </tr>
        <tr>
            <td>Pandemic Events</td>
            <td>Staff unavailability due to illness</td>
        </tr>
    </table>

    <h3>8.4 System Redundancy</h3>
    <p>Penthian maintains high availability through:</p>
    <ul>
        <li>Multi-zone cloud architecture (AWS or equivalent)</li>
        <li>Redundant instances for web servers, databases, and blockchain nodes</li>
        <li>Auto-scaling and load balancing to manage traffic surges</li>
        <li>Offsite backups stored in separate physical regions</li>
        <li>Secondary environments for hot-failover during outages</li>
    </ul>

    <h3>8.5 Data Backup Protocol</h3>
    <ul>
        <li>Daily incremental backups of all user and transaction data</li>
        <li>Weekly full system backups stored securely with encryption</li>
        <li>Monthly validation tests to ensure backup integrity</li>
        <li>Backups stored across multiple jurisdictions to comply with legal requirements</li>
    </ul>

    <h3>8.6 Disaster Recovery Plan (DRP)</h3>
    <p>Key steps of Penthian’s DRP:</p>
    <ol>
        <li>Incident detection and triage</li>
        <li>Activation of the DRP and response team</li>
        <li>Switch to backup infrastructure or failover systems</li>
        <li>User and stakeholder notifications</li>
        <li>System restoration and testing</li>
        <li>Post-incident review and documentation</li>
    </ol>
    <p>Critical services (e.g., wallet access, admin panel, transaction engine) must be restored within 4 hours.</p>

    <h3>8.7 Communication Strategy</h3>
    <p>In the event of a disruption:</p>
    <ul>
        <li>Users will be notified via email, in-app notifications, and website banners</li>
        <li>Major stakeholders (regulators, partners) will be contacted directly</li>
        <li>A status page (e.g., status.penthian.com) may be updated in real time</li>
        <li>Incident logs and updates will be documented for transparency and audit</li>
    </ul>

    <h3>8.8 Testing & Review</h3>
    <ul>
        <li>Quarterly simulation drills involving key staff and systems</li>
        <li>Annual full-scale recovery exercise</li>
        <li>Regular updates to contact lists, failover plans, and playbooks</li>
        <li>Lessons learned are documented and used to update the BCDR procedures</li>
    </ul>

