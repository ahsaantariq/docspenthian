    <h2>7. Software Development & Change Management</h2>
    <p>This section outlines the controls and procedures used to manage software development, updates, and smart contract deployment on the Penthian platform. It ensures all changes are secure, traceable, and aligned with regulatory and operational requirements.</p>

    <h3>7.1 Development Lifecycle Model</h3>
    <p>Penthian follows an Agile Development Lifecycle using a CI/CD (Continuous Integration/Continuous Deployment) pipeline. Key stages include:</p>
    <ol>
        <li>Requirement Gathering</li>
        <li>Architecture & Design</li>
        <li>Development & Peer Review</li>
        <li>Testing (Unit, Integration, QA)</li>
        <li>Staging & UAT (User Acceptance Testing)</li>
        <li>Production Deployment</li>
        <li>Post-Deployment Monitoring</li>
    </ol>
    <p>All steps are documented and version-controlled using Git repositories.</p>

    <h3>7.2 Code Review & Testing Standards</h3>
    <p>Every code change must pass:</p>
    <ul>
        <li>Automated unit tests with minimum 90% coverage</li>
        <li>Manual peer review by at least one senior developer</li>
        <li>Static code analysis using secure code scanning tools</li>
        <li>QA testing in isolated environments</li>
    </ul>
    <p>For smart contracts:</p>
    <ul>
        <li>Contract logic must undergo internal testing and simulation</li>
        <li>At least one external audit is required before deployment to mainnet</li>
        <li>Results must be published or made available upon request</li>
    </ul>

    <h3>7.3 Smart Contract Deployment Protocols</h3>
    <p>Smart contract deployments follow a strict process:</p>
    <ul>
        <li>Tested on testnets (e.g., Mumbai, Goerli) before mainnet release</li>
        <li>Reviewed by internal and third-party security experts</li>
        <li>Controlled by multi-signature wallets or time-locked upgrade mechanisms</li>
        <li>Monitored post-launch via block explorer tools and event logs</li>
    </ul>
    <p>Upgrades are communicated in advance and include rollback strategies where possible.</p>

    <h3>7.4 Change Management Process</h3>
    <p>All changes to code, infrastructure, or platform features are tracked through a formal Change Management system. This includes:</p>
    <ul>
        <li>Change request submission</li>
        <li>Risk assessment and impact analysis</li>
        <li>Approval from CTO and Compliance Officer</li>
        <li>Scheduled deployment windows</li>
        <li>Testing confirmation and stakeholder sign-off</li>
    </ul>
    <p>Emergency changes follow a fast-tracked protocol but must still be logged and reviewed post-deployment.</p>

    <h3>7.5 Rollback & Contingency Procedures</h3>
    <p>For all major deployments:</p>
    <ul>
        <li>A rollback plan is prepared in case of critical failure</li>
        <li>Snapshots or backups of databases and systems are taken before deployment</li>
        <li>Canary or phased rollouts may be used for new features</li>
    </ul>
    <p>If a failure occurs:</p>
    <ul>
        <li>Affected components are reverted to the last stable state</li>
        <li>All users are notified within 2 hours (if material)</li>
        <li>Incident reports are reviewed by the Board and stored for audit</li>
    </ul>

    <h3>7.6 Third-Party Code & Open-Source Libraries</h3>
    <p>Use of third-party or open-source code is allowed only if:</p>
    <ul>
        <li>The code is from a reputable, actively maintained source</li>
        <li>License terms are compatible with Penthian’s platform and tokenization models</li>
        <li>Code is reviewed internally for backdoors or vulnerabilities</li>
        <li>Dependencies are version-locked to prevent supply chain exploits</li>
    </ul>

    <h3>7.7 Audit Trails & Documentation</h3>
    <p>All development activities are:</p>
    <ul>
        <li>Logged in project management systems (e.g., Jira, GitHub/GitLab)</li>
        <li>Linked to unique pull requests and issue tickets</li>
        <li>Assigned to specific team members with timestamps</li>
        <li>Archived for at least 5 years for audit purposes</li>
