  <h2>1. Purpose & Scope</h2>
    <h3>1.1 Purpose</h3>
    <p>The purpose of this Information Technology (IT) Policy is to establish a secure, efficient, and resilient digital infrastructure that supports the operations of Penthian Ltd, a regulated virtual asset marketplace operating under the supervision of the Financial Services Commission (FSC) of Mauritius.</p>
    <p>This policy governs the deployment, use, and maintenance of all information systems — including smart contracts, wallets, servers, cloud infrastructure, data protection protocols, access controls, and cybersecurity defenses — that underpin the Penthian ecosystem.</p>
    <p>The IT Policy is also designed to align with:</p>
    <ul>
        <li>The Virtual Asset and Initial Token Offering Services Act 2021 (VAITOS)</li>
        <li>FSC-issued guidelines for VASP license holders</li>
        <li>International cybersecurity best practices (e.g., ISO/IEC 27001, NIST)</li>
        <li>Platform-specific standards for ERC-3643 compliance, data privacy, and investor protection</li>
    </ul>
    <p>This document will be updated regularly to reflect changes in technology, regulation, and business model evolution.</p>

    <h3>1.2 Scope</h3>
    <p>This policy applies to all:</p>
    <ul>
        <li>Digital systems and infrastructure managed by or on behalf of Penthian Ltd</li>
        <li>Third-party service providers and contractors with access to Penthian’s IT systems</li>
        <li>Employees, developers, agents, and operators responsible for interacting with the production and test environments</li>
        <li>User-facing components such as wallets, investor dashboards, admin panels, and token smart contracts</li>
        <li>Hosting and storage systems, including cloud platforms, server instances, and backup repositories</li>
        <li>Operational tools, such as analytics dashboards, monitoring scripts, support systems, and logging databases</li>
    </ul>
    <p>This policy extends across all jurisdictions where Penthian operates or intends to onboard users, and all digital systems regardless of their physical hosting location.</p>

    <h2>2. Roles & Responsibilities</h2>
    <p>Establishing a secure and compliant IT environment requires clear allocation of responsibilities across technical, operational, and executive functions. This section defines the individuals and entities responsible for managing, overseeing, and securing Penthian’s technology infrastructure.</p>

    <h3>2.1 Chief Technology Officer (CTO)</h3>
    <p>The CTO holds overall responsibility for:</p>
    <ul>
        <li>Design, deployment, and management of Penthian’s IT infrastructure</li>
        <li>Oversight of development teams, technology partners, and integration vendors</li>
        <li>Implementation of this IT Policy and alignment with platform needs</li>
        <li>Ensuring compliance with FSC technology standards and VAITOS obligations</li>
        <li>Reviewing and approving major upgrades, smart contract changes, and architecture shifts</li>
    </ul>
    <p>The CTO reports directly to the Board of Directors and works closely with the Head of Compliance.</p>

    <h3>2.2 Head of Compliance & Risk</h3>
    <p>The Head of Compliance is responsible for:</p>
    <ul>
        <li>Ensuring that all technology practices align with AML/CFT, data privacy, and investor protection requirements</li>
        <li>Coordinating audits and IT risk reviews</li>
        <li>Collaborating with the CTO to embed regulatory controls into the platform’s technical architecture</li>
        <li>Approving new tools or third-party providers that may access sensitive data</li>
    </ul>

    <h3>2.3 DevOps & Engineering Teams</h3>
    <p>Internal or contracted DevOps teams are responsible for:</p>
    <ul>
        <li>Secure development, testing, and deployment of platform code</li>
        <li>Maintaining version control, access management, and release protocols</li>
        <li>Monitoring uptime, performance, and vulnerability alerts</li>
        <li>Ensuring that all smart contracts (ERC-3643, ERC-1155, etc.) follow peer review and audit processes</li>
    </ul>

    <h3>2.4 Cybersecurity & Infrastructure Partners</h3>
    <p>Penthian may contract with external security and hosting providers for:</p>
    <ul>
        <li>Firewall, anti-DDoS, and intrusion prevention</li>
        <li>Routine penetration testing and smart contract audits</li>
        <li>Managed cloud services (e.g., AWS, Azure)</li>
        <li>Business continuity and disaster recovery planning</li>
    </ul>
    <p>These vendors must adhere to strict SLAs and data handling agreements.</p>

    <h3>2.5 Platform Users (Investors, Issuers, Agents)</h3>
    <p>Users must:</p>
    <ul>
        <li>Protect their own private keys and wallet credentials</li>
        <li>Avoid phishing, malware, or unauthorized access attempts</li>
        <li>Report any bugs, system anomalies, or suspicious activity</li>
        <li>Comply with KYC/KYB procedures, including wallet whitelisting under ERC-3643</li>
    </ul>
    <p>Users are provided with guidance and support to meet these obligations.</p>

    <h3>2.6 System Administrators</h3>
    <p>System admins are authorized to:</p>
    <ul>
        <li>Create, modify, and revoke user roles within the backend systems</li>
        <li>Configure data storage, access logs, and analytics dashboards</li>
        <li>Enforce access control policies and monitor real-time alerts</li>
        <li>Coordinate incident response in collaboration with the CTO and Compliance team</li>
    </ul>
    <p>Admins operate under the principle of least privilege and are subject to audit.</p>

    <h3>2.7 Board of Directors</h3>
    <p>The Board holds oversight over all critical technology decisions and must:</p>
    <ul>
        <li>Approve the annual IT strategy and security roadmap</li>
        <li>Review quarterly IT risk assessments and incident reports</li>
        <li>Sign off on any proposed changes to the IT Policy or security model</li>
        <li>Ensure the platform’s infrastructure is fit for licensing, growth, and user protection</li>
    </ul>

    <h2>3. Platform Architecture & Hosting Environment</h2>
    <p>This section outlines how Penthian's digital infrastructure is architected, hosted, and maintained to ensure availability, scalability, security, and regulatory compliance.</p>

    <h3>3.1 Core System Components</h3>
    <p>The Penthian platform consists of the following core components:</p>
    <ul>
        <li><strong>Investor Interface:</strong> Front-end web application and mobile-accessible portal for investors to browse assets, view portfolios, and complete transactions.</li>
        <li><strong>Issuer Dashboard:</strong> Secure environment where property developers, business owners, and asset issuers list offerings, submit documentation, and manage token sales.</li>
        <li><strong>Admin Console:</strong> Backend interface for system administrators, compliance officers, and support teams to monitor activity and enforce permissions.</li>
        <li><strong>Smart Contracts:</strong> ERC-3643- and ERC-1155-compliant smart contracts managing token issuance, transfer rules, identity permissions, revenue distribution, and governance.</li>
        <li><strong>KYC/KYB Integration Layer:</strong> Embedded APIs connecting Penthian with third-party KYC/AML providers (primarily Veriff) to manage user identity, compliance checks, and wallet whitelisting.</li>
        <li><strong>Data & Analytics Layer:</strong> Reporting tools, dashboards, and exportable logs to support compliance, audits, and business intelligence.</li>
    </ul>

    <h3>3.2 Hosting Environment</h3>
    <p>Penthian's infrastructure is hosted in a secure cloud-native environment with the following features:</p>
    <ul>
        <li><strong>Primary cloud partner:</strong> AWS (Amazon Web Services) or equivalent tier-1 provider</li>
        <li><strong>Regions:</strong> Hosting in jurisdictions aligned with Mauritius and international data residency laws</li>
        <li><strong>Redundancy:</strong> Multi-zone and multi-region availability zones to protect against outages</li>
        <li><strong>Encryption:</strong> Full encryption of data at rest and in transit using AES-256 and TLS 1.2+</li>
        <li><strong>Monitoring:</strong> Real-time infrastructure health monitoring and alerting via tools like CloudWatch or Datadog</li>
    </ul>

    <h3>3.3 Smart Contract Hosting</h3>
    <p>Smart contracts are deployed on Ethereum-compatible blockchains, currently:</p>
    <ul>
        <li>Ethereum Mainnet</li>
        <li>Polygon</li>
        <li>Binance Smart Chain</li>
        <li>Additional chains under evaluation for cross-chain deployment</li>
    </ul>
    <p>Each smart contract is:</p>
    <ul>
        <li>Fully open-source and externally auditable</li>
        <li>Tested in sandbox/testnet environments before deployment</li>
        <li>Managed via multi-signature wallets or time-locked governance mechanisms</li>
    </ul>

    <h3>3.4 Token Standards Used</h3>
    <p>Penthian supports multiple token standards based on asset type:</p>
    <ul>
        <li><strong>ERC-3643:</strong> For identity-verified permissioned tokens, especially for securities and jurisdictionally sensitive real estate assets.</li>
        <li><strong>ERC-1155:</strong> For fractional real estate offerings and token bundling (e.g., multiple units).</li>
        <li><strong>ERC-20 (where needed):</strong> For SME ownership tokens or utility integrations.</li>
    </ul>
    <p>All tokens are governed by transfer restrictions, permission registries, and on-chain compliance logic.</p>

    <h3>3.5 Data Storage & Backup</h3>
    <p>Penthian follows a cloud-first, encrypted storage model:</p>
    <ul>
        <li><strong>Databases:</strong> Managed cloud databases (e.g., AWS RDS) with automatic backup and failover</li>
        <li><strong>Off-chain storage:</strong> Secure hosting of documents, identity files, and metadata (e.g., via AWS S3)</li>
        <li><strong>Backup frequency:</strong> Daily incremental backups and weekly full backups stored in geographically separate regions</li>
        <li><strong>Disaster recovery RTO/RPO:</strong> Maximum 4 hours (RTO) and 1 hour (RPO)</li>
    </ul>

    <h3>3.6 Dev/Test/Production Environments</h3>
    <p>The development lifecycle is segmented into:</p>
    <ul>
        <li><strong>Development Environment:</strong> Isolated from production, with dummy/testnet tokens</li>
        <li><strong>Testing/Staging Environment:</strong> Mirrors production environment for QA and audit prep</li>
        <li><strong>Production Environment:</strong> Restricted to verified deployments only; subject to 24/7 monitoring and multi-factor access</li>
    </ul>
    <p>Code must pass through unit tests, static analysis, and peer review before being deployed to production.</p>

    <h2>4. Access Control & Authentication</h2>
    <p>Effective access control is essential to safeguarding the integrity of Penthian’s systems, data, and assets. This section outlines how we manage user and admin access, authenticate identities, and enforce the principle of least privilege.</p>

    <h3>4.1 Access Control Principles</h3>
    <p>Penthian’s access control framework is based on the following principles:</p>
    <ul>
        <li><strong>Role-Based Access Control (RBAC):</strong> Users and staff are granted system access based strictly on their job functions or responsibilities.</li>
        <li><strong>Least Privilege:</strong> No user is given more access than necessary to perform their task.</li>
        <li><strong>Separation of Duties:</strong> Critical operations (e.g., deploying contracts, transferring tokens, issuing refunds) require approval or involvement from multiple roles.</li>
        <li><strong>Auditability:</strong> All access attempts and changes are logged and reviewed periodically.</li>
    </ul>

    <h3>4.2 Authentication Methods</h3>
    <p>Penthian employs multi-factor authentication (MFA) and secure credentials for all system access:</p>
    <table>
        <tr>
            <th>User Type</th>
            <th>Authentication Method</th>
        </tr>
        <tr>
            <td>Platform Admins</td>
            <td>Email + Password + TOTP-based MFA</td>
        </tr>
        <tr>
            <td>Developers</td>
            <td>GitHub/GitLab + SSH Keys + 2FA</td>
        </tr>
        <tr>
            <td>Users (Investors/Issuers)</td>
            <td>Email + Password + Device-Based OTP</td>
        </tr>
        <tr>
            <td>API Access</td>
            <td>Token-based access with expiration/rotation</td>
        </tr>
    </table>

    <h3>4.3 Wallet Whitelisting & ERC-3643 Integration</h3>
    <p>As part of identity compliance:</p>
    <ul>
        <li>Wallets are whitelisted after successful KYC/KYB via Veriff.</li>
        <li>Smart contracts enforce token transfer restrictions based on identity and region.</li>
        <li>Revoked, blacklisted, or non-compliant wallets are automatically disabled.</li>
    </ul>
    <p>This ensures that only verified, permissioned participants can interact with sensitive tokens.</p>

    <h3>4.4 Admin Access Management</h3>
    <p>All administrative access:</p>
    <ul>
        <li>Is granted only by the CTO or Compliance Officer</li>
        <li>Requires signed NDA and policy acknowledgment</li>
        <li>Is provisioned via secure infrastructure tools (e.g., IAM, RBAC systems)</li>
        <li>Includes logging and alerting on every login, role change, or configuration update</li>
    </ul>
    <p>Admin access is reviewed quarterly and revoked immediately upon role termination.</p>

    <h3>4.5 Password & Credential Policies</h3>
    <p>Penthian enforces the following policies:</p>
    <ul>
        <li>Minimum password length: 12 characters</li>
        <li>Passwords must include uppercase, lowercase, numbers, and symbols</li>
        <li>Passwords must be changed every 90 days</li>
        <li>Reuse of the last 5 passwords is not permitted</li>
        <li>All stored passwords are hashed using bcrypt or equivalent</li>
    </ul>
    <p>Staff and users are encouraged to use password managers to store credentials securely.</p>

    <h3>4.6 Session Management</h3>
    <ul>
        <li>User sessions expire after 15 minutes of inactivity</li>
        <li>Admin sessions expire after 5 minutes of inactivity</li>
        <li>Login attempts are limited to 5 failures before temporary lockout</li>
        <li>Session tokens are invalidated upon logout or IP/location change</li>
    </ul>

    <h3>4.7 Access Reviews & Monitoring</h3>
    <ul>
        <li>Quarterly audits of user and admin access</li>
        <li>Daily monitoring of login logs, access attempts, and admin actions</li>
        <li>Automatic alerts for unusual access patterns (e.g., multiple locations, time anomalies)</li>
        <li>Immediate review of any failed authentication spike or brute-force attempt</li>
    </ul>

    <h2>5. Cybersecurity & Threat Management</h2>
    <p>Cybersecurity is critical to maintaining trust, platform integrity, and regulatory compliance. This section details Penthian’s approach to threat detection, prevention, and response across its blockchain-based ecosystem.</p>

    <h3>5.1 Cybersecurity Objectives</h3>
    <p>Penthian’s cybersecurity framework is designed to:</p>
    <ul>
        <li>Protect user assets, identity data, and private keys</li>
        <li>Prevent unauthorized access, data breaches, and denial-of-service (DoS) attacks</li>
        <li>Detect and respond to threats in real-time</li>
        <li>Maintain uptime, performance, and operational continuity</li>
        <li>Align with FSC cybersecurity requirements, VAITOS obligations, and international security standards (e.g., ISO 27001, NIST)</li>
    </ul>

    <h3>5.2 Threat Monitoring Tools</h3>
    <p>Penthian uses a multi-layered monitoring stack including:</p>
    <ul>
        <li>Web Application Firewall (WAF) for filtering malicious HTTP requests</li>
        <li>DDoS Protection Services (e.g., Cloudflare, AWS Shield)</li>
        <li>Intrusion Detection/Prevention Systems (IDS/IPS)</li>
        <li>Real-time Log Analysis for unusual patterns and failed logins</li>
        <li>Smart Contract Monitoring (e.g., block explorer alerts, transaction volume tracking)</li>
        <li>24/7 Infrastructure Alerts via third-party monitoring dashboards (e.g., Datadog, PagerDuty)</li>
    </ul>

    <h3>5.3 Smart Contract Security</h3>
    <p>Smart contracts deployed by Penthian undergo:</p>
    <ul>
        <li>Unit testing and peer review</li>
        <li>Formal verification for critical financial functions</li>
        <li>External third-party audits by blockchain security firms before mainnet deployment</li>
        <li>On-chain monitoring for anomalies (e.g., unexpected token transfers or gas spikes)</li>
    </ul>
    <p>Penthian enforces immutable upgrade logic or time-locked changes to prevent unauthorized updates.</p>

    <h3>5.4 External Penetration Testing</h3>
    <p>At minimum, bi-annual penetration tests are conducted by certified cybersecurity firms. Tests cover:</p>
    <ul>
        <li>Web and mobile application endpoints</li>
        <li>Admin portals and investor dashboards</li>
        <li>Authentication, input validation, and session management</li>
        <li>Backend infrastructure and data storage</li>
        <li>Exposure via APIs, 3rd-party services, and DNS-level risks</li>
    </ul>
    <p>Reports are documented and remediation actions are prioritized.</p>

    <h3>5.5 Incident Response Protocol (IRP)</h3>
    <p>Penthian maintains a documented IRP that includes:</p>
    <ol>
        <li>Threat Detection & Logging</li>
        <li>Triage & Severity Classification</li>
        <li>Incident Response Team (IRT) Notification</li>
        <li>Mitigation and Containment</li>
        <li>Stakeholder Communication</li>
        <li>Root Cause Analysis and Recovery</li>
        <li>Post-Mortem Documentation & Audit Report</li>
    </ol>
    <p>The IRP includes defined SLAs depending on severity (Critical, High, Medium, Low) and must be reviewed quarterly.</p>

    <h3>5.6 Vulnerability Management</h3>
    <ul>
        <li>Weekly scans for known vulnerabilities using automated tools</li>
        <li>Patch management system for operating systems, frameworks, and libraries</li>
        <li>Zero-day escalation process with clear lines of responsibility</li>
        <li>Use of secure coding practices and dependency version locking in development workflows</li>
    </ul>

    <h3>5.7 Staff Security Awareness</h3>
    <p>All staff undergo:</p>
    <ul>
        <li>Annual cybersecurity training</li>
        <li>Simulated phishing exercises</li>
        <li>Secure software development training (for developers)</li>
        <li>Policy refresher sessions on IRP, access control, and password hygiene</li>
    </ul>
    <p>Compliance with security training is logged and enforced.</p>

    <h3>5.8 Blockchain-Specific Threats & Controls</h3>
    <p>Penthian recognizes unique risks in blockchain platforms and mitigates them through:</p>
    <table>
        <tr>
            <th>Threat</th>
            <th>Control</th>
        </tr>
        <tr>
            <td>Smart contract exploits</td>
            <td>External audit, time-locked upgrade contracts</td>
        </tr>
        <tr>
            <td>Private key leaks</td>
            <td>Use of multisig and HSMs for admin wallets</td>
        </tr>
        <tr>
            <td>Sybil attacks</td>
            <td>Whitelisting via Veriff + ERC-3643</td>
        </tr>
        <tr>
            <td>Pump-and-dump schemes</td>
            <td>Platform moderation, asset review process</td>
        </tr>
        <tr>
            <td>Wallet spoofing/phishing</td>
            <td>UX warnings, transaction simulators, URL monitoring</td>
        </tr>
    </table>

    <h2>6. Data Protection & Privacy</h2>
    <p>Protecting user data is a core ethical and regulatory responsibility. This section outlines how Penthian Ltd collects, stores, processes, and safeguards personal and financial data in alignment with the Data Protection Act 2017 (Mauritius), the General Data Protection Regulation (GDPR), and other global standards.</p>

    <h3>6.1 Core Data Protection Principles</h3>
    <p>Penthian adheres to the following foundational principles:</p>
    <ul>
        <li>Lawfulness, fairness, and transparency in data collection and usage</li>
        <li>Purpose limitation: Only collect data necessary for operations and compliance</li>
        <li>Data minimization: Store the least amount of personal data possible</li>
        <li>Accuracy: Keep records updated and correct errors promptly</li>
        <li>Storage limitation: Retain data only as long as needed</li>
        <li>Integrity and confidentiality: Secure all data using encryption and access controls</li>
        <li>Accountability: Maintain audit trails and cooperate with regulators</li>
    </ul>

    <h3>6.2 Data Collection</h3>
    <p>Penthian collects the following categories of data:</p>
    <table>
        <tr>
            <th>Category</th>
            <th>Examples</th>
        </tr>
        <tr>
            <td>Identity</td>
            <td>Full name, date of birth, passport/ID, national ID numbers</td>
        </tr>
        <tr>
            <td>Contact</td>
            <td>Email, phone, residential address</td>
        </tr>
        <tr>
            <td>Financial</td>
            <td>Source of funds, bank account info, transaction history</td>
        </tr>
        <tr>
            <td>Blockchain</td>
            <td>Wallet addresses, token ownership, on-chain activity</td>
        </tr>
        <tr>
            <td>Compliance</td>
            <td>KYC/KYB checks, risk scores, sanctions screening results</td>
        </tr>
        <tr>
            <td>Usage</td>
            <td>IP address, device info, user activity on the platform</td>
        </tr>
    </table>
    <p>All data is collected via secure channels (HTTPS, secure APIs) and with user consent.</p>

    <h3>6.3 KYC/KYB & Onboarding Compliance</h3>
    <ul>
        <li>Penthian integrates Veriff as a KYC/KYB provider.</li>
        <li>Data collected is securely transmitted to and stored by Veriff, which is GDPR- and ISO 27001–certified.</li>
        <li>Users must pass identity checks before transacting or accessing tokenized assets.</li>
        <li>Failed or expired checks result in automatic wallet suspension.</li>
    </ul>
    <p>Veriff provides regular audit logs and data storage compliance summaries.</p>

    <h3>6.4 Data Storage & Encryption</h3>
    <p>All personal data is stored securely using:</p>
    <ul>
        <li>AES-256 encryption at rest</li>
        <li>TLS 1.2+ encryption in transit</li>
        <li>Secure cloud storage (e.g., AWS S3 with bucket-level access control)</li>
        <li>Regular backups and encrypted disaster recovery copies</li>
    </ul>
    <p>Sensitive records (e.g., government IDs) are stored in a separate vault with restricted access.</p>

    <h3>6.5 User Rights</h3>
    <p>Under GDPR and Mauritian law, users may:</p>
    <ul>
        <li>Request access to their personal data</li>
        <li>Request correction or deletion of inaccurate data</li>
        <li>Object to certain data uses (e.g., marketing)</li>
        <li>Request data portability (machine-readable export)</li>
        <li>Lodge complaints with the Data Protection Commissioner</li>
    </ul>
    <p>Penthian responds to requests within 30 days and maintains a request log.</p>

    <h3>6.6 Third-Party Data Sharing</h3>
    <p>Penthian does not sell user data under any circumstances.</p>
    <p>Data may only be shared with:</p>
    <ul>
        <li>KYC/AML providers (e.g., Veriff)</li>
        <li>Regulatory authorities (e.g., FSC, law enforcement)</li>
        <li>Licensed custodians or legal partners (under strict NDA and access limits)</li>
        <li>Hosting or analytics services (pseudonymized and encrypted only)</li>
    </ul>
    <p>All vendors must meet Penthian’s data protection standards and sign DPA agreements.</p>

    <h3>6.7 Breach Detection & Notification</h3>
