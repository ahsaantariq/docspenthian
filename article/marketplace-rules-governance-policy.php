        <p>Penthian reserves the right to disqualify or blacklist any participant who:</p>
        <ul>
            <li>Provides false or misleading information</li>
            <li>Fails KYC/KYB or subsequent reverification</li>
            <li>Attempts to manipulate markets or commit fraud</li>
            <li>Engages in any prohibited activity under this policy or the law</li>
            <li>Is involved in hacking, wash trading, front-running, or market abuse</li>
        </ul>
        <p>A formal process is followed to review and resolve such cases, with appeal rights under dispute procedures.</p>
