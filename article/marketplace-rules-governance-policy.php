        <p><strong>a. General Eligibility for All Users</strong></p>
        <p>To participate in the Penthian Marketplace, users must:</p>
        <ul>
            <li>Be at least 18 years old or of legal age in their jurisdiction</li>
            <li>Successfully complete KYC/KYB verification via Veriff or another approved provider</li>
            <li>Not be a resident or national of any sanctioned country</li>
            <li>Not appear on any global sanctions or watchlists</li>
            <li>Accept and agree to the Marketplace Rules, Terms of Use, and Risk Disclosures</li>
            <li>Maintain a compliant non-custodial wallet or use Penthian’s approved smart wallet service</li>
        </ul>
        <p><strong>b. Specific Requirements for Asset Issuers</strong></p>
        <p>Issuers must additionally:</p>
        <ul>
            <li>Undergo enhanced due diligence and business verification</li>
            <li>Provide asset documentation (title deeds, legal opinions, valuations, operating agreements)</li>
            <li>Commit to using only approved token standards (e.g. ERC-1155 for regulated tokens)</li>
            <li>Agree to performance reporting obligations and investor rights disclosure</li>
            <li>Obtain regulatory approvals, where required, in the asset’s country of origin</li>
        </ul>
        <p><strong>c. Specific Requirements for Institutional Investors</strong></p>
        <p>Institutional or high-net-worth participants must:</p>
        <ul>
            <li>Provide proof of source of funds and legal entity structure</li>
            <li>Undergo KYB and ultimate beneficial ownership (UBO) verification</li>
            <li>Acknowledge their responsibilities under financial regulations (e.g. MiCA, FATF)</li>
            <li>Use authorized wallets capable of supporting transfer restriction and compliance modules</li>
        </ul>
