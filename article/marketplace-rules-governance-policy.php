        <h2>7. Investor Rights & Governance Mechanisms</h2>
        <p>Penthian is designed to promote inclusive and transparent participation in real-world asset ownership. This section outlines the rights of investors who hold asset-backed tokens on the platform, and the governance tools available to influence key decisions concerning tokenized properties, SMEs, or other listed assets.</p>
        <h3>7.1 Investor Rights</h3>
        <p>Token holders on the Penthian platform may receive the following rights, depending on the asset type and token structure:</p>
        <p><strong>a. Economic Rights</strong></p>
        <ul>
            <li>Proportional yield distributions from income-generating assets (e.g. rental returns, profit share)</li>
            <li>Appreciation upside from future sales or liquidity events</li>
            <li>Access to auction proceeds in case of asset liquidation</li>
            <li>Participation in buyback events (where issuers or third parties repurchase tokens)</li>
        </ul>
        <p><strong>b. Governance Rights</strong></p>
        <ul>
            <li>Voting rights on key asset decisions (e.g. property upgrades, lease changes, resale timing)</li>
            <li>Rights to submit or support governance proposals</li>
            <li>Delegated voting for those who prefer not to vote directly</li>
        </ul>
        <p><strong>c. Information Rights</strong></p>
        <ul>
            <li>Access to real-time performance dashboards</li>
            <li>Quarterly reporting from asset issuers or operators</li>
            <li>Notification of material changes (e.g. valuations, tenants, disputes, compliance events)</li>
        </ul>
        <p><strong>d. Redemption Rights</strong></p>
        <ul>
            <li>In cases of asset sale, foreclosure, or token buyback, token holders are entitled to pro-rata redemption of proceeds, subject to legal priority and platform rules</li>
        </ul>
        <h3>7.2 Governance Architecture</h3>
        <p>Penthian enables decentralized governance features built into its token and platform logic:</p>
        <table>
            <tr>
                <th>Mechanism</th>
                <th>Description</th>
            </tr>
            <tr>
                <td>On-Chain Voting</td>
                <td>Token holders cast votes using ERC-3643 or ERC-1155 tokens tied to identity-verified wallets</td>
            </tr>
            <tr>
                <td>Proposal System</td>
                <td>Token holders can submit proposals (e.g., change property manager, initiate asset resale) via a structured process</td>
            </tr>
            <tr>
                <td>Delegation</td>
