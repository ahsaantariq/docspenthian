        <h2>6. Smart Contract Standards & Compliance</h2>
        <p>Penthian’s tokenized assets operate using Ethereum-based smart contract standards to ensure compliance, interoperability, and investor safety. This section defines the technical requirements for tokenization and compliance controls embedded within the platform’s smart contracts.</p>
        <h3>6.1 Approved Token Standards</h3>
        <p>Penthian supports the following Ethereum token standards for asset tokenization:</p>
        <ul>
            <li><strong>ERC-20:</strong> For compliance-controlled real estate or regulated markets with fungible tokens.</li>
            <li><strong>ERC-721:</strong> For unique, non-fungible assets such as individual properties or collectibles.</li>
            <li><strong>ERC-1155:</strong> For multi-use or fractional tokens representing SMEs or mixed-asset portfolios.</li>
            <li><strong>ERC-3643:</strong> For identity-linked compliance tokens that enforce KYC/KYB and jurisdictional restrictions.</li>
        </ul>
        <p>All tokens must be audited by a Penthian-approved third-party auditor before deployment.</p>
        <h3>6.2 Compliance Features</h3>
        <p>Smart contracts on Penthian include built-in compliance features:</p>
        <ul>
            <li><strong>KYC/AML Integration:</strong> Only wallets with valid ERC-3643 identity tokens can interact with asset-backed tokens.</li>
            <li><strong>Transfer Restrictions:</strong> Tokens can only be transferred to KYC-verified wallets in non-restricted jurisdictions.</li>
            <li><strong>Investor Caps:</strong> Contracts enforce limits on the number of investors or token holdings per wallet, as required by law.</li>
            <li><strong>Freeze Mechanisms:</strong> Admin or multi-sig wallets can pause token activity in case of regulatory or security issues.</li>
            <li><strong>Revocation Logic:</strong> Ability to invalidate tokens or revoke access for non-compliant users.</li>
        </ul>
        <h3>6.3 Audit & Security Requirements</h3>
        <p>All smart contracts must:</p>
        <ul>
            <li>Undergo a full security audit by a reputable third-party firm</li>
            <li>Be tested for vulnerabilities (e.g., reentrancy, overflow, unauthorized access)</li>
            <li>Include emergency pause and upgrade mechanisms (with multi-sig governance)</li>
            <li>Be deployed on a Penthian-approved blockchain (e.g., Ethereum, Polygon)</li>
        </ul>
        <p>Audit reports are made available to investors upon request.</p>
        <h3>6.4 On-Chain & Off-Chain Compliance</h3>
        <p>Penthian maintains compliance through a combination of:</p>
        <ul>
            <li><strong>On-Chain:</strong> Smart contract logic for identity, transfer restrictions, and governance.</li>
            <li><strong>Off-Chain:</strong> KYC/KYB verification via Veriff, legal reviews, and regulatory reporting.</li>
        </ul>
        <p>This hybrid approach ensures compliance with both blockchain and traditional financial regulations.</p>
    </div>

