        <h2>4. Primary Offering Rules (Initial Token Sale)</h2>
        <p>The primary offering (also referred to as the Initial Token Sale or ITS) is the first opportunity for investors to acquire fractional ownership in a newly listed asset on the Penthian Marketplace. This section outlines the standards and rules governing how such offerings are structured, promoted, and executed to ensure compliance, fairness, and investor protection.</p>
        <h3>4.1 Definition</h3>
        <p>A Primary Offering is a fixed-period sale of asset-backed tokens issued directly by the asset owner or project sponsor (Issuer) to investors via the Penthian platform. These tokens represent ownership or economic rights in the underlying real-world asset.</p>
        <h3>4.2 Offer Types</h3>
        <p>Penthian supports several offering models, including:</p>
        <ul>
            <li><strong>Fixed-Price Offerings:</strong> Tokens are sold at a predetermined price until the offering closes or reaches full subscription.</li>
            <li><strong>Time-Bound Offerings:</strong> Investors have a set number of days to subscribe, regardless of how many tokens are sold.</li>
            <li><strong>Target Raise Offerings:</strong> A funding threshold must be met (e.g. soft cap / hard cap) for the offering to be successful.</li>
            <li><strong>Auction-Based Offerings:</strong> Where investor demand sets token price through a bidding mechanism.</li>
        </ul>
        <p>Each offering model must be pre-approved by Penthian’s Listings and Compliance teams.</p>
        <h3>4.3 Offering Requirements</h3>
        <p>Each Initial Token Sale must meet the following criteria:</p>
        <p><strong>a. Clear Offer Documentation</strong></p>
        <ul>
            <li>Investor Offer Summary (token price, number of tokens, minimum contribution)</li>
            <li>Risk Disclosure Statement</li>
            <li>Token Terms and Conditions</li>
            <li>Expected yield/ROI disclosure (if applicable)</li>
            <li>Exit strategy (e.g. buyback clause, resale liquidity option)</li>
        </ul>
        <p><strong>b. Duration and Limits</strong></p>
        <ul>
            <li>Maximum offer duration: 90 days</li>
            <li>Minimum and maximum investment limits may be imposed per investor (based on jurisdiction and KYC level)</li>
            <li>Token purchase lockup period, if any, must be clearly disclosed</li>
        </ul>
        <p><strong>c. Wallet & Smart Contract Setup</strong></p>
        <ul>
            <li>Tokens must be deployed from a whitelisted, verified issuer wallet</li>
            <li>Investor wallets must be KYC-verified and linked to a Veriff-verified profile</li>
            <li>Smart contract must restrict token transfer until offering period is closed</li>
        </ul>
        <h3>4.4 Marketing & Promotion Guidelines</h3>
        <p>Issuers must adhere to strict guidelines:</p>
        <ul>
            <li>No misleading or guaranteed-return statements</li>
            <li>All marketing content must be pre-approved by Penthian</li>
            <li>Jurisdictional restrictions must be respected in advertising (e.g. MiCA, ASIC, SEC restrictions)</li>
            <li>Promotions must include disclaimers and clearly indicate that token purchases carry risk</li>
        </ul>
        <h3>4.4 Oversubscription & Allocation</h3>
        <p>In the case of oversubscription:</p>
        <ul>
            <li>Allocation is made on a first-come, first-served basis or</li>
            <li>A pro-rata distribution is used based on the amount of funds committed</li>
            <li>Excess funds are returned automatically via smart contract or manually (if in fiat)</li>
        </ul>
        <h3>4.5 Completion & Settlement</h3>
        <p>Once the offering ends:</p>
        <ul>
            <li>Tokens are released to investor wallets</li>
            <li>Smart contract functions unlock secondary trading (if applicable)</li>
            <li>Issuer receives funds (minus platform fees, held in escrow if required)</li>
            <li>A post-offering update is sent to all investors confirming allocation and next steps</li>
        </ul>
    </div>

