        <h2>5. Secondary Market Rules</h2>
        <p>Penthian’s secondary market allows investors to buy and sell previously issued asset-backed tokens directly with each other. This creates liquidity for fractional ownership and enhances price discovery. To ensure transparency, fairness, and regulatory alignment, all secondary trading is governed by the rules below.</p>
        <h3>5.1 Definition</h3>
        <p>The Secondary Market is a permissioned peer-to-peer exchange environment within the Penthian platform, where token holders may:</p>
        <ul>
            <li>List their asset-backed tokens for sale</li>
            <li>Bid on existing token listings</li>
            <li>Participate in auctions (if enabled)</li>
            <li>Transfer tokens to other eligible, KYC-verified users</li>
        </ul>
        <p>Secondary trading is enabled only after the lock-up period of the primary offering has ended and smart contracts are activated for resale.</p>
        <h3>5.2 Trading Eligibility</h3>
        <p>To trade on the secondary market, users must:</p>
        <ul>
            <li>Be KYC-verified through Veriff or another approved provider</li>
            <li>Not reside in restricted jurisdictions</li>
            <li>Have passed ongoing compliance checks (e.g. sanctions, AML)</li>
            <li>Use a wallet with a valid ERC-3643 identity token</li>
            <li>Comply with any lock-up periods or transfer restrictions embedded in the token smart contract</li>
        </ul>
        <h3>5.3 Trading Mechanisms</h3>
        <p>Penthian supports the following secondary market trading mechanisms:</p>
        <ul>
            <li><strong>Fixed-Price Listings:</strong> Sellers list tokens at a set price.</li>
            <li><strong>Auction Listings:</strong> Tokens are sold to the highest bidder within a specified timeframe.</li>
            <li><strong>Swap Proposals:</strong> Peer-to-peer token exchanges, subject to compliance checks.</li>
        </ul>
        <p>All trades are executed via smart contracts, with transfer restrictions enforced to ensure only verified wallets participate.</p>
        <h3>5.4 Fees & Costs</h3>
        <p>Secondary market trading incurs:</p>
        <ul>
            <li>Platform fees (as disclosed in the fee schedule)</li>
            <li>Gas fees for on-chain transactions</li>
            <li>Any applicable taxes or regulatory levies, depending on jurisdiction</li>
        </ul>
        <p>Fee schedules are published on the platform and updated as needed.</p>
        <h3>5.5 Prohibited Activities</h3>
        <p>The following are strictly prohibited in the secondary market:</p>
        <ul>
            <li>Wash trading (artificially inflating trading volume)</li>
            <li>Front-running (exploiting advance knowledge of trades)</li>
            <li>Market manipulation (e.g., pump-and-dump schemes)</li>
            <li>Trading by non-verified or blacklisted users</li>
            <li>Attempts to bypass smart contract restrictions</li>
        </ul>
        <p>Violations may lead to token freezes, account suspension, or legal action.</p>
        <h3>5.6 Trading Suspension</h3>
        <p>Penthian may suspend secondary trading for an asset if:</p>
        <ul>
            <li>Regulatory issues are detected (e.g., non-compliance with VAITOS Act)</li>
            <li>Fraud or suspicious activity is identified</li>
            <li>Asset performance raises material concerns (e.g., insolvency, legal disputes)</li>
            <li>Technical issues affect platform integrity (e.g., smart contract vulnerabilities)</li>
        </ul>
        <p>Suspensions are communicated to all token holders with a clear rationale and estimated resolution timeline.</p>
    </div>

