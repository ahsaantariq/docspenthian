  <h2>For Class S Virtual Asset Marketplace License (Mauritius FSC)</h2>

    <div class="section">
        <h2>1. Purpose & Scope</h2>
        <h3>1.1 Purpose</h3>
        <p>The purpose of this Marketplace Rules and Governance Policy is to define the operational, compliance, and governance framework under which Penthian Ltd’s tokenized asset marketplace operates. It establishes the legal, technical, and procedural standards applicable to all users, issuers, asset managers, and investors engaging on the Penthian platform.</p>
        <p>This document ensures that the marketplace operates in a manner that is:</p>
        <ul>
            <li>Fair, transparent, and non-discriminatory</li>
            <li>Fully compliant with Mauritius’ Virtual Asset and Initial Token Offering Services Act 2021 (VAITOS Act)</li>
            <li>Technologically aligned with smart contract standards (e.g. ERC-721, ERC-1155)</li>
            <li>Secure, enforceable, and capable of protecting all stakeholders across the primary and secondary markets</li>
        </ul>
        <p>It also governs how users interact with asset-backed tokens, how fractional ownership is established, and how governance, voting, and dispute resolution mechanisms function on-chain and off-chain.</p>
        <h3>1.2 Scope</h3>
        <p>This policy applies to the full lifecycle of assets and participants on the Penthian platform, including:</p>
        <ul>
            <li><strong>Asset Issuers</strong> – property developers, SMEs, business owners listing real-world or digital assets</li>
            <li><strong>Investors</strong> – individuals or institutions purchasing tokenized fractions of assets</li>
            <li><strong>Marketplace Operators</strong> – Penthian Ltd and its appointed agents</li>
            <li><strong>Technology Providers</strong> – smart contract developers, custodians, and external service integrators</li>
            <li><strong>Compliance Entities</strong> – KYC/AML providers (e.g. Veriff), legal and audit agents</li>
        </ul>
        <p>It covers:</p>
        <ul>
            <li>The listing and delisting of tokenized assets</li>
            <li>Primary offerings and fractional sales</li>
            <li>Secondary trading (peer-to-peer, auctions, swaps)</li>
            <li>Token standards and transfer restrictions</li>
            <li>Governance rights, voting, and smart contract execution</li>
            <li>Market suspension or enforcement events</li>
            <li>Dispute handling, asset redemption, and liquidation procedures</li>
        </ul>
        <p>This document is binding on all users of the Penthian platform and may be updated as required by law, technological evolution, or regulatory guidance.</p>
