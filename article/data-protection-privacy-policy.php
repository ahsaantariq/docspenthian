    <h2>1. Introduction & Purpose</h2>
    <p>This Data Protection & Privacy Policy sets out the principles and procedures followed by Penthian Ltd to protect the personal data of its users, clients, partners, employees, and agents.</p>
    <p>Penthian is committed to protecting individual privacy and ensuring that all data collected is processed lawfully, fairly, and transparently. This policy is designed to comply with:</p>
    <ul>
        <li>Mauritius Data Protection Act 2017</li>
        <li>General Data Protection Regulation (GDPR) for EU users</li>
        <li>Best practices as outlined by the Financial Services Commission (FSC)</li>
    </ul>
    <p>This policy applies to all personal data held by Penthian Ltd, whether collected online via its platform or offline through internal business operations.</p>

    <h2>2. Legal Basis for Data Processing</h2>
    <p>Penthian Ltd processes personal data only where it has a lawful basis to do so. The following legal bases are used depending on the nature and purpose of the data:</p>

    <h3>2.1 Consent</h3>
    <ul>
        <li>Clients provide explicit consent when they create an account, complete KYC/KYB checks, or agree to marketing communications</li>
        <li>Consent is always freely given, specific, informed, and unambiguous</li>
        <li>Users may withdraw consent at any time via their account settings or by contacting Penthian’s Data Protection Officer (DPO)</li>
    </ul>

    <h3>2.2 Contractual Necessity</h3>
    <p>Data is processed to fulfill contractual obligations such as:</p>
    <ul>
        <li>Opening accounts</li>
        <li>Verifying identity</li>
        <li>Executing smart contract transactions</li>
        <li>Facilitating investor onboarding and payouts</li>
    </ul>

    <h3>2.3 Legal & Regulatory Obligation</h3>
    <p>Penthian processes data to meet its obligations under:</p>
    <ul>
        <li>AML/CFT laws and reporting to the FIU</li>
        <li>Tax compliance</li>
        <li>FSC license conditions, including record-keeping and audits</li>
    </ul>

    <h3>2.4 Legitimate Interest</h3>
    <p>Penthian may process data to:</p>
    <ul>
        <li>Improve platform performance and security</li>
        <li>Prevent fraud and abuse</li>
        <li>Communicate with clients about updates or system changes</li>
    </ul>
    <p>This is done only where Penthian’s interests do not override the user’s fundamental rights and freedoms</p>

    <h3>2.5 Vital Interests & Public Tasks</h3>
    <p>In rare cases, data may be processed to:</p>
    <ul>
        <li>Protect the vital interests of a user or third party (e.g., preventing financial crime)</li>
        <li>Carry out a task in the public interest or for compliance with national authorities</li>
    </ul>

    <h2>3. Categories of Data Collected</h2>
    <p>Penthian Ltd collects and processes personal data that is necessary to deliver its services, comply with regulations, and maintain a secure and efficient platform. Data is collected both directly from users and through automated systems.</p>

    <h3>3.1 Identity & Contact Information</h3>
    <p>Collected during onboarding, KYC/KYB, and user registration:</p>
    <ul>
        <li>Full name</li>
        <li>Date of birth</li>
        <li>Nationality</li>
        <li>Residential and business address</li>
        <li>Email address and phone number</li>
        <li>Identification documents (e.g., passport, national ID, driver’s license)</li>
        <li>Proof of address (e.g., utility bill, bank statement)</li>
    </ul>

    <h3>3.2 Financial & Wallet Information</h3>
    <p>Collected during account creation, asset purchase, and investment activity:</p>
    <ul>
        <li>Bank account details (for fiat settlements)</li>
        <li>Wallet addresses (crypto public keys)</li>
        <li>Proof of source of funds/wealth</li>
        <li>Payment history and transaction logs</li>
        <li>Investment holdings, earnings, and distributions</li>
    </ul>

    <h3>3.3 AML/CFT & Risk Data</h3>
    <p>Collected via Veriff and internal compliance systems:</p>
    <ul>
        <li>Risk rating and investor classification</li>
        <li>Enhanced due diligence (EDD) files</li>
        <li>Sanctions screening results</li>
        <li>Suspicious transaction reports (STRs)</li>
        <li>Monitoring alerts and case logs</li>
    </ul>

    <h3>3.4 Usage & Technical Data</h3>
    <p>Collected via cookies, web analytics, and platform logs:</p>
    <ul>
        <li>IP address</li>
        <li>Device information</li>
        <li>Login timestamps and geolocation</li>
        <li>Platform usage metrics (e.g., session duration, clicks, downloads)</li>
        <li>Browser and operating system type</li>
    </ul>

    <h3>3.5 Communications & Preferences</h3>
    <p>Collected through user interaction with Penthian services:</p>
    <ul>
        <li>Support inquiries</li>
        <li>Feedback and surveys</li>
        <li>Marketing preferences and subscription settings</li>
        <li>Investor voting records or participation in governance proposals</li>
    </ul>

    <h2>4. Data Collection Methods</h2>
    <p>Penthian Ltd collects personal data through both direct and automated channels, ensuring full transparency and lawful processing at each stage of interaction with the user or business client.</p>

    <h3>4.1 Direct Collection</h3>
    <p>Penthian collects data directly from users through:</p>
    <ul>
        <li>Account registration forms</li>
        <li>KYC/KYB verification via Veriff</li>
        <li>Customer onboarding workflows</li>
        <li>Asset listing and investor documentation submissions</li>
        <li>Support inquiries and helpdesk communications</li>
    </ul>
    <p>All data collected directly is subject to the user’s review and agreement at the time of submission.</p>

    <h3>4.2 Third-Party Services</h3>
    <ul>
        <li><strong>Veriff:</strong> Used to perform identity verification, sanctions screening, and AML checks. All data handled by Veriff is processed in compliance with GDPR and relevant data protection laws.</li>
        <li><strong>Payment processors and banking partners:</strong> May transmit limited financial data to confirm transactions or funding.</li>
        <li><strong>Analytics providers:</strong> Tools like Google Analytics may collect usage and device information, anonymized and aggregated.</li>
    </ul>

    <h3>4.3 Automated Collection</h3>
    <p>Penthian automatically collects technical and behavioral data through:</p>
    <ul>
        <li>Cookies and browser storage</li>
        <li>Blockchain transaction monitoring (on-chain behavior tracking)</li>
        <li>Server and infrastructure logs</li>
        <li>Security tools that flag anomalies or suspicious behavior</li>
    </ul>
    <p>Users are informed of cookies and tracking upon first platform entry and can manage preferences in settings.</p>

    <h3>4.4 Internal System Tracking</h3>
    <ul>
        <li>Internal logs capture admin actions, audit trails, and change history</li>
        <li>System events are tracked to monitor operational health, suspicious login attempts, and investor activity</li>
    </ul>
    <p>All logs are stored securely and retained in accordance with the Record-Keeping Policy.</p>

    <h2>5. Data Usage & Purpose</h2>
    <p>Penthian Ltd collects and processes personal data only for specified, lawful, and legitimate purposes. The data is not used in ways that are incompatible with these original purposes.</p>

    <h3>5.1 Core Operational Use</h3>
    <p>Personal data is processed to:</p>
    <ul>
        <li>Register and manage user accounts</li>
        <li>Conduct KYC/KYB and AML/CFT checks (via Veriff)</li>
        <li>Enable users to buy, sell, or hold tokenized assets</li>
        <li>Record and display ownership on-chain</li>
        <li>Facilitate governance actions such as voting or asset decisions</li>
        <li>Execute smart contracts for payouts and distributions</li>
        <li>Communicate with users regarding account activity, system changes, or legal updates</li>
    </ul>

    <h3>5.2 Legal & Compliance Use</h3>
    <p>Data is used to meet obligations under:</p>
    <ul>
        <li>FSC regulatory requirements</li>
        <li>Mauritius AML/CFT Act</li>
        <li>Data Protection Act 2017</li>
        <li>Tax reporting obligations</li>
        <li>Suspicious transaction reporting to the FIU</li>
    </ul>

    <h3>5.3 Security & Fraud Prevention</h3>
    <p>Personal and technical data is analyzed to:</p>
    <ul>
        <li>Detect and prevent fraud, identity theft, or unauthorized account access</li>
        <li>Monitor and investigate suspicious or unusual transactions</li>
        <li>Enforce sanctions screening and blacklist alerts</li>
        <li>Ensure safe and compliant use of the Penthian platform</li>
    </ul>

    <h3>5.4 Service Improvement</h3>
    <p>Anonymized and aggregated data may be used to:</p>
    <ul>
        <li>Improve platform UX, performance, and design</li>
        <li>Optimize investment offerings and customer journeys</li>
        <li>Evaluate investor behavior to inform asset structuring</li>
        <li>Conduct platform audits and system testing</li>
    </ul>

    <h3>5.5 Communication & Engagement</h3>
    <p>With user consent or legitimate interest, Penthian may:</p>
    <ul>
        <li>Send updates, newsletters, or product releases</li>
        <li>Invite users to surveys or governance discussions</li>
        <li>Notify stakeholders of new investment opportunities or market updates</li>
    </ul>
    <p>Users can unsubscribe or manage preferences via their account dashboard.</p>

    <h2>6. Data Sharing & Transfers</h2>
    <p>Penthian Ltd shares personal data only with trusted third parties, affiliates, or regulatory authorities when there is a lawful basis for doing so. All data transfers are governed by strict confidentiality, security, and legal requirements.</p>

    <h3>6.1 Third-Party Service Providers</h3>
    <p>Penthian engages third parties for operational, legal, and technical support, including:</p>
    <ul>
        <li>Veriff for KYC/AML verification</li>
        <li>Cloud service providers (e.g. AWS, DigitalOcean) for secure data hosting</li>
        <li>Legal and compliance consultants</li>
        <li>Blockchain analytics tools for risk and transaction monitoring</li>
        <li>Payment processors for fiat and crypto settlements</li>
    </ul>
    <p>All third-party providers are bound by Data Processing Agreements (DPAs) and confidentiality clauses in line with GDPR and Mauritius data laws.</p>

    <h3>6.2 Regulatory & Legal Disclosure</h3>
    <p>Data may be disclosed to:</p>
    <ul>
        <li>The Financial Services Commission (FSC)</li>
        <li>The Financial Intelligence Unit (FIU)</li>
        <li>Courts, law enforcement, or tax authorities under valid legal obligation</li>
        <li>Other supervisory or regulatory bodies as required by jurisdiction</li>
    </ul>
    <p>Disclosures are made only after legal review and under strict access controls.</p>

    <h3>6.3 Cross-Border Data Transfers</h3>
    <p>In cases where data must be transferred outside of Mauritius (e.g., cloud services or international compliance tools), Penthian ensures:</p>
    <ul>
        <li>The receiving country has adequate data protection laws, or</li>
        <li>Appropriate safeguards are in place such as:</li>
        <li>Standard Contractual Clauses (SCCs)</li>
        <li>Binding Corporate Rules (BCRs)</li>
        <li>Explicit user consent where applicable</li>
    </ul>

    <h3>6.4 No Unauthorized Sales</h3>
    <p>Penthian does not sell, lease, or trade personal data to advertisers, brokers, or unrelated third parties. Any form of monetization of user data is expressly prohibited by company policy.</p>

    <h2>7. Data Retention & Deletion</h2>
    <p>Penthian Ltd retains personal data only for as long as necessary to fulfill the purposes for which it was collected and to comply with legal, regulatory, and operational obligations.</p>

    <h3>7.1 Retention Periods</h3>
    <p>Retention periods vary depending on the category of data and the applicable legal or business requirement:</p>
    <ul>
        <li>KYC/AML records: Minimum of 7 years from the end of the business relationship, in compliance with AML/CFT laws</li>
        <li>Transaction and ownership data: Retained permanently on-chain for traceability and auditability</li>
        <li>User communications and support logs: Retained for 2 years from the date of last interaction</li>
        <li>Marketing preferences and consent records: Retained until consent is withdrawn</li>
        <li>Platform logs and analytics: Retained for 12–24 months depending on relevance</li>
    </ul>

    <h3>7.2 Blockchain Considerations</h3>
    <ul>
        <li>On-chain data, including wallet addresses and token ownership, is immutable by design</li>
        <li>While deletion from the blockchain is not possible, Penthian ensures:</li>
        <li>Off-chain identifiers are pseudonymized or anonymized</li>
        <li>Linking metadata is stored securely and can be deleted or restricted</li>
    </ul>

    <h3>7.3 User-Initiated Deletion</h3>
    <p>Users may request:</p>
    <ul>
        <li>Account deactivation or deletion</li>
        <li>Removal of off-chain personal data (subject to legal hold or retention obligations)</li>
        <li>Revocation of marketing consent</li>
    </ul>
    <p>Requests are processed within 30 days, unless an exception applies.</p>

    <h3>7.4 Legal Holds & Exceptions</h3>
    <p>Data may be retained longer than standard if:</p>
    <ul>
        <li>Required for ongoing investigations, audits, or litigation</li>
        <li>Necessary to comply with FIU or regulatory instructions</li>
        <li>Preservation is required under data protection or financial regulations</li>
    </ul>

    <h3>7.5 Secure Disposal</h3>
    <ul>
        <li>Physical documents are shredded and destroyed securely</li>
        <li>Digital files are deleted from live systems and backups using certified data destruction protocols</li>
    </ul>

    <h2>8. Data Security Measures</h2>
    <p>Penthian Ltd implements robust technical, organizational, and procedural safeguards to protect personal data from unauthorized access, disclosure, alteration, or destruction.</p>

    <h3>8.1 Encryption & Secure Storage</h3>
    <ul>
        <li>All personal data in transit and at rest is encrypted using industry-standard protocols (e.g., AES-256, TLS 1.2+)</li>
        <li>Sensitive KYC/KYB documents are stored in secure, access-controlled environments</li>
        <li>Blockchain data is cryptographically secured and hashed immutably</li>
    </ul>

    <h3>8.2 Access Controls & Authentication</h3>
    <ul>
        <li>Access to personal data is restricted to authorized personnel only</li>
        <li>Role-based access is enforced across systems, with multi-factor authentication (MFA) for admin access</li>
        <li>Logging and audit trails are maintained to track user actions and system changes</li>
    </ul>

    <h3>8.3 Vendor & Third-Party Oversight</h3>
    <ul>
        <li>All third-party processors are assessed for data protection compliance</li>
        <li>Data Processing Agreements (DPAs) are in place with vendors</li>
        <li>Regular audits and due diligence are conducted on service providers (e.g., Veriff, hosting platforms)</li>
    </ul>

    <h3>8.4 Monitoring & Incident Response</h3>
    <ul>
        <li>Real-time monitoring tools are in place to detect unauthorized access, unusual activity, or potential data breaches</li>
        <li>An Incident Response Plan is activated within 24 hours in the event of a breach</li>
        <li>Users and regulators are notified in line with legal timelines (e.g., within 72 hours under GDPR)</li>
    </ul>

    <h3>8.5 Employee Awareness & Training</h3>
    <ul>
        <li>All staff undergo mandatory data protection training during onboarding and annually thereafter</li>
        <li>Employees are trained on safe handling of personal data, phishing risks, and internal reporting procedures</li>
        <li>Confidentiality clauses are embedded in employment and contractor agreements</li>
    </ul>

    <h3>8.6 Physical Security</h3>
    <ul>
        <li>Office environments and server rooms (if applicable) are protected by:</li>
        <li>Access badges and biometric restrictions</li>
        <li>CCTV monitoring</li>
        <li>Visitor logging and authorization procedures</li>
    </ul>

    <h2>9. User Rights</h2>
    <p>Penthian Ltd respects the rights of individuals regarding their personal data and ensures users can exercise those rights in compliance with the Mauritius Data Protection Act 2017, GDPR, and other applicable regulations.</p>

    <h3>9.1 Right to Access</h3>
    <p>Users have the right to:</p>
    <ul>
        <li>Request confirmation of whether Penthian processes their personal data</li>
        <li>Receive a copy of their data held by the company</li>
        <li>Obtain details about the purpose of processing, data categories, and data recipients</li>
    </ul>

    <h3>9.2 Right to Rectification</h3>
    <p>Users can request correction of:</p>
    <ul>
        <li>Inaccurate or incomplete personal information</li>
        <li>Outdated or incorrectly recorded data</li>
    </ul>
    <p>Penthian must respond promptly and complete rectifications without undue delay.</p>

    <h3>9.3 Right to Erasure ("Right to be Forgotten")</h3>
    <p>Users may request deletion of their data when:</p>
    <ul>
        <li>The data is no longer necessary for the purposes collected</li>
        <li>Consent is withdrawn and no other legal basis exists</li>
        <li>Data was unlawfully processed</li>
        <li>Erasure is required by law</li>
    </ul>
    <p>Note: Blockchain-stored records cannot be erased, but off-chain identifiers will be removed wherever legally allowed.</p>

    <h3>9.4 Right to Restriction of Processing</h3>
    <p>Users may request to restrict processing in cases where:</p>
    <ul>
        <li>The data accuracy is contested</li>
        <li>Processing is unlawful but the user prefers restriction over deletion</li>
        <li>The data is no longer needed by Penthian but required by the user for legal claims</li>
        <li>The user has objected to processing, pending verification</li>
    </ul>

    <h3>9.5 Right to Data Portability</h3>
    <p>Users may request a copy of their personal data in a structured, commonly used, and machine-readable format. Where feasible, data can be transmitted directly to another controller at the user’s request.</p>

    <h3>9.6 Right to Object</h3>
    <p>Users may object to:</p>
    <ul>
        <li>Direct marketing</li>
        <li>Processing based on legitimate interests, unless Penthian demonstrates compelling legal grounds</li>
    </ul>

    <h3>9.7 Right to Withdraw Consent</h3>
    <p>If processing is based on consent, users may withdraw it at any time without affecting the lawfulness of prior processing.</p>

    <h3>9.8 Right to Lodge a Complaint</h3>
    <p>Users have the right to file a complaint with:</p>
    <ul>
        <li>The Data Protection Office of Mauritius, or</li>
        <li>Another relevant data protection authority if based outside Mauritius</li>
    </ul>

    <h2>10. Cookies & Tracking Technologies</h2>
    <p>Penthian Ltd uses cookies and similar tracking technologies to enhance user experience, improve platform performance, and ensure security. Users are informed of cookie use upon visiting the site and can manage their preferences at any time.</p>

    <h3>10.1 What Are Cookies?</h3>
    <p>Cookies are small text files stored on a user’s browser or device when visiting a website. They help the site remember information such as:</p>
    <ul>
        <li>Login sessions</li>
        <li>Language preferences</li>
        <li>User activity (e.g., pages visited, items viewed)</li>
        <li>Security tokens and session IDs</li>
    </ul>

    <h3>10.2 Types of Cookies Used</h3>
    <p>Penthian uses the following categories of cookies:</p>
    <h4>a. Strictly Necessary Cookies</h4>
    <ul>
        <li>Essential for platform functionality and security</li>
        <li>Enable login, wallet access, and form submissions</li>
        <li>Cannot be disabled</li>
    </ul>
    <h4>b. Performance & Analytics Cookies</h4>
    <ul>
        <li>Help analyze user behavior and site performance</li>
        <li>Use tools like Google Analytics (anonymized and aggregated data)</li>
        <li>Require user consent</li>
    </ul>
    <h4>c. Functional Cookies</h4>
    <ul>
        <li>Remember user settings and preferences</li>
        <li>Enhance experience but not essential to core functionality</li>
    </ul>
    <h4>d. Marketing & Retargeting Cookies</h4>
    <ul>
        <li>Track user behavior across sites to personalize offers or advertisements</li>
        <li>Only used with explicit consent</li>
    </ul>

    <h3>10.3 Third-Party Cookies</h3>
    <ul>
        <li>Penthian may allow trusted third parties (e.g., analytics providers or partners) to place cookies</li>
        <li>All third-party cookies comply with applicable data protection laws</li>
    </ul>

    <h3>10.4 User Consent & Control</h3>
    <ul>
        <li>Users are prompted to accept or reject non-essential cookies upon first visit</li>
        <li>Preferences can be updated via the cookie banner or browser settings</li>
        <li>Disabling some cookies may affect site functionality</li>
    </ul>

    <h3>10.5 Browser & Device Settings</h3>
    <p>Users can:</p>
    <ul>
        <li>Block or delete cookies through their browser settings</li>
        <li>Use “Do Not Track” signals, although Penthian may not honor all of them</li>
        <li>Control mobile tracking through app or OS-level permissions</li>
    </ul>

    <h2>11. Children’s Privacy</h2>
    <p>Penthian Ltd’s services are not intended for use by individuals under the age of 18. The platform does not knowingly collect, process, or store personal data of minors.</p>

    <h3>11.1 Age Restriction</h3>
    <ul>
        <li>All users must be at least 18 years old to create an account, invest in assets, or participate in any activity on the Penthian platform.</li>
        <li>This age requirement is enforced through identity verification and KYC procedures conducted via Veriff.</li>
    </ul>

    <h3>11.2 Parental Consent</h3>
    <ul>
        <li>If Penthian becomes aware that personal data has been collected from a person under 18 without verified parental or guardian consent, such data will be promptly deleted.</li>
        <li>Parents or guardians may contact the Data Protection Officer to request removal of any inadvertently collected information.</li>
    </ul>

    <h3>11.3 Platform Design Considerations</h3>
    <ul>
        <li>The platform design, marketing, and educational content are tailored for adults and professional investors.</li>
        <li>No services, features, or gamified tools are developed to target children.</li>
    </ul>

    <h3>11.4 Legal Compliance</h3>
    <p>This approach aligns with:</p>
    <ul>
        <li>The Mauritius Data Protection Act 2017</li>
        <li>Global best practices on child data protection (e.g., GDPR Article 8)</li>
        <li>Responsible financial conduct standards enforced by the FSC</li>
    </ul>

    <h2>12. Changes to This Policy</h2>
    <p>Penthian Ltd reserves the right to modify or update this Data Protection & Privacy Policy at any time to reflect changes in legal requirements, operational practices, or technology.</p>

    <h3>12.1 Notification of Changes</h3>
    <ul>
        <li>Material changes will be communicated to users via:</li>
        <li>Email notifications</li>
