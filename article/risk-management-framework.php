        </tr>
        <tr>
            <td>Chief Executive Officer (CEO)</td>
            <td>Sets tone from the top. Ensures organizational alignment to the Risk Framework and integration across departments.</td>
        </tr>
        <tr>
            <td>Chief Technology Officer (CTO)</td>
            <td>Manages IT/cyber risks, smart contract audits, system architecture, blockchain security, and tech vendor compliance.</td>
        </tr>
        <tr>
            <td>Head of Compliance & Risk</td>
            <td>Owns the Risk Management Framework. Leads implementation, monitoring, regulatory engagement, and internal training.</td>
        </tr>
        <tr>
            <td>Risk & Compliance Committee</td>
            <td>Reviews quarterly risk dashboards, approves changes to policies, and ensures risk mitigation aligns with platform growth.</td>
        </tr>
        <tr>
            <td>Staff & Contractors</td>
            <td>Must understand and comply with risk controls relevant to their roles. Required to report suspicious activity and policy violations.</td>
        </tr>
    </table>

    <h3>2.3 Escalation Protocols</h3>
    <ul>
        <li>Operational risks (e.g., failed transactions, data errors) are escalated to department heads within 24 hours</li>
        <li>Compliance or legal risks (e.g., KYC issues, sanctions violations) are escalated to the Head of Compliance within 12 hours</li>
        <li>Critical risks (e.g., smart contract failure, data breach, regulatory notice) are reported to the CEO and Board within 6 hours</li>
    </ul>
    <p>All incidents are logged in the Risk Register with details of detection, response, resolution, and lessons learned.</p>

    <h3>2.4 Risk Governance Tools</h3>
    <p>To support governance and visibility, Penthian maintains:</p>
    <ul>
        <li>A centralized Risk Register updated monthly</li>
        <li>Board-level Risk Dashboards with color-coded scoring and trend analysis</li>
        <li>A Whistleblower Policy and anonymous reporting channel</li>
        <li>Quarterly risk awareness workshops for staff and contractors</li>
    </ul>

    <h2>3. Risk Identification & Classification</h2>
    <p>Proactive risk identification is the foundation of effective risk management. At Penthian, we classify and monitor risks across all dimensions of the business — financial, operational, legal, technological, and reputational — to ensure nothing is overlooked as we scale our tokenization marketplace globally.</p>

    <h3>3.1 Risk Identification Methods</h3>
    <p>Risks are identified through multiple channels:</p>
    <ul>
        <li>Internal workshops and staff self-assessments</li>
        <li>Incident and complaint analysis</li>
        <li>Compliance reviews and regulatory updates</li>
        <li>Audit findings (internal and external)</li>
        <li>Smart contract code reviews and testnet deployments</li>
        <li>Real-time monitoring dashboards (KYC, wallet behavior, on-chain activity)</li>
        <li>Market intelligence (e.g., changes in crypto regulation or blockchain exploits)</li>
    </ul>
    <p>Identified risks are recorded in the central Risk Register, with details on origin, impact, and likelihood.</p>

    <h3>3.2 Risk Categories</h3>
    <p>Penthian classifies risks under the following major categories:</p>
    <table>
        <tr>
            <th>Category</th>
            <th>Description</th>
            <th>Examples</th>
        </tr>
        <tr>
            <td>Operational</td>
            <td>Failures in internal processes, systems, or people</td>
            <td>Transaction delays, payout errors, staff misconduct</td>
        </tr>
        <tr>
            <td>Technology / Cybersecurity</td>
            <td>Risks related to IT infrastructure, blockchain, or smart contracts</td>
            <td>Hacking, downtime, wallet compromise, DDoS</td>
        </tr>
        <tr>
            <td>Compliance / Legal</td>
            <td>Breaches of laws, regulations, or license terms</td>
            <td>AML failures, KYC lapses, unlicensed activity</td>
        </tr>
        <tr>
            <td>Market / Financial</td>
            <td>Economic shifts or investor behavior affecting revenue or liquidity</td>
            <td>Market volatility, liquidity crunch, token price drops</td>
        </tr>
        <tr>
            <td>Strategic</td>
            <td>Poor business decisions or misaligned growth strategies</td>
            <td>Entering high-risk jurisdictions, overexposure to one asset class</td>
        </tr>
        <tr>
            <td>Reputation</td>
            <td>Loss of investor trust or media scrutiny</td>
            <td>Data leaks, lawsuits, platform misuse</td>
        </tr>
