    <p>KRIs are reviewed in every quarterly report and help inform risk escalation decisions.</p>

    <h3>6.5 Whistleblower & Escalation Channels</h3>
    <p>To encourage a transparent culture of accountability:</p>
    <ul>
        <li>Penthian maintains a whistleblower email and anonymous form</li>
        <li>All staff are trained to report red flags, conflicts of interest, or breaches</li>
        <li>High-risk issues are escalated directly to the Head of Compliance and CEO</li>
    </ul>
    <p>The whistleblower program is covered under our Internal Controls and Code of Conduct policies.</p>

    <h2>7. Business Continuity & Incident Response</h2>
    <p>Penthian’s ability to withstand and recover from operational disruptions — whether caused by cyberattacks, system failures, legal action, or force majeure — is central to maintaining platform trust and investor protection. This section outlines our preparedness strategy to ensure resilience and rapid response in the face of unforeseen events.</p>

    <h3>7.1 Objectives</h3>
    <ul>
        <li>Protect investor assets and sensitive data</li>
        <li>Ensure platform uptime and transactional continuity</li>
        <li>Minimize legal, financial, and reputational fallout</li>
        <li>Satisfy FSC regulatory expectations for operational resilience</li>
        <li>Restore business operations within target Recovery Time Objectives (RTOs)</li>
    </ul>

    <h3>7.2 Continuity Framework</h3>
    <p>The Business Continuity Plan (BCP) and Disaster Recovery Plan (DRP) are embedded into daily operations, covering:</p>
    <ul>
        <li>Tokenization services</li>
        <li>Wallet services</li>
        <li>Smart contract interactions</li>
        <li>KYC/AML infrastructure</li>
        <li>Administrative systems</li>
        <li>Customer support and investor communications</li>
    </ul>
    <p>These plans are tested at least once per year through live drills and tabletop simulations.</p>

    <h3>7.3 Incident Response Lifecycle</h3>
    <p>When an incident is detected, Penthian follows this protocol:</p>
    <ol>
        <li><strong>Detection & Verification</strong>
            <ul>
                <li>Monitoring systems raise an alert</li>
                <li>Compliance/IT teams verify the issue’s authenticity</li>
            </ul>
        </li>
        <li><strong>Classification & Severity Assessment</strong>
            <ul>
                <li>Categorize the incident (e.g., smart contract bug, wallet exploit, regulatory breach)</li>
                <li>Rate severity using predefined impact thresholds</li>
            </ul>
        </li>
        <li><strong>Containment & Escalation</strong>
            <ul>
                <li>Isolate affected services</li>
                <li>Notify CEO, Board, and Compliance Officer</li>
                <li>Communicate with FSC within 72 hours if material</li>
            </ul>
        </li>
        <li><strong>Remediation & Recovery</strong>
            <ul>
                <li>Deploy backup systems or hot-failover environments</li>
                <li>Patch smart contracts if necessary (time-locked governance)</li>
                <li>Resume normal service after testing</li>
            </ul>
        </li>
        <li><strong>Post-Incident Review</strong>
            <ul>
                <li>Document cause, response, lessons learned</li>
                <li>Update controls and risk scores</li>
                <li>Share summary with stakeholders if appropriate</li>
            </ul>
        </li>
    </ol>

    <h3>7.4 Incident Severity Tiers</h3>
    <table>
        <tr>
            <th>Tier</th>
            <th>Description</th>
            <th>Response Time</th>
            <th>Escalation</th>
        </tr>
        <tr>
            <td>1 – Low</td>
            <td>No user impact</td>
            <td>48 hours</td>
            <td>Internal fix</td>
        </tr>
        <tr>
            <td>2 – Medium</td>
            <td>User friction, minor downtime</td>
            <td>12–24 hours</td>
            <td>Risk Committee</td>
        </tr>
        <tr>
            <td>3 – High</td>
            <td>Platform-wide impact or regulatory breach</td>
            <td>&lt; 6 hours</td>
            <td>CEO, Board, FSC</td>
        </tr>
    </table>

    <h3>7.5 Stakeholder Communication</h3>
    <p>If an incident affects user funds, system availability, or asset integrity, Penthian will:</p>
    <ul>
        <li>Notify users via email, app banner, and/or status page</li>
        <li>Provide real-time updates and recovery expectations</li>
        <li>Open dedicated support channels</li>
        <li>Publish a public post-mortem (where appropriate)</li>
    </ul>

    <h3>7.6 Testing & Review</h3>
    <ul>
        <li>DRP and BCP plans are tested annually, including cross-department drills</li>
        <li>After-action reports are reviewed by the Risk Committee</li>
        <li>Updates are logged and distributed via internal documentation tools (e.g., Notion, Confluence)</li>
    </ul>

