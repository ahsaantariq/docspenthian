    <h2>5. Risk Mitigation & Control Measures</h2>
    <p>Effective mitigation ensures that identified risks are not just tracked but proactively controlled through preventative, detective, and corrective actions. At Penthian, risk controls are tailored to the unique nature of blockchain infrastructure, tokenized assets, investor flows, and FSC compliance.</p>

    <h3>5.1 Types of Controls</h3>
    <p>Penthian implements the following types of risk controls:</p>
    <p><strong>Preventative Controls</strong></p>
    <ul>
        <li>KYC/AML enforcement using Veriff integration</li>
        <li>Mandatory two-factor authentication (2FA) for platform users and staff</li>
        <li>Smart contract audit reviews and automated deployment protocols</li>
        <li>Role-based access control (RBAC) for admin and backend systems</li>
        <li>Transaction validation checks (asset caps, jurisdiction blocks, etc.)</li>
    </ul>
    <p><strong>Detective Controls</strong></p>
    <ul>
        <li>Real-time wallet behavior monitoring (e.g., abnormal withdrawals, multi-accounting)</li>
        <li>Blockchain analytics integration (e.g., Chainalysis or equivalent)</li>
        <li>Internal log reviews and transaction discrepancy alerts</li>
        <li>Scheduled internal audits and compliance checks</li>
        <li>Periodic third-party penetration testing</li>
    </ul>
    <p><strong>Corrective Controls</strong></p>
    <ul>
        <li>Smart contract upgrade plans and rollback paths</li>
        <li>Incident response procedures for security or compliance breaches</li>
        <li>Immediate temporary suspension of flagged accounts</li>
        <li>Escalation and communication protocols to address stakeholder concerns</li>
    </ul>

    <h3>5.2 Key Mitigating Measures by Risk Category</h3>
    <table>
        <tr>
            <th>Risk Category</th>
            <th>Mitigation Measures</th>
        </tr>
        <tr>
            <td>Operational</td>
            <td>SOPs for asset onboarding, investor payouts, admin actions. Backup infrastructure and BCP drills.</td>
        </tr>
        <tr>
            <td>Cybersecurity</td>
            <td>Firewalls, intrusion detection systems, encryption protocols, segregated wallets, cold storage for treasury assets.</td>
        </tr>
        <tr>
            <td>Smart Contracts</td>
            <td>Multi-layer audit: internal, external (e.g., CertiK/Trail of Bits), testnet simulations, time-locked deployment.</td>
        </tr>
        <tr>
            <td>Compliance</td>
            <td>Automated KYC/AML with customizable rule sets. Ongoing screening, sanction checks, and reporting workflows.</td>
        </tr>
        <tr>
            <td>Financial/Market</td>
            <td>Token caps, liquidity pooling logic, risk disclosure to users, stress testing under low liquidity scenarios.</td>
        </tr>
        <tr>
            <td>Third-Party Vendors</td>
            <td>Vendor due diligence, SLAs, cybersecurity clauses, and annual performance reviews.</td>
        </tr>
        <tr>
            <td>Geopolitical</td>
            <td>Jurisdiction-based restrictions, flexible operating model (e.g., non-custodial approach), contingency partners.</td>
        </tr>
    </table>

    <h3>5.3 Defense in Depth Strategy</h3>
    <p>Penthian employs a multi-layered defense model that includes:</p>
    <ol>
        <li><strong>Frontline controls:</strong> Tech tools, blockchain rules, user authentication</li>
        <li><strong>System controls:</strong> Smart contract logic, token standards (e.g., ERC-3643 permissioning)</li>
        <li><strong>Policy controls:</strong> Onboarding rules, marketplace guidelines, internal manuals</li>
        <li><strong>Oversight controls:</strong> Audit trails, monitoring dashboards, compliance reviews</li>
        <li><strong>Recovery controls:</strong> Incident response, BCP, legal notifications</li>
    </ol>
    <p>This layered strategy ensures that no single point of failure can compromise investor assets or platform operations.</p>

    <h3>5.4 Control Ownership & Accountability</h3>
    <p>Each key risk and control is assigned a responsible person or team, typically:</p>
    <ul>
        <li><strong>CTO:</strong> Technical controls, smart contracts, devOps</li>
        <li><strong>Head of Compliance & Risk:</strong> Regulatory and AML controls</li>
        <li><strong>Product/Marketplace Teams:</strong> Token listing, platform behavior controls</li>
        <li><strong>Legal Advisor:</strong> Licensing, jurisdictional compliance, and token structure risks</li>
    </ul>
    <p>Accountability is documented in the Risk Register, updated monthly.</p>

    <h3>5.5 Continuous Improvement</h3>
    <p>Controls are:</p>
    <ul>
        <li>Reviewed quarterly and updated post-incident or audit</li>
        <li>Stress-tested against emerging threats</li>
        <li>Benchmarked against FSC requirements, global best practices, and ISO standards (ISO/IEC 27001, 31000)</li>
        <li>Integrated with our internal culture of ownership, transparency, and stewardship</li>
    </ul>

    <h2>6. Risk Monitoring & Reporting</h2>
    <p>Consistent and transparent risk monitoring ensures that emerging threats, control failures, or compliance gaps are quickly identified and escalated. At Penthian, risk monitoring is continuous, data-driven, and integrated across technology and operations.</p>

    <h3>6.1 Real-Time Monitoring Systems</h3>
    <p>Penthian employs various tools and processes to monitor risk across its virtual asset ecosystem:</p>
    <ul>
        <li><strong>Blockchain Activity Monitors:</strong> To flag irregular wallet behavior, suspicious transfers, or network-level threats</li>
        <li><strong>Transaction Analytics Dashboards:</strong> To review real-time volume, abnormal trades, and asset movement anomalies</li>
        <li><strong>KYC/AML Alerts (via Veriff):</strong> To detect flagged users, mismatched documentation, or high-risk geographies</li>
        <li><strong>Admin Panel Logs:</strong> To track backend changes, login attempts, and permission escalations</li>
        <li><strong>Uptime & Infrastructure Alerts:</strong> For smart contract or platform downtime, hosted services, and API responses</li>
    </ul>
    <p>These systems generate alerts, reports, and flags that are triaged by the compliance and tech teams.</p>

    <h3>6.2 Internal Risk Reviews</h3>
    <p>Internal risk reviews are conducted:</p>
    <ul>
        <li>Monthly by the Risk & Compliance team</li>
        <li>Quarterly by the Board Risk Committee</li>
        <li>Immediately in response to major incidents or flagged threats</li>
    </ul>
    <p>Key deliverables include:</p>
    <ul>
        <li>Risk heat maps and register updates</li>
        <li>Incident logs and remediation reports</li>
        <li>Trend analysis of recurring or emerging risks</li>
        <li>Effectiveness review of existing controls</li>
    </ul>

    <h3>6.3 External Risk Reporting</h3>
    <p>Penthian proactively communicates relevant risks and status updates to:</p>
    <ul>
        <li><strong>Regulators (FSC):</strong> Immediate notification of any material breaches, cyber incidents, or systemic platform failures (within 72 hours)</li>
        <li><strong>Auditors:</strong> All audit logs, smart contract changes, and compliance reports</li>
        <li><strong>Investors (via Platform Updates):</strong> Any known platform risks, market conditions, or token-related disruptions</li>
        <li><strong>Partners & Vendors:</strong> Where integration or dependency risk may affect service</li>
    </ul>
    <p>All communications are logged for accountability and shared securely with authorized stakeholders.</p>

