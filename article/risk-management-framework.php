     <h2>1. Purpose & Objectives</h2>

    <h3>1.1 Purpose</h3>
    <p>The purpose of this Risk Management Framework is to define a structured, transparent, and proactive approach to identifying, assessing, mitigating, monitoring, and reporting risks that may impact the operations, assets, clients, and reputation of Penthian Ltd, a licensed Virtual Asset Marketplace (Class S) under the Financial Services Commission (FSC) of Mauritius.</p>
    <p>This framework ensures that risk management is embedded across all layers of the organization — from strategic decisions to operational processes — and that it remains aligned with our legal obligations, technological infrastructure, and ethical values.</p>

    <h3>1.2 Key Objectives</h3>
    <p>The key objectives of this Risk Management Framework are to:</p>
    <ul>
        <li><strong>Ensure Compliance:</strong> Meet all FSC regulatory expectations under the Virtual Asset and Initial Token Offering Services (VAITOS) Act, AML/CFT laws, and applicable data protection regulations.</li>
        <li><strong>Protect Stakeholders:</strong> Safeguard the interests of investors, asset issuers, token holders, partners, and the broader ecosystem by anticipating and mitigating risks before they escalate.</li>
        <li><strong>Embed Governance:</strong> Integrate risk management into Penthian’s corporate governance structure, with clear roles and responsibilities assigned to leadership, compliance, operations, and technology teams.</li>
        <li><strong>Support Innovation Securely:</strong> Enable innovation in asset tokenization and blockchain technology while managing associated cyber, legal, financial, and operational risks.</li>
        <li><strong>Build Trust & Reputation:</strong> Foster trust in Penthian as a responsible, resilient, and principled marketplace guided by transparency, integrity, and the fruits of the Spirit (love, joy, peace, patience, kindness, goodness, faithfulness, gentleness, and self-control).</li>
        <li><strong>Promote Resilience:</strong> Ensure business continuity and the ability to adapt quickly in the face of disruption, market volatility, security incidents, or regulatory change.</li>
    </ul>

    <h3>1.3 Scope of Application</h3>
    <p>This framework applies to all:</p>
    <ul>
        <li>Penthian Ltd operations, systems, and business activities globally</li>
        <li>Staff, consultants, and third-party vendors</li>
        <li>Tokenization and trading of real estate, SME shares, or other digital assets</li>
        <li>Interactions with smart contracts, wallets, investor dashboards, APIs, and on-chain protocols</li>
        <li>Strategic decisions related to new product launches, geographic expansion, or partnerships</li>
    </ul>

    <h3>1.4 Alignment With Core Values</h3>
    <p>Penthian believes effective risk management must be grounded in our core belief system. We operate not only to maximize investor returns, but also to uphold ethical stewardship, responsible leadership, and community empowerment.</p>
    <p>Our decisions — including those related to risk — are guided by biblical virtues and kingdom-minded leadership. This foundation enhances our risk culture by ensuring that the pursuit of growth is never separated from the principles of wisdom, accountability, and love for others.</p>

    <h2>2. Risk Governance Structure</h2>
    <p>Effective risk management requires clear leadership, defined responsibilities, and a culture of ownership at every level of the organization. This section outlines the governance structure Penthian Ltd uses to oversee and manage risk across all business and technology functions.</p>

    <h3>2.1 Risk Oversight Model</h3>
    <p>Penthian adopts a Three Lines of Defense Model to ensure accountability and independence in its risk management activities:</p>
    <p><strong>First Line of Defense</strong></p>
    <p><strong>Operational Management</strong></p>
    <ul>
        <li>Frontline business units, including product, tech, asset onboarding, investor services</li>
        <li>Responsible for identifying, assessing, and managing day-to-day risks</li>
        <li>Required to escalate emerging risks and incidents</li>
    </ul>
    <p><strong>Second Line of Defense</strong></p>
    <p><strong>Compliance, Risk, and Legal</strong></p>
    <ul>
        <li>Owns the Risk Management Framework</li>
        <li>Conducts independent risk assessments and monitors control effectiveness</li>
        <li>Provides guidance on risk policies, regulatory compliance, and investor protection</li>
    </ul>
    <p><strong>Third Line of Defense</strong></p>
    <p><strong>Internal & External Audit</strong></p>
    <ul>
        <li>Provides independent assurance to the Board and regulators</li>
        <li>Reviews risk controls, internal systems, and governance processes</li>
        <li>Ensures compliance with FSC, AML/CFT, IT, and cybersecurity obligations</li>
    </ul>

    <h3>2.2 Key Risk Governance Roles</h3>
    <table>
        <tr>
            <th>Role</th>
            <th>Responsibilities</th>
        </tr>
        <tr>
            <td>Board of Directors</td>
