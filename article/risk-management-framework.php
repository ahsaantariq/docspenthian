    <h2>9. Framework Review & Update Procedures</h2>
    <p>Maintaining a dynamic and responsive risk framework is essential as Penthian grows, expands into new markets, and adapts to regulatory, technological, and operational change. This section outlines how the Risk Management Framework is reviewed, updated, and governed over time.</p>

    <h3>9.1 Review Frequency</h3>
    <p>Penthian commits to reviewing and updating this Risk Management Framework:</p>
    <ul>
        <li>Annually, as part of the Board Risk Committee’s scheduled review cycle</li>
        <li>Immediately, following a material incident, regulatory update, or platform-level change (e.g., protocol migration, token model adjustment)</li>
        <li>Post-Audit, if recommended by internal or external auditors</li>
        <li>On Demand, if required by the Financial Services Commission (FSC) of Mauritius or other relevant authorities</li>
    </ul>

    <h3>9.2 Review Ownership</h3>
    <p>The review process is managed by the Head of Compliance & Risk, who is responsible for:</p>
    <ul>
        <li>Leading the risk re-assessment process</li>
        <li>Coordinating input from key departments (tech, legal, operations, product, investor services)</li>
        <li>Ensuring the Risk Register and KRIs reflect current realities</li>
        <li>Submitting the revised Framework to the CEO and Board for final approval</li>
    </ul>

    <h3>9.3 Stakeholder Engagement</h3>
    <p>To ensure this framework remains practical and effective, Penthian involves:</p>
    <ul>
        <li><strong>Board Members:</strong> For strategic oversight and appetite alignment</li>
        <li><strong>Executive Team:</strong> For operational alignment and cross-functional feedback</li>
        <li><strong>Staff & Contractors:</strong> Through surveys and risk workshops</li>
        <li><strong>Third-Party Auditors:</strong> To independently validate the Framework's integrity</li>
        <li><strong>Regulators (FSC):</strong> To incorporate regulatory expectations and directives</li>
    </ul>

    <h3>9.4 Version Control</h3>
    <p>Each version of the Risk Management Framework includes:</p>
    <ul>
        <li>Version Number</li>
        <li>Date of Approval</li>
        <li>Summary of Key Changes</li>
        <li>List of Contributors/Reviewers</li>
    </ul>
    <p>Archived versions are securely stored and available for inspection by internal audit or regulators upon request.</p>

    <h3>9.5 Communication of Changes</h3>
    <p>Once reviewed and approved, all changes to this Framework are:</p>
    <ul>
        <li>Communicated via internal email and posted on the compliance portal</li>
