        </tr>
    </table>

    <h3>3.3 Blockchain-Specific Risks</h3>
    <p>As a blockchain-based platform, Penthian pays special attention to:</p>
    <ul>
        <li><strong>Smart Contract Vulnerabilities:</strong> Bugs, logic errors, upgradability flaws</li>
        <li><strong>Chain Dependency Risk:</strong> Downtime or forks in chains (e.g., Ethereum, Polygon)</li>
        <li><strong>Gas Fee Volatility:</strong> Impact on transaction throughput or user costs</li>
        <li><strong>Token Standard Risks:</strong> Misuse of ERC-3643 permissions, misuse of tokenized identity</li>
        <li><strong>Oracles & Bridges:</strong> Compromise of data feeds or cross-chain mechanisms</li>
        <li><strong>Custodial/Non-Custodial Wallet Risks:</strong> Lost keys, incorrect routing, phishing</li>
    </ul>
    <p>These are tracked separately in a Blockchain Risk Register maintained by the CTO.</p>

    <h3>3.4 Emerging Risks</h3>
    <p>Penthian actively monitors and classifies “early signal” risks such as:</p>
    <ul>
        <li>Changes in global tokenization regulation (e.g., MiCA, VARA, SEC updates)</li>
        <li>New attack vectors (e.g., LLM-enhanced phishing, AI-based exploits)</li>
        <li>Regulatory perception of DeFi/tokenized securities</li>
        <li>Shifts in investor sentiment toward digital ownership</li>
    </ul>
    <p>Emerging risks are assessed quarterly and presented to the Board via the Risk Committee.</p>

    <h2>4. Risk Assessment & Scoring Methodology</h2>
    <p>Once risks are identified, Penthian uses a structured assessment process to evaluate their potential impact and likelihood. This helps prioritize mitigation efforts and allocate resources effectively across the business.</p>

    <h3>4.1 Risk Assessment Model</h3>
    <p>Penthian employs a qualitative risk matrix model, assessing each risk based on:</p>
    <ul>
        <li><strong>Likelihood (Probability):</strong> How likely is the risk to occur?</li>
        <li><strong>Impact (Severity):</strong> What would be the consequence if the risk materializes?</li>
